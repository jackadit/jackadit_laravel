<h1>Introduction à Windows XP</h1>

<p class="notice remarque">Ce cours est bien entendu désuet, mais pour les nostalgiques et pour le travail que cela a demandé, je laisse ce cours en ligne ;-)</p>
<p>&nbsp;</p>
<p>L'objectif de ce cours est de proposer une série d'exercices pratiques visant à acquérir les compétences nécessaires pour subvenir aux différentes tâches d'administration liées à un environnement réseau sous Windows XP.</p>
<p>Ce cours ne se veut pas exhaustif, mais essaye humblement de contribuer à partager des connaissances acquises sur le "terrain" et au fil des formations ou pérégrinations sur le Web.</p>
<p>J'espère que vous y trouverez les réponses à vos questions.</p>
<p>Si vous deviez y decéler des erreurs, n'hésitez pas à m'envoyer un mail. J'essayerai de corriger le plus rapidement possible.</p>
<p>Bonne lecture et bonne formation.</p>
