<h2>Contexte utilisateur</h2>
<p>Vous l'aurez sûrement compris, quand on parle d'accessibilité, ce n'est évidement pas limité aux aveugles, malvoyants, daltoniens, handicapés physiques ou mentaux... mais il y a aussi ceux qui font face à des problèmes d'accessibilité technologique : certains ne peuvent pas voir les images, d'autres n'ont pas le java ou le javascript, certains n'ont pas de souris et d'autres encore n'ont que peu de bande passante.</p>
<p>On estime que 5,5 millions de personnes, environ, sont atteintes d'une invalidité en France (près d'un 1/5 de la population mondiale présente un type de handicap reconnu) et le nombre de seniors susceptibles de souffrir de déficiences diverses augmente rapidement.</p>
<p>La <a rel="nofollow" class="external" target="_blank" href="https://www.legifrance.gouv.fr/loda/id/JORFTEXT000000809647/">loi n°2005-102 du 11 février 2005</a> pour l'égalité des droits et des chances, la participation et la citoyenneté des personnes handicapées, fait de l'accessibilité une exigence pour tous les services de communication publique en ligne de l'État, des collectivités territoriales et des établissements publics qui en dépendent. Si le réseau Internet et les ressources du Web ne sont pas accessibles aux personnes ayant des incapacités et aux personnes âgées, cela devient un facteur d'aggravation du handicap ou des situations de fragilité.</p>

<p>De nombreux utilisateurs peuvent être amenés à opérer dans des contextes très différents de celui de l'internaute moyen :</p>
<ul>
	<li>ils peuvent ne pas être en mesure de voir, entendre, se mouvoir,</li>
	<li>ils peuvent avoir des difficultés à lire ou à comprendre le contenu textuel. Ils peuvent être dans l'impossibilité d'utiliser un clavier ou une souris,</li>
	<li>ils peuvent avoir un écran n'affichant que le texte, qu'un nombre réduit de couleurs, de petite taille ou avec une résolution particulière,</li>
	<li>ils peuvent avoir une version ancienne d'un logiciel de consultation, un logiciel entièrement différent, ou un système d'exploitation différent,</li>
	<li>ils peuvent être amenés à se connecter depuis des terminaux mobiles, et accéder aux services en ligne en bas-débit ou avec des écrans de petite taille.</li>
</ul>

<h2>Personnalisation ou accessibilité ?</h2>

	<p>En tenant compte de tous les types de handicaps ou de déficiences, nous pouvons être amené à proposer des alternatives d'affichage.</p>
	<p>Comme nous venons de le voir précédemment, le handicap n'est pas le seul critère d'accessibilité. Les usages généralisés des smartphones et autres tablettes, nous obligent également à penser à ce type de supports lors de nos développements web.</p>
	<p>Ainsi peut-on parler de personnalisation environnementale plus que d'accessibilité lié au handicap.</p>

<p class="h3">Moteur de recherche = aveugle !</p>

	<p>Un dernier point, et pas le moindre, je l'aborderai de la manière suivante. Si vous créer un site vous avez certainement envie qu'il soit visité. Et pour cela, une des conditions, c'est d'être bien référencé au niveau des moteurs de recherche. Or un moteur de recherche est aveugle !</p>
	<p>Si vous faite attention à l'accessibilité de vos sites, vous ferez en même temps un référencement naturel optimisé pour les moteurs de recherche.</p>