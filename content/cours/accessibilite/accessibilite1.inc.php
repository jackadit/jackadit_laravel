<h3>Handicaps visuels</h3>

<h4>Vision floue</h4>
    <h5>Cataracte</h5>
    <p>Solution : adapter la taille des textes pour garantir le confort de lecture. Modifier le contraste entre la couleur du texte et la couleur du fond.</p>

    <h5>Dégénérescence maculaire liée à l'âge (DMLA)</h5>
    <p>Solution : augmenter fortement la taille des textes.</p>

    <h5>Presbytie, hypermétropie, myopie, astigmatie</h5>
    <p>Solution : augmenter légèrement la taille des textes. Modifier le contraste entre la couleur du texte et la couleur du fond.</p>

    <h5>Malvoyance</h5>
    <p>Solution : augmenter fortement la taille des textes. Modifier le contraste entre la couleur du texte et la couleur du fond.</p>

<h4>Daltonisme</h4>
	<h5>Deutéranopie : perception du bleu et du rouge, mais pas du vert</h5>
	<p>Solution : modifier les couleurs pour qu'elles soient correctement distinguées par l'usager atteint de deutéranopie.</p>

	<h5>Protanopie : perception du vert et du bleu, mais pas du rouge</h5>
	<p>Solution : modifier les couleurs pour qu'elles soient correctement distinguées par l'usager atteint de protanopie.</p>

	<h5>Tritanopie : non-perception de la lumière bleue ce qui entraîne des confusions entre le bleu et le gris, le vert et le bleu, etc.</h5>
	<p>Solution : modifier les couleurs pour qu'elles soient correctement distinguées par l'usager atteint de tritanopie.</p>

	<h5>Achromatie : perception partielle avec une distinction du bleu ou une cécité complète des couleurs. Dans ce cas, la personne ne voit qu’en noir et blanc, avec des nuances de gris.</h5>
	<p>Solution : modifier le contraste entre la couleur du texte et la couleur du fond.</p>

<h3>Handicaps cognitifs</h3>

	<h4>Dyslexie</h4>
	<p>Solution : utiliser une police plus lisible.</p>

	<h4>Épilepsie photosensible</h4>
	<p>Solution : permettre de stopper les contenus animés.</p>

	<h4>Sénior</h4>
	<p>Solution : augmenter la taille du texte et modifier la police de caractères.</p>

	<h4>Confort</h4>
	<p>Solution : augmenter le contraste, modifier la police de caractères et augmenter la taille des textes.</p>


<h3>Handicaps temporaires de la vision</h3>

	<h4>Vision Floue</h4>
