<h1>Pourquoi l'accessibilité Web ?</h1>

<p>Si dans l'inconscient collectif, on a tendance à considérer l'accessibilité web comme étant principalement à l'usage des non-voyants, je voudrais ici mettre un peu plus de clarté derrière cette notion d'accessibilité et plus largement expliquer ce qu'on entend par handicap quand il s'agit d'accéder à un site internet ou à un document en ligne.</p>
<p>Pour cela, il est nécessaire dans un premier temps de clarifier les définitions suivantes.</p>

<h2>Définitions clés</h2>
<dl>
	<dt>La déficience (synonyme : infirmité)</dt>
		<dd>La déficience du fonctionnement d'un organe ou d'un système est évaluée par rapport au fonctionnement habituel de cet organe.</dd>
		<p class="notice exemple">Exemple : Une pathologie de l'articulation des doigts peut limiter leur extension, causant une infirmité (ou déficience)</p>

	<dt>L'incapacité</dt>
		<dd>Représente les conséquences de la déficience d'un organe ou d'un système sur le fonctionnement de l'individu en termes de limitation de fonctions ou de restriction d'activités.</dd>
		<p class="notice exemple">Exemple : Une personne qui a un diabète insulino dépendant apparu dans l'enfance et qui est capable de vivre "normalement" ; elle est soumise à des contraintes mais n'a pas d'incapacité. Elle n'est pas handicapée.</p>

	<dt>Le handicap (ou désavantage)</dt>
		<dd>Le handicap est l'écart ou l'intervalle entre l'incapacité de l'individu et les ressources personnelles, matérielles et sociales dont il dispose pour pallier ces incapacités. Cet écart lui confère un désavantage social.</dd>
</dl>

<p>Ainsi peut-on constater que les personnes pouvant être impactées par l'accessibilité ne se limitent et de loin pas aux non-voyants.

<h2>Les différents types de handicaps</h2>

<p>Je vais lister les principales catégories de handicap que l'on peut rencontrer et dont n'importe quel développeur devrait avoir conscience lorsqu'il crée un site internet.</p>
