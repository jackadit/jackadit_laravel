<h1>Exercice d'application.</h1>
<h2>Exercices.</h2>
<p>Pour les exercices, nous allons nous baser sur un site vitrine comportant une page principale et une page de contact.</p>
<p>Il est demandé d'utiliser un système de versionning pour partager les sources.</p>
<p>Votre site devra respecter au minimum les directives suivantes :</p>
<ol><li>Choisir un DOCTYPE et identifier votre langue</li>
	<li>Définir des titres de page qui ont une signification</li>
	<li>Prévoir des liens pour aller directement au contenu, à la barre de recherche, au menu</li>
	<li>Utiliser un fil d'ariane avec les liens</li>
	<li>Présenter votre contenu principal en premier</li>
	<li>Prévoir l'affichage d'un plan du site</li>
	<li>Définir des contrastes suffisants</li>
	<li>Utiliser des liens réels</li>
	<li>Ajouter des titres aux liens</li>
	<li>Éviter d'ouvrir de nouvelles fenêtres</li>
	<li>Définir les accronymes</li>		
	<li>Utiliser de vraies en-têtes de tableaux</li>
	<li>Fournir un résumé de tableau</li>
	<li>Utiliser de vraies listes (ou les simuler correctement)</li>
	<li>Fournir des équivalents texte aux images ayant un sens</li>
	<li>Utiliser des tailles de polices relatives</li>
	<li>Étiquetter les éléments de formulaire</li>
	<li>Permettre la recherche sur tout le contenu</li>
	<li>Créer une déclaration d'accessibilité</li>
	<li>Conclusion</li>
</ol>

