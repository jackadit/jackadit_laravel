<h2>Les tests unitaires.</h2>

Les tests unitaires sont reportés sous la forme d'un document annexe car le volume est trop important pour figurer ici de manière efficace et utilisable.<br>
Il s'agit de l'annexe 2 dont l'objectif est de fournir un ensemble de tests permettant de vérifier l'accessibilité des contenus Web. Il peut être utilisé lors de la mise en oeuvre des contenus pour tester le respect des conditions d'accessibilité ou dans le cadre de contenus existants pour mesurer leur niveau d'accessibilité.<br>
Cette annexe est donc structurée en douze chapitres thématiques qui sont :</p>
<ul>
<li>Cadres</li>
<li>Couleurs</li>
<li>Formulaires</li>
<li>Image</li>
<li>Multimédia</li>
<li>Navigation</li>
<li>Présentation</li>
<li>Scripts</li>
<li>Standards</li>
<li>Structure</li>
<li>Tableaux</li>
<li>Textes</li>
</ul>
<p>Chaque chapitre thématique regroupe des séries de tests. Chaque test est expliqué et relié au WCAG 2.0 au moyen d'une fiche dont la composition est détaillée ci-après. Le niveau de priorité en est déduit.</p>

<h2>Composition d'une fiche « test unitaire ».</h2>
<p>Une fiche est structurée de la manière suivante:</p>
<p><strong>Champ d'application</strong>: Indique les éléments HTML, CSS, javascript ou autre sur lesquels le test doit être appliqué.</p>

<p><strong>Procédure de test</strong>: Indique la méthodologie à suivre pour réaliser le test.</p>

<p><strong>Difficulté de test</strong>: Indique la difficulté estimée pour appliquer le test. 3 niveaux : facile, moyen, difficile.</p>

<p><strong>WCAG 2.0 Techniques</strong>: Cette partie fait référence aux techniques disponibles pour mettre en oeuvre l'accessibilité. 
Elles sont disponibles <a rel="nofollow" class="external" target="_blank" href="https://www.w3.org/TR/WCAG20-TECHS/">ici</a>. 
Les techniques sont regroupées en grands domaines listés ci-dessous :</p>
<ul>
<li>General Techniques (code G)</li>
<li>HTML and XHTML Techniques (code H)</li>
<li>CSS Techniques (code C)</li>
<li>Client-side Scripting Techniques (code SCR)</li>
<li>Server-side Scripting Techniques (code SVR)</li>
<li>SMIL Techniques (code SM)</li>
<li>Plain Text Techniques (code T)</li>
<li>ARIA Techniques (code ARIA)</li>
</ul>


<p><strong>WCAG 2.0 Failures</strong>: Cette partie fait référence aux défauts observés dans la mise en oeuvre de l'accessibilité. 
Ils sont disponibles <a rel="nofollow" class="external" target="_blank" href="https://www.w3.org/TR/WCAG20-TECHS/failures.html">ici</a> et numérotés de F1 à F89.</p>
<ul>
<li>Common Failures (code F)</li>
</ul>

<p><strong>Critère de succès</strong>: Lien vers le(s) critère(s) de succès correspondant dans le WCAG 2.0.</p>

<p><strong>Niveau WCAG déduit</strong></p>

<p>« Afin de répondre aux besoins de divers groupes et de différents contextes, trois niveaux de conformité ont été définis : A (le plus bas), AA et AAA (le plus élevé). »</p>
<p>Les critères de succès ont été associés à l'un des niveaux sur la base de divers facteurs dont la liste est proposée (en anglais) dans la page suivante :</p>
<p><a rel="nofollow" class="external" target="_blank" href="http://www.w3.org/TR/UNDERSTANDING-WCAG20/conformance.html#uc-levels-head">http://www.w3.org/TR/UNDERSTANDING-WCAG20/conformance.html#uc-levels-head</a></p>
<p>En tant que méthode d'application des WCAG 2.0, le RGAA propose une liste de tests unitaires. Dans la mesure où chaque technique WCAG 2.0 peut être associée à plusieurs critères de succès associés à des niveaux de conformité différents, les tests unitaires du RGAA ont été associés au niveau de conformité le plus bas.</p>
<p>Si le succès à un test est requis pour satisfaire à un critère de succès de niveau A et à un autre critère de succès de niveau AA, le test sera directement associé au niveau A. Dans ce cas le niveau WCAG 2.0 associé au test est appelé niveau déduit.</p>
<p>Exemple: le test 4 de la thématique structure « Présence d'une hiérarchie de titres complète » est associé aux critères de succès suivants :</p>
<ul type="circle">
<li>1.3.1: Information et relation (niveau A)</li>
<li>2.4.1 : Contourner des blocs (niveau A)</li>
<li>2.4.10: en-têtes de section (niveau AAA)</li>
</ul>
<p>Le niveau WCAG déduit de ce test est donc le niveau A.</p>
<p>Pour satisfaire les critères WCAG 2.0 obligatoires dans le RGAA, une page devra passer avec succès tous les tests de niveaux déduits A et AA. Il est également utile de vérifier le déploiement des tests de niveau AAA, qui ont une utilité certaine, bien que non essentielle pour l'accessibilité, sans pour autant que le succès à ces tests soit une obligation formelle.</p>

