<h1>Accessibilité : Thématiques et critères RGAA.</h1>
<h2>Qu'est ce que le RGAA ?</h2>
<p>Le RGAA (Référentiel général d'accessibilité pour les administrations) est un recueil de règles et de bonnes pratiques qui visent à améliorer
l'accessibilité des sites Web des administrations.</p>
<p>Il se fonde sur les normes et standards en vigueur, notamment le standard international WCAG 2.0.</p>
<p>Il préconise que les contenus Web soient « perceptibles, utilisables, compréhensibles et robustes ».</p>
<p>Le RGAA est un document conçu pour rendre service concrètement sur le terrain. Cela se traduit dans sa structure,
qui propose :</p>
<ul>
<li>une présentation des règles pour l'accessibilité des contenus Web (WCAG 2.0);</li>
<li>un guide d'accompagnement, destiné aux responsables des projets de mise en conformité;</li>
<li>des critères de succès, ensemble d'exigences que doit respecter un site Web pour être accessible;</li>
<li>des tests de conformité, qui permettent de vérifier l'accessibilité des contenus Web.</li>
</ul>


<h2>Les objectifs du RGAA.</h2>
<p>Le RGAA a pour objectif de favoriser l'accessibilité des contenus diffusés sous forme numérique à travers les
canaux Web, téléphonie et télévisuel.<br />
Dans sa version actuelle :</p>
<ul>
<li>il guide les autorités administratives dans l'adoption et la mise en œuvre de bonnes pratiques assurant l'accessibilité
des contenus Web à tous les publics, quels que soient leurs équipements et leurs aptitudes physiques ou mentales;</li>
<li>il offre aux autorités administratives la possibilité de mesurer la conformité de leurs contenus Web au RGAA et, donc, aux standards
internationaux d'interopérabilité (WCAG 2.0);</li>
<li>il impose aux autorités administratives en charge d'un site Internet d'établir et de mettre en ligne l'attestation de conformité au RGAA. </li>
</ul>

