<li>Le <a rel="nofollow" class="external" target="_blank" href="http://www.legifrance.gouv.fr/affichTexte.do?cidTexte=JORFTEXT000020616980&dateTexte=&categorieLien=id">décret n° 2009 546 du 14 mai 2009</a> fixe le cadre du référentiel d'accessibilité des services de communication publique en ligne.</li>
<li>L'<a rel="nofollow" class="external" target="_blank" href="http://www.legifrance.gouv.fr/affichTexte.do?cidTexte=JORFTEXT000021208630&dateTexte=&categorieLien=id">arrêté du 21 octobre 2009</a> met en vigueur le référentiel d'accessibilité des services de communication publique en ligne</li>
</ul>

<h2>À qui s'adresse le RGAA ?</h2>
<p>Le RGAA s'adresse en priorité aux autorités administratives :
ministères, établissements publics administratifs, organismes de sécurité sociale, collectivités locales, etc.</p>
<p>Au sein de ces autorités, les utilisateurs du RGAA sont :</p>
<ul>
<li>les responsables des services de communication en ligne (directeurs de la communication, webmestres, graphistes, ergonomes...),</li>
<li>les responsables de systèmes d'information ou de téléservices,</li>
<li>ainsi que les chefs de projet et les développeurs.
</li>
</ul>

<p>Le RGAA concerne également des acteurs tels que les prestataires travaillant sur les systèmes d'information
de l'administration, les éditeurs de services de communication en ligne, etc.
</p>

<h3>De qui émane le RGAA ?</h3>
<p>La conception du RGAA est le résultat du travail conjoint de la direction générale de la modernisation de l'État (DGME)
et de la délégation interministérielle aux personnes handicapées (DIPH) et d'experts de l'accessibilité des secteurs public et privé.</p>
<p>Sa mise en œuvre sera assurée par le comité interministériel du handicap (CIH)</p>

 
<h2>Les critères de succès.</h2>

<p>Les critères de succès sont reportés sous la forme d'un document annexe car le volume est trop important pour figurer ici de manière efficace et utilisable.<br>
Il s'agit de l'annexe 1 qui a pour objectif de lister l'ensemble des conditions auxquelles tout contenu Web doit se conformer pour être accessible.</p>
<p>Reprenant l'organisation du document WCAG 2.0, cette annexe est structurée en quatre chapitres appelés « Principe » qui sont :</p>
<ul>
<li>Principe 1 : perceptible</li>
<li>Principe 2 : utilisable</li>
<li>Principe 3 : compréhensible</li>
<li>Principe 4 : robuste</li>
</ul>
<p>Chaque « Principe » regroupe une série de règles. Chaque règle est détaillée sous la forme d'un ou plusieurs « critères de succès » reliés aux tests unitaires.</p>
<p>Ce document est à inclure en annexe du cahier des charges de toute opération de mise en oeuvre ou de refonte de contenu Web.</p>

