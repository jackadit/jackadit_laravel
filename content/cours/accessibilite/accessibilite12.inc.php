  <dt class="label"><a name="contrast-ratiodef" id="contrast-ratiodef"></a>rapport de contraste</dt>
    <dd><p>(L1 + 0,05) / (L2 + 0,05), où</p>
      <ul>
        <li><p>L1 est la <a title="définition : luminosité relative" href="#relativeluminancedef" class="termref">luminosité relative</a>
          de la couleur la plus claire et </p>
        </li>
        <li><p>L2 est la <a title="définition : luminosité relative" href="#relativeluminancedef" class="termref">luminosité relative</a>
          de la couleur la plus sombre.</p>
        </li>
      </ul>
    </dd>

      <p class="notice remarque"><em>Note 1&nbsp;: </em>le rapport de contraste peut varier
      de 1 à 21 (communément écrit 1:1, 1 pour 1, à 21:1, 21 pour 1).</p>
      <p class="notice remarque"><em>Note 2&nbsp;: </em>étant donné que les auteurs ne
      contrôlent pas la configuration de l'utilisateur concernant le rendu du
      texte (par exemple le lissage de police ou l'anti-crénelage), le rapport
      de contraste du texte peut être évalué en désactivant
      l'anti-crénelage.</p>
      <p class="notice remarque"><em>Note 3&nbsp;: </em>en ce qui concerne les critères de
      succès 1.4.3 et 1.4.6, le contraste est mesuré en tenant compte de
      l'arrière-plan sur lequel le texte est normalement affiché. Si aucune
      couleur d'arrière-plan n'est spécifiée, il est considéré comme
      blanc.</p>
      <p class="notice remarque"><em>Note 4&nbsp;: </em>la couleur d'arrière-plan est la
      couleur spécifiée du contenu sur lequel le texte est normalement
      affiché. Il est considéré comme une erreur de ne pas définir une
      couleur d'arrière-plan lorsque la couleur du texte est spécifiée,
      parce que la couleur d'arrière-plan de l'utilisateur est inconnue et ne
      peut donc pas être évaluée pour vérifier si le contraste est
      suffisant. Pour la même raison, il est aussi considéré comme une
      erreur de ne pas définir la couleur du texte lorsqu'une couleur
      d'arrière-plan est spécifiée.</p>
      <p class="notice remarque"><em>Note 5&nbsp;: </em>lorsqu'il y a une bordure autour de
      la lettre, la bordure peut augmenter le contraste et serait utilisée
      dans le calcul du contraste entre la lettre et son arrière-plan. La
      couleur d'une bordure étroite autour de la lettre serait utilisée à la
      place de la lettre. Une bordure large autour de la lettre qui remplit
      l'espace dans lequel se découpe le détail de la lettre agit comme un
      halo et serait considérée comme un arrière-plan.</p>
      <p class="notice remarque"><em>Note 6&nbsp;: </em>la conformité aux WCAG devrait
      être évaluée pour les paires de couleurs spécifiées dans le contenu
      qu'un auteur s'attendrait à voir apparaître de façon adjacente dans
      une présentation habituelle. Les auteurs n'ont pas besoin de prendre en
      considération les présentations inhabituelles comme les changements de
      couleurs faits par l'agent utilisateur sauf si ces changements sont
      provoqués par le code de l'auteur.</p>



