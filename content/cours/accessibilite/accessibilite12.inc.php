        <li><p>se conforme au niveau déterminé et </p>
        </li>
        <li><p>fournit toutes les informations similaires et les mêmes <a title="définition : fonctionnalité" href="#functiondef" class="termref">fonctionnalités</a> dans la même <a title="définition : langue" href="#human-langdef" class="termref">langue</a> et</p>
        </li>
        <li><p>est aussi à jour que le contenu non conforme et </p>
        </li>
        <li><p>pour laquelle au moins l'une des affirmations suivantes est vraie&nbsp;:</p>
          <ol class="enumla">
            <li><p>la version conforme peut être atteinte à partir de la page
              non conforme via un <a title="définition : mécanisme" href="#mechanismdef" class="termref">mécanisme</a> <a title="définition : compatible avec l'accessibilité" href="#accessibility-supporteddef" class="termref">compatible
              avec l'accessibilité</a> ou</p>
            </li>
            <li><p>la version non conforme peut être atteinte seulement à partir de la version conforme ou </p>
            </li>
            <li><p>la version non conforme peut être atteinte seulement à partir d'une page conforme qui fournit aussi un mécanisme pour atteindre la version conforme.</p>
            </li>
          </ol>
        </li>
      </ol>
    </dd>

      <p class="notice remarque"><em>Note 1&nbsp;: </em>dans cette définition, «&nbsp;peut
      être atteinte seulement&nbsp;» signifie qu'il y a un mécanisme comme une
      redirection conditionnelle, qui empêche un utilisateur
      «&nbsp;d'atteindre&nbsp;» (de charger) la page non conforme à moins que
      l'utilisateur ne vienne justement de la version conforme de cette même page.</p>
      <p class="notice remarque"><em>Note 2&nbsp;: </em>la version de remplacement n'a pas
      besoin d'être appariée page par page avec la version originale (par
      exemple la version de remplacement conforme peut se présenter en
      plusieurs pages).</p>
      <p class="notice remarque"><em>Note 3&nbsp;: </em>si des versions sont proposées dans
      plusieurs langues, une version de remplacement conforme est donc requise pour chacune de ces langues.</p>
      <p class="notice remarque"><em>Note 4&nbsp;: </em>des versions de remplacement peuvent
      aussi être fournies afin d'accommoder différents environnements
      technologiques ou différents groupes d'utilisateurs. Chaque version
      devrait être aussi conforme que possible. Une version devrait être
      entièrement conforme afin de satisfaire à <a href="#cc1">l'exigence de conformité 1</a>.</p>
      <p class="notice remarque"><em>Note 5&nbsp;: </em>la version de remplacement conforme
      n'a pas besoin d'être située dans le périmètre de conformité ni
      même sur le même site Web tant qu'elle est aussi librement disponible que la version non conforme.</p>
      <p class="notice remarque"><em>Note 6&nbsp;: </em>une version de remplacement ne
      devrait pas être confondue avec un <a title="définition : contenu additionnel" href="#suppcontentdef" class="termref">contenu additionnel</a> qui s'ajoute à la page originale
      pour en améliorer la compréhension.</p>
      <p class="notice remarque"><em>Note 7&nbsp;: </em>permettre la configuration des
      préférences de l'utilisateur à l'intérieur du contenu afin de
      produire une version conforme est un mécanisme acceptable pour atteindre
      une autre version tant que la méthode utilisée pour configurer les préférences est compatible avec l'accessibilité.</p>

