<h1>Accessibilité : Glossaire.</h1>

<p>Cette section est <a title="définition : normative" href="#normativedef" class="termref">normative</a>. </p>
<dl>
  <dt class="label"><a name="abbreviationsdef" id="abbreviationsdef"></a>abréviation</dt>
    <dd><p>forme abrégée d'un mot, d'une expression ou d'un nom lorsque l'abréviation ne fait pas encore partie de la langue courante </p></dd>

      <p class="notice remarque"><em>Note 1&nbsp;: </em>ceci comprend les sigles et les acronymes formés à partir des initiales des mots où&nbsp;:</p>
      <ol class="">
        <li><p><strong>les sigles</strong> sont une forme abrégée d'un nom ou d'une expression constituée des lettres initiales des mots ou des syllabes contenues dans ce nom ou cette expression.</p>

          <p class="notice"><em>Remarque 1&nbsp;: </em>ils ne sont pas définies dans toutes les langues.</p>

          <p class="notice exemple"><em>Exemple 1&nbsp;: </em>SNCF est le sigle de la Société nationale des chemins de fer français. </p>
          <p class="notice exemple"><em>Exemple 2&nbsp;: </em>ESP est le sigle, en anglais, de <span xml:lang="en" lang="en"><em>extrasensory perception</em></span>, la perception extrasensorielle.</p>

        </li>
        <li><p><strong>les acronymes</strong> sont des abréviations formées à partir des premières lettres ou des parties d'autres mots (dans un nom ou une expression) et qui peuvent être prononcés comme un
          mot. </p>
          <p class="notice exemple"><em>Exemple&nbsp;: </em>ONU est un acronyme constitué des premières lettres de Organisation des Nations unies.</p>
        </li>
      </ol>
      <p class="notice remarque"><em>Note 2&nbsp;: </em>quelques sociétés ont adopté ce qui constituait un sigle ou un acronyme comme nom de leur société. Dans un tel cas, le nouveau nom de la société est constitué des lettres (par exemple, Ecma) et le mot n'est plus alors considéré comme une
      abréviation.</p>
    
  <dt class="label"><a name="useragentdef" id="useragentdef"></a>agent utilisateur</dt>
    <dd><p>tout logiciel qui récupère et présente le contenu Web aux utilisateurs</p></dd>

      <p class="notice exemple"><em>Exemple&nbsp;: </em>les navigateurs Web, les lecteurs de média, les modules d'extensions et les autres programmes — dont <a title="définition: technologie d'assistance" href="#atdef" class="termref">les technologies d'assistance</a> — qui aident à
      récupérer, restituer et interagir avec le contenu Web.</p>

    
  <dt class="label"><a id="larger-scaledef" name="larger-scaledef"></a> (texte) agrandi</dt>
    <dd>avec au minimum 18 points ou 14 points gras ou une taille de caractère équivalente pour les polices chinoises, japonaises ou coréennes (CJK).</dd>
 
      <p class="notice remarque"><em>Note 1&nbsp;: </em>les polices avec des traits extraordinairement fins ou des aspects et des caractéristiques inhabituels qui réduisent la reconnaissance de la forme des lettres sont plus difficiles à lire, spécialement à des niveaux de contrastes bas.</p>
      <p class="notice remarque"><em>Note 2&nbsp;: </em>la taille de caractère est la taille lorsque le contenu est affiché. Cela n'inclut pas le redimensionnement qui pourrait être fait par l'utilisateur.</p>
      <p class="notice remarque"><em>Note 3&nbsp;: </em>la taille réelle des caractères qu'un utilisateur voit est dépendante à la fois de la définition de la taille faite par l'auteur, du périphérique de restitution de l'utilisateur ou du paramétrage de l'agent utilisateur. Pour la majorité des polices de corps de texte, 14 et 18 points est sensiblement équivalent à 1,2 et 1,5 em ou à 120% et 150% de la taille par défaut du corps de texte (en considérant que la police de corps est à 100%), mais les auteurs seraient tenus de vérifier cela pour les polices particulières qui seraient utilisées. Quand les polices sont définies en unités relatives, la taille réelle du point est mesurée par l'agent utilisateur pour l'affichage. La taille d'un point doit être obtenue auprès de l'agent utilisateur ou calculée sur la même base de mesure que celle de l'agent utilisateur, lors de l'évaluation de ce critère de succès. Les utilisateurs qui ont une basse vision auront à choisir les paramètres appropriés.</p>
      <p class="notice remarque"><em>Note 4&nbsp;: </em>lors de l'utilisation de texte sans spécification de taille de caractère, la taille la plus petite utilisée dans les principaux navigateurs pour le texte dont la taille n'est pas spécifiée serait considérée comme une taille raisonnable pour la police. Si un titre de niveau 1 est restitué en 14pt gras ou plus sur les principaux navigateurs, alors il est raisonnable de considérer qu'il s'agit de texte agrandi. Le redimensionnement relatif peut être calculé de manière identique à partir de la taille par défaut.</p>
      <p class="notice remarque"><em>Note 5&nbsp;: </em>la taille 18 ou 14 points pour les textes en alphabet latin est définie à partir de la taille minimum pour les grands caractères (14pt) et de la taille standard plus grande (18pt). Pour les autres polices telles que les langues CJK, les tailles «&nbsp;équivalentes&nbsp;» seraient la taille de grand caractère minimum utilisée pour ces langues et la taille de grands caractères plus grande suivante.</p>

  <dt class="label"><a name="context-sensitivehelpdef" id="context-sensitivehelpdef"></a>aide contextuelle</dt>
    <dd>Texte d'aide qui fournit des informations relatives à la fonction actuellement utilisée.</dd>
    <p class="notice remarque"><em>Note&nbsp;: </em>des étiquettes claires peuvent jouer le rôle d'aide contextuelle.</p>
    
  <dt class="label"><a name="ambiguouslinkdef" id="ambiguouslinkdef"></a>ambigu pour tout utilisateur</dt>
    <dd>L'intention ne peut être déterminée à partir du lien et de toute l'information de la page Web présentée à l'utilisateur en même temps que ce lien. (c'est-à-dire qu'un lecteur sans limitations fonctionnelles ne connaîtrait pas la fonction d'un lien avant de l'activer).</dd>
      <p class="notice exemple"><em>Exemple&nbsp;: </em>le mot goyave dans la phrase suivante utilisé comme lien&nbsp;: «&nbsp;L'une des exportations importantes est la goyave&nbsp;». Ce lien pourrait conduire à une définition de la goyave, à un graphe présentant une liste des quantités de goyave exportées ou à une photo de gens récoltant la goyave. Jusqu'à ce que le lien soit activé, tout utilisateur est dans l'incertitude et une personne handicapée n'est donc pas désavantagée.</p>

  <dt class="label"><a name="asciiartdef" id="asciiartdef"></a>art ASCII</dt>
    <dd>dessin créé par une disposition spatiale de caractères ou glyphes (habituellement à partir des 95 caractères imprimables définis en ASCII).</dd>
  <dt class="label"><a name="audiodef" id="audiodef"></a>audio</dt>
    <dd>La technologie de reproduction des sons.</dd>

    <p class="notice remarque"><em>Note&nbsp;: </em>le son peut être créé de façon synthétique (y compris la synthèse vocale), être enregistré à partir de sons réels ou les deux.</p>
    
  <dt class="label"><a name="audiodescdef" id="audiodescdef"></a>audio-description</dt>
    <dd>Narration ajoutée à une piste sonore pour décrire les détails visuels importants qui ne peuvent être compris à partir de la piste sonore principale seulement.</dd>
      <p class="notice remarque"><em>Note 1&nbsp;: </em>l'audio-description d'une <a title="définition : vidéo" href="#videodef" class="termref">vidéo</a>
      fournit de l'information à propos des actions, des personnages, des changements de scènes, du texte apparaissant à l'écran et d'autres contenus visuels. </p>
      <p class="notice remarque"><em>Note 2&nbsp;: </em>dans une audio-description standard, la narration est ajoutée durant les pauses qui existent dans le dialogue. (Voir aussi <a title="définition : audio-description étendue" href="#extended-addef" class="termref">audio-description étendue</a>.)</p>
      <p class="notice remarque"><em>Note 3&nbsp;: </em>lorsque toute l'information de la <a title="définition : vidéo" href="#videodef" class="termref">vidéo</a>
      est déjà donnée dans la piste <a title="définition : audio" href="#audiodef" class="termref">audio</a>, aucune audio-description supplémentaire n'est requise.</p>
      <p class="notice remarque"><em>Note 4&nbsp;: </em>aussi nommée «&nbsp;vidéo-description&nbsp;» et «&nbsp;narration descriptive&nbsp;».</p>

    
  <dt class="label"><a id="extended-addef" name="extended-addef"></a>audio-description étendue</dt>
    <dd>Audio-description ajoutée à une présentation audiovisuelle en mettant en pause la <a class="termref" href="#videodef" title="définition : vidéo">vidéo</a> de manière à avoir le temps d'ajouter des descriptions supplémentaires.</dd>
    <p class="notice remarque"><em>Note&nbsp;: </em>cette technique est à utiliser seulement si le sens de la <a class="termref" href="#videodef" title="définition : vidéo">vidéo</a> est perdu sans <a class="termref" href="#audiodescdef" title="définition : audio-description">audio-description</a>
      supplémentaire et que les pauses entre les dialogues ou la narration sont trop courtes.</p>

  <dt class="label"><a name="blockstextdef" id="blockstextdef"></a>blocs de texte</dt>
    <dd>Plus d'une phrase de texte.</dd>
  <dt class="label"><a name="CAPTCHAdef" id="CAPTCHAdef"></a>CAPTCHA</dt>
      <dd>sigle de <span xml:lang="en" lang="en">«&nbsp;Completely Automated Public Turing test to tell Computers and Humans Apart&nbsp;»</span> (test public de Turing entièrement automatique ayant pour but de distinguer les humains des ordinateurs)</dd>

      <p class="notice remarque"><em>Note 1&nbsp;: </em>les tests de type CAPTCHA demandent souvent à l'utilisateur de taper un texte présenté dans une image ou un extrait audio déformés.</p>
      <p class="notice remarque"><em>Note 2&nbsp;: </em>un test de Turing est tout système de tests conçu pour distinguer un humain d'un ordinateur. Il est nommé en l'honneur du célèbre informaticien Alan Turing. Ce terme a été popularisé par les chercheurs de l'Université <span xml:lang="en" lang="en">Carnegie Mellon</span>. <a href="#CAPTCHA">[CAPTCHA]</a></p>

  <dt class="label"><a name="context-changedef" id="context-changedef"></a>changements de contexte</dt>
    <dd>Changements majeurs dans le contenu d'une <a title="définition : page Web" href="#webpagedef" class="termref">page Web</a> qui, s'ils sont faits sans que l'utilisateur en soit conscient, peuvent désorienter l'utilisateur qui ne peut voir l'ensemble de la page en même temp.</dd>
      <p>Les changements de contexte comprennent les changements de&nbsp;: </p>
      <ol class="enumar">
        <li><a title="définition : agent utilisateur" href="#useragentdef" class="termref">agent utilisateur</a>&nbsp;;</li>
        <li><a title="définition: espace de restitution" href="#viewportdef" class="termref">espace de restitution</a>&nbsp;;</li>
        <li>focus&nbsp;;</li>
        <li><a title="définition : contenu Web" href="#contentdef" class="termref">contenu</a> qui modifie la signification de la <a title="définition : page Web" href="#webpagedef" class="termref">page Web</a>.</li>
      </ol>

      <p class="notice remarque"><em>Note&nbsp;: </em>Un changement de contenu n'est pas
      toujours un changement de contexte. Un changement dans le contenu comme
      le déploiement d'une arborescence, un menu dynamique ou un déplacement
      de tabulation ne change pas nécessairement le contexte à moins qu'il ne
      change aussi l'un des éléments énumérés ci-dessus (par exemple le
      focus).</p>

      <p class="notice exemple"><em>Exemple&nbsp;: </em>l'ouverture d'une nouvelle
      fenêtre, le déplacement du focus sur un composant différent, le
      déplacement vers une nouvelle page (y compris tout ce qui, pour
      l'utilisateur, aurait l'air d'un déplacement vers une autre page) ou la
      réorganisation significative du contenu d'une page sont autant
      d'exemples d'un changement de contexte.</p>

  <dt class="label"><a name="blinksdef" id="blinksdef"></a>clignotement</dt>
    <dd>Alternance entre deux états visuels d'une façon qui veut attirer l'attention.</dd>

      <p class="notice remarque"><em>Note&nbsp;: </em>voir aussi <a title="définition : flash" href="#flash-def" class="termref">flash</a>.
      Il est possible que quelque chose soit suffisamment gros et clignote de façon suffisamment lumineuse à la fréquence appropriée pour être aussi considéré comme un flash.</p>

    
  <dt class="label"><a name="accessibility-supporteddef" id="accessibility-supporteddef"></a>compatible avec l'accessibilité</dt>
    <dd>Compatible avec les <a title="définition: technologie d'assistance (tel qu'utilisé dans ce document)" href="#atdef" class="termref">technologies d'assistance</a> des utilisateurs ainsi qu'avec les fonctions d'accessibilité des navigateurs et des autres <a title="définition: agent utilisateur" href="#useragentdef" class="termref">agents utilisateurs</a>.</dd>
      <p>Pour que l'utilisation d'une technologie Web (ou d'une fonctionnalité
      d'une technologie) soit considérée comme compatible avec
      l'accessibilité, les conditions 1 et 2 doivent toutes deux être
      respectées pour une technologie Web (ou pour une fonctionnalité d'une
      technologie)&nbsp;: </p>
      <ol class="enumar">
        <li><p><strong>La façon dont la <a title="définition: technologie Web" href="#technologydef" class="termref">technologie Web</a> est utilisée doit être
          compatible avec les technologies d'assistance des
          utilisateurs.</strong> Cela signifie que la façon dont la
          technologie est utilisée a été testée dans une perspective
          d'interopérabilité avec des utilisateurs des technologies
          d'assistance dans la ou les <a title="définition: langue" href="#human-langdef" class="termref">langues</a> du contenu&nbsp;;</p>
          <p><strong>ET</strong> </p>
        </li>
        <li><p><strong>La technologie Web doit fonctionner avec des agents
          utilisateurs qui sont compatibles avec l'accessibilité et qui sont
          à la disposition des utilisateurs.</strong> Cela signifie qu'au
          moins une des quatre affirmations suivantes est vraie&nbsp;:</p>
          <ol class="enumla">
            <li><p>La technologie fonctionne de façon native dans des agents
              utilisateurs largement distribués qui sont eux-mêmes
              compatibles avec l'accessibilité (comme HTML et CSS)&nbsp;;</p>
              <p><strong>OU</strong> </p>
            </li>
            <li><p>La technologie fonctionne avec un module d'extension
              largement distribué et qui est lui-même compatible avec
              l'accessibilité&nbsp;;</p>
              <p><strong>OU</strong> </p>
            </li>
            <li><p>Le contenu est disponible dans un environnement fermé comme
              le réseau d'une université ou d'une entreprise où l'agent
              utilisateur requis par la technologie et utilisé par
              l'organisation est lui-même compatible avec l'accessibilité&nbsp;;
              </p>
              <p><strong>OU</strong> </p>
            </li>
            <li><p>Les agents utilisateurs avec lesquels fonctionne la
              technologie sont compatibles avec l'accessibilité et sont
              disponibles en téléchargement ou à l'achat d'une façon
              qui&nbsp;:</p>
              <ul>
                <li><p>ne coûte pas plus cher à une personne handicapée
                  qu'à une personne sans limitations fonctionnelles
                  <strong>et</strong></p>
                </li>
                <li><p>est aussi facile à trouver et à obtenir par une
                  personne handicapée qu'elle l'est pour une personne sans
                  limitations fonctionnelles.</p>
                </li>
              </ul>
            </li>
          </ol>
        </li>
      </ol>

      <div class="note">
      <p class="notice remarque"><em>Note 1&nbsp;: </em>le Groupe de travail des WCAG et le
      W3C ne précisent pas quel niveau de compatibilité avec les technologies
      d'assistance une technologie Web particulière doit avoir pour être
      considérée comme compatible avec l'accessibilité. (Voir <a rel="nofollow" class="external" target="_blank" href="http://www.w3.org/TR/UNDERSTANDING-WCAG20/conformance.html#uc-support-level-head" hreflang="en">le niveau de compatibilité avec les technologies
      d'assistance nécessaire à la «&nbsp; compatibilité avec l'accessibilité
      &nbsp;» (en anglais)</a>.)</p>
      <p class="notice remarque"><em>Note 2&nbsp;: </em>les technologies Web peuvent être
      utilisées d'une manière qui n'est pas compatible avec l'accessibilité
      tant que l'utilisation du contenu n'en <a title="définition: dépendre" href="#reliedupondef" class="termref">dépend</a> pas et que la page dans
      son ensemble satisfait aux exigences de conformité, incluant <a href="#cc4">l'Exigence de conformité 4&nbsp;: <strong>L'usage des
      technologies selon des méthodes exclusivement compatibles avec
      l'accessibilité</strong></a> et <a href="#cc5">l'Exigence de conformité
      5&nbsp;: Non interférence</a> sont satisfaites.</p>
      <p class="notice remarque"><em>Note 3&nbsp;: </em>quand une <a title="définition: technologie Web" href="#technologydef" class="termref">technologie Web</a> est utilisée d'une manière qui est
      «&nbsp;compatible avec l'accessibilité&nbsp;», cela n'implique pas que toute
      la technologie ou que tous les usages de cette technologie sont
      compatibles. La plupart des technologies, y compris HTML, comportent une
      fonction ou un usage qui n'est pas compatible. Les pages sont conformes
      aux WCAG seulement si les usages de la technologie qui sont compatibles
      avec l'accessibilité sont les seuls dont dépend l'utilisation du
      contenu et que ces usages permettent de satisfaire aux exigences des
      WCAG.</p>
      <p class="notice remarque"><em>Note 4&nbsp;: </em>lorsqu'une technologie Web est
      citée et qu'elle se présente en plusieurs versions, la ou les versions
      compatibles devraient être spécifiées.</p>
      <p class="notice remarque"><em>Note 5&nbsp;: </em>pour les auteurs, une façon de
      repérer les usages d'une technologie qui sont compatibles avec
      l'accessibilité consisterait à consulter la compilation des usages qui
      sont documentés comme compatibles avec l'accessibilité. (Voir <a rel="nofollow" class="external" target="_blank" href="http://www.w3.org/TR/UNDERSTANDING-WCAG20/conformance.html#uc-documented-lists-head" hreflang="en">Comprendre les usages des technologies compatibles avec
      l'accessibilité (en anglais)</a>.) Les auteurs, les sociétés, les
      vendeurs de technologies ou n'importe qui d'autre peuvent documenter les
      usages des technologies qui sont compatibles avec l'accessibilité.
      Toutefois, tous les usages des technologies documentés devraient
      satisfaire à la définition des technologies Web compatibles avec
      l'accessibilité telle qu'elle est énoncée ci-dessus.</p>
      </div>

  <dt class="label"><a name="user-interface-componentdef" id="user-interface-componentdef"></a>composant d'interface utilisateur</dt>
    <dd>Partie du contenu qui est perçue par les utilisateurs comme un élément de contrôle unique pour une fonction distinct.</dd>

      <p class="notice remarque"><em>Note 1&nbsp;: </em>plusieurs composants d'interface utilisateur peuvent être implémentés au sein d'un seul programme. La notion de composant n'est pas liée ici aux techniques de programmation, mais plutôt à ce que les utilisateurs perçoivent comme des éléments  de contrôle distincts.</p>
      <p class="notice remarque"><em>Note 2&nbsp;: </em>les composants d'interface utilisateur incluent les éléments de formulaire et les liens aussi bien que des composants générés par scripts.</p>

      <p class="notice exemple"><em>Exemple&nbsp;: </em>un microprogramme (applet) dispose d'un «&nbsp;élément de contrôle&nbsp;» permettant de se déplacer dans le contenu ligne par ligne, page par page ou au hasard. Puisque chacune de ces fonctions devrait avoir un nom et fonctionner indépendamment, elles constitueraient chacune un «&nbsp;composant d'interface utilisateur&nbsp;».</p>

  <dt class="label"><a name="conformancedef" id="conformancedef"></a>conformité</dt>
    <dd>Satisfaire à toutes les exigences d'une norme ou d'un standard donné, d'une règle ou d'une spécification.</dd>

  <dt class="label"><a name="suppcontentdef" id="suppcontentdef"></a>contenu additionnel</dt>
    <dd><a title="définition : contenu (Web)" href="#contentdef" class="termref">Contenu</a> supplémentaire illustrant ou clarifiant le contenu primaire.</dd>

      <p class="notice exemple"><em>Exemple 1&nbsp;: </em>la version audio d'une <a title="définition : page Web" href="#webpagedef" class="termref">page Web</a>.</p>
      <p class="notice exemple"><em>Exemple 2&nbsp;: </em>l'illustration d'un <a title="définition : processus" href="#processdef" class="termref">processus</a> complexe.</p>
      <p class="notice exemple"><em>Exemple 3&nbsp;: </em>un paragraphe résumant les principales conclusions et recommandations d'un rapport de recherche.</p>

  <dt class="label"><a name="non-text-contentdef" id="non-text-contentdef"></a>contenu non textuel</dt>
    <dd>Tout contenu qui n'est pas une suite de caractères <a title="définition : déterminé (déterminable) par un programme informatique" href="#programmaticallydetermineddef" class="termref">déterminée par un programme informatique</a> ou suite de caractères sans signification dans aucune <a title="définition : langue" href="#human-langdef" class="termref">langue</a>.</dd>
    <p class="notice remarque"><em>Note&nbsp;: </em>ceci inclut <a title="définition : art ASCII" href="#asciiartdef" class="termref">l'art ASCII</a> (qui est un dessin à base de caractères), les émoticônes, l'écriture «&nbsp;leetspeak&nbsp;» (qui utilise la substitution de caractères) et les images représentant du texte.</p>

  <dt class="label"><a name="contentdef" id="contentdef"></a>contenu Web</dt>
    <dd>Information et expérience sensorielle à communiquer à l'utilisateur au moyen d'un <a title="définition : agent utilisateur" href="#useragentdef" class="termref">agent utilisateur</a>, y compris le code ou le balisage qui définit la <a title="définition : structure" href="#structuredef" class="termref">structure</a>, la <a title="définition : présentation" href="#presentationdef" class="termref">présentation</a> et les interactions du contenu.</dd>
  
  <dt class="label"><a name="pdlinkcontextdef" id="pdlinkcontextdef"></a>contexte du lien déterminé par un programme informatique</dt>
    <dd>Information supplémentaire qui peut être <a title="définition : Déterminé (déterminable) par un programme informatique" href="#programmaticallydetermineddef" class="termref">déterminée par un
      programme informatique</a> à partir des <a title="définition : relations" href="#relationshipsdef" class="termref">relations</a> avec un lien, combinée avec le texte du
      lien et présentée aux utilisateurs sous différentes formes</p>
      <p class="notice exemple"><em>Exemple&nbsp;: </em>en HTML, l'information qui est déterminée par un programme informatique à partir d'un lien en français, y compris le texte qui est dans le même paragraphe, la même liste ou la même cellule de tableau que le lien, ou une cellule d'en-tête de tableau associée avec la cellule contenant le lien.</p>
      <p class="notice remarque"><em>Note&nbsp;: </em>puisque les lecteurs d'écran interprètent la ponctuation, ils peuvent aussi fournir le contexte de la phrase en cours, lorsque le focus est sur le lien contenu dans cette phrase.</p>

  <dt class="label"><a name="user-controllabledef" id="user-controllabledef"></a>contrôlable par l'utilisateur</dt>
    <dd>Donnée auxquelles les utilisateurs ont accès.</dd>

    <p class="notice remarque"><em>Note&nbsp;: </em>ceci ne concerne pas les journaux de connexions (logs) Internet et les données de supervision des moteurs de recherche.</p>

    <p class="notice exemple"><em>Exemple&nbsp;: </em>les champs nom et adresse d'un compte utilisateur.</p>
    
  <dt class="label"><a name="fullscreenwindowdef" id="fullscreenwindowdef"></a>dans une fenêtre plein écran</dt>
    <dd>Sur les écrans d'ordinateur de bureau ou portable de taille courante dont l'espace de restitution est maximisé.</dd>

    <p class="notice remarque"><em>Note&nbsp;: </em>du fait que les gens gardent leurs ordinateurs pendant plusieurs années, il est conseillé de ne pas se baser sur les dernières résolutions d'écrans des ordinateurs de bureau ou portable, mais de prendre en considération la plus représentative des résolutions d'écrans durant une période de plusieurs années précédant cette évaluation.</p>

  <dt class="label"><a name="programmaticallysetdef" id="programmaticallysetdef"></a>défini par programmation</dt>
    <dd>Défini par un logiciel utilisant des méthodes qui fonctionnent avec les agents utilisateur, y compris les technologies d'assistance.</dd>

  <dt class="label"><a name="reliedupondef" id="reliedupondef"></a>dépendre
  (technologies)</dt>
    <dd>Le contenu ne serait pas <a title="définition: conformité" href="#conformancedef" class="termref">conforme</a> si cette <a title="définition : technologie Web" href="#technologydef" class="termref">technologie</a> est inactivée ou si elle n'est pas compatible.</dd>

