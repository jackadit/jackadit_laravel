      fonctionnelles particulières. L'assistance fournie par une technologie
      d'assistance est plus spécifique et appropriée aux besoins des
      utilisateurs visés. Un agent utilisateur usuel peut comporter des
      fonctionnalités importantes pour les technologies d'assistance comme
      l'extraction du contenu Web à partir d'objets de programmation ou
