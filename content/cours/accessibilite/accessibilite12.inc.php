  <dt class="label"><a id="general-thresholddef" name="general-thresholddef"></a> seuil de flash générique et seuil de flash rouge</dt>
    <dd><p>un <a class="termref" href="#flash-def" title="définition : flash">flash</a> ou une séquence d'images changeant rapidement est en dessous du seuil de flash (c'est-à-dire que le contenu est <strong>conforme</strong>) si l'une des conditions suivantes est satisfaite&nbsp;:</p>
      <ol class="enumar">
        <li><p>il n'y a pas plus de trois <strong>flashs génériques </strong>et pas plus de trois <strong>flashs rouges</strong> par seconde&nbsp;; ou</p>
        </li>
        <li><p>la surface d'affichage combinée des flashs simultanés ne représente pas plus de 0,006 stéradian dans chaque champ visuel de 10 degrés sur l'écran (soit 25% de chaque champ visuel de 10 degrés sur l'écran) à une distance habituelle de visualisation</p>
        </li>
      </ol>
      <p>où&nbsp;:</p>
      <ul>
        <li><p>un <strong>flash générique </strong>est défini comme une
          alternance de <a class="termref" href="#relativeluminancedef" title="définition : luminosité relative">luminosité relative</a>
          de 10% ou plus par rapport à la luminosité relative maximum, où la luminosité relative de l'image la plus sombre est en dessous de 0,80 et où «&nbsp;une alternance&nbsp;» est définie comme étant une augmentation suivie d'une diminution ou une diminution suivie d'une augmentation, et</p>
        </li>
        <li><p>un <strong>flash rouge</strong> est défini comme toute alternance de transition impliquant un rouge saturé.</p>
        </li>
      </ul>
      <p><em>Exception&nbsp;: </em>Le flash qui suit un modèle précis et équilibré comme du bruit blanc ou un modèle de damier alterné avec des «&nbsp;carrés&nbsp;» dont les côtés font moins de 0,1 degré (du champ visuel à une distance habituelle de visualisation) ne dépasse pas le  seuil de flash.</p>
    </dd>
    
      <p class="notice remarque"><em>Note 1&nbsp;: </em>pour les logiciels ou le contenu Web, un rectangle de 341 x 256 pixels n'importe où sur la surface d'affichage de l'écran, quand la résolution est à 1024 x 768 pixels, fournit une bonne estimation de ce que représente 10 degrés du champ visuel sur l'écran pour un écran et une distance habituelle de visualisation (par exemple des écrans de 38 à 43 centimètres (15 à 17 pouces) à une distance de 55 à 65 centimètres (22-26 pouces)). (Un affichage à une résolution supérieure du même contenu produirait des images plus petites et plus sûres, c'est pourquoi des résolutions inférieures sont utilisées pour définir le seuil.)</p>
      <p class="notice remarque"><em>Note 2&nbsp;: </em>une transition est le changement, dans un temps donné, de luminosité relative (ou de luminosité relative ou de couleur pour le flash rouge) entre les pics et les creux adjacents dans un ensemble de mesures de luminosité relative (ou de luminosité relative ou de couleur pour le flash rouge)</p>
      <p class="notice remarque"><em>Note 3&nbsp;: </em>la définition couramment utilisée dans le domaine pour <strong>«&nbsp;deux transitions opposées de rouge saturé&nbsp;»</strong> est&nbsp;: pour chacun ou tous les états impliqués dans chacune des transitions, R/(R+ G + B) &gt;= 0,8, et le changement dans la valeur de (R-G-B)x320 est &gt; 20 (les valeurs négatives de (R-G-B)x320 sont considérées comme nulles) pour chacune des transitions. Les valeurs R, G, B se classent entre 0 et 1 tel que spécifié dans la définition de «&nbsp;luminosité relative&nbsp;». <a href="#HARDING-BINNIE">[HARDING-BINNIE]</a></p>
      <p class="notice remarque"><em>Note 4&nbsp;: </em>des outils sont disponibles pour effectuer l'analyse depuis des captures vidéos. Cependant, aucun outil n'est nécessaire pour évaluer cette condition si le flash est inférieur ou égal à 3 flashs par seconde. Le contenu est automatiquement conforme (voir #1 et #2 ci-dessus).</p>

