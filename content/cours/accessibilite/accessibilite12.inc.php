  <dt class="label"><a name="relativeluminancedef" id="relativeluminancedef"></a>luminosité relative</dt>
    <dd>Luminosité relative d'un quelconque point de l'espace colorimétrique normalisé à 0 pour le noir le plus foncé et à 1 pour le blanc le plus clair.</dd>

      <div class="notice remarque"><p><em>Note 1&nbsp;: </em>pour l'espace colorimétrique de couleur sRGB, la luminance relative d'une couleur est définie par L = 0,2126 * <strong>R</strong> + 0,7152 * <strong>G</strong> + 0,0722 *
      <strong>B</strong> où <strong>R</strong>, <strong>G</strong> and <strong>B</strong> sont définis par&nbsp;: </p>
      <ul>
        <li><p>si R<sub>sRGB</sub> &lt;= 0,03928 alors <strong>R</strong> = R<sub>sRGB</sub>/12,92 sinon <strong>R</strong> = ((R<sub>sRGB</sub>+0,055)/1,055) ^ 2,4</p>
        </li>
        <li><p>si G<sub>sRGB</sub> &lt;= 0,03928 alors --<strong>G</strong> = G<sub>sRGB</sub>/12,92 sinon <strong>G</strong> = ((G<sub>sRGB</sub>+0,055)/1,055) ^ 2,4</p>
        </li>
        <li><p>si B<sub>sRGB</sub> &lt;= 0,03928 alors <strong>B</strong> = B<sub>sRGB</sub>/12.92 sinon <strong>B</strong> = ((B<sub>sRGB</sub>+0,055)/1,055) ^ 2,4</p>
        </li>
      </ul>
      
      <p>et R<sub>sRGB</sub>, G<sub>sRGB</sub> et B<sub>sRGB</sub> sont définis par&nbsp;:</p>

      <ul>
        <li><p>R<sub>sRGB</sub> = R<sub>8bit</sub>/255</p>
        </li>
        <li><p>G<sub>sRGB</sub> = G<sub>8bit</sub>/255</p>
        </li>
        <li><p>B<sub>sRGB</sub> = B<sub>8bit</sub>/255</p>
        </li>
      </ul>
      
      <p>Le caractère "^" est l'opérateur exponentiel. (formules tirées de <a href="#sRGB" hreflang="en">[sRGB]</a> et <a href="#IEC-4WD" hreflang="en">[IEC-4WD]</a>).</p>
      </div>

      <p class="notice remarque"><em>Note 2&nbsp;: </em>la plupart des systèmes utilisés aujourd'hui pour afficher le contenu Web adoptent l'encodage sRGB. À moins que l'on sache qu'un autre espace colorimétrique va être utilisé pour traiter et afficher le contenu, les auteurs devraient l'évaluer en utilisant l'espace colorimétrique sRGB. Si l'on utilise d'autres espaces colorimétriques, voir <a rel="nofollow" class="external" target="_blank" href="http://www.w3.org/TR/UNDERSTANDING-WCAG20/visual-audio-contrast-contrast.html" hreflang="en">Comprendre le critère de succès 1.4.3 (en anglais)</a>.</p>
      <p class="notice remarque"><em>Note 3&nbsp;: </em>si un tramage est effectué après l'affichage, alors la valeur source de la couleur est utilisée. Pour les couleurs tramées à la source, les valeurs moyennes des couleurs qui sont tramées devraient être utilisées (R moyen, G moyen, B moyen).</p>
      <p class="notice remarque"><em>Note 4&nbsp;: </em>il existe des outils qui effectuent automatiquement les calculs lors des tests de contraste et de flash.</p>
      <p class="notice remarque"><em>Note 5&nbsp;: </em>une <a rel="nofollow" class="external" target="_blank" href="http://www.w3.org/TR/WCAG/relative-luminance.xml">version MathML de la définition de la luminosité relative</a> est disponible.</p>
  
