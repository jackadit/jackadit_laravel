  </li>
  <li><p><strong><a rel="nofollow" class="external" target="_blank" href="https://www.w3.org/WAI/WCAG21/Understanding/" hreflang="en">Comprendre les WCAG (en anglais)</a></strong> - Un guide
    pour comprendre et mettre en œuvre les WCAG. On y trouve un court document de «&nbsp;compréhension&nbsp;» pour chaque règle et chaque critère de succès dans les WCAG ainsi que pour certains éléments clés.</p>
  </li>
  <li><p><strong><a rel="nofollow" class="external" target="_blank" href="https://www.w3.org/WAI/WCAG21/Techniques/" hreflang="en">Les
