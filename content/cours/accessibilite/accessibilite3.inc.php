<h1>L'accessibilité des contenus Web (WCAG).</h1>

<h2>Les différents niveaux de lecture des WCAG.</h2>

<p>Les particuliers et les organisations qui utilisent les WCAG sont très
variés et comprennent des concepteurs Web, des développeurs Web, des
décideurs, des acheteurs, des enseignants et des étudiants. Afin de répondre
aux besoins variés de ces publics, plusieurs niveaux de lecture sont
proposés&nbsp;: de grands <em>principes</em>, des <em>règles</em> générales,
des <em>critères de succès</em> testables ainsi qu'un riche ensemble de
<em>techniques suffisantes, de techniques recommandées</em> et d'<em>échecs
fréquents documentés</em> avec des exemples, des liens vers des ressources et
du code.</p>
<ul>
  <li><p><strong>Les principes</strong> - Le premier niveau traite des quatre principes fondamentaux pour l'accessibilité des contenus Web&nbsp;:
    perceptible, utilisable, compréhensible et robuste.</p>
    <p>Voir aussi <a rel="nofollow" class="external" target="_blank" hreflang="fr" href="https://www.w3.org/Translations/NOTE-UNDERSTANDING-WCAG20-fr/intro.html">Comprendre
    les quatre principes de l'accessibilité</a>.</p>
  </li>
  <li><p><strong>Les règles</strong> - Après les principes il y a les règles. Les 13 règles du WCAG 2.1 (12 avec le WCAG 2.0) donnent les objectifs de base que les auteurs
    doivent atteindre afin de rendre le contenu plus accessible pour les utilisateurs ayant différents types de limitations fonctionnelles. Les
    règles ne sont pas testables, mais elles fournissent la trame et les objectifs principaux pour aider les auteurs à comprendre les critères de
    succès et à mieux mettre en œuvre les techniques.</p>
    <p>Voir aussi <a rel="nofollow" class="external" target="_blank" hreflang="fr" href="https://www.w3.org/Translations/WCAG20-fr/#guidelines">Les Règles des WCAG 2.1</a></p>
  </li>
  <li><p><strong>Les critères de succès</strong> - Avec le WCAG 2.1, 78 critères de succès testables (61 avec le WCAG 2.0) sont fournis pour chaque règle afin de permettre l'utilisation des WCAG là où des exigences et des tests de conformité sont
    nécessaires, notamment pour la spécification de conception, l'achat, la réglementation et les accords contractuels.</p>
	<p><a rel="nofollow" class="external" target="_blank" hreflang="fr" href="https://www.w3.org/Translations/NOTE-UNDERSTANDING-WCAG20-fr/NOTE-UNDERSTANDING-WCAG20-fr-20110706/">Comprendre les critères du WCAG2.0</a></p>
    
  <p>Afin de répondre aux besoins de divers groupes et de différents contextes, trois niveaux de conformité ont été définis&nbsp;: A (le plus bas), AA et AAA (le plus élevé). Des informations complémentaires sur les niveaux des WCAG peuvent être
    consultées dans <a rel="nofollow" class="external" target="_blank" hreflang="fr" href="https://www.w3.org/Translations/WCAG20-fr/#conformance">Comprendre les niveaux de conformité</a>.</p>
  </li>
  <li><p><strong>Techniques suffisantes et techniques recommandées</strong> -
    Pour chaque <em>règle</em> et chaque <em>critère de succès</em> des WCAG, le groupe de travail a également documenté une grande variété de
    <em>techniques</em>. Ces techniques sont informatives et se divisent en deux catégories&nbsp;: celles qui sont <em>suffisantes</em> pour satisfaire au
    critère de succès et celles qui sont <em>recommandées</em>. Les techniques recommandées vont plus loin que ce qui est requis par chacun
    des critères de succès et elles permettent aux auteurs de mieux respecter les règles. Certaines techniques recommandées concernent des problèmes
    d'accessibilité qui ne sont pas traités par les critères de succès testables. Lorsque des échecs fréquents sont connus, ils sont aussi
    documentés. Voir également <a rel="nofollow" class="external" target="_blank" hreflang="en" href="https://www.w3.org/WAI/WCAG22/Understanding/">les
    techniques suffisantes et les techniques recommandées dans Comprendre les WCAG 2.2 (en anglais)</a>.</p>
  </li>
</ul>

<p class="notice remarque">Les WCAG 2.2 sont encore à l'état de document de travail au moment où j'écris ces lignes (29 avril 2021), mais englobent les versions précédentes.</p>

<p>Tous ces niveaux de lecture (les principes, les règles, les critères de
succès ainsi que les techniques suffisantes et les techniques recommandées)
forment un ensemble qui donne une aide sur la façon de créer un contenu plus
accessible. Les auteurs sont encouragés à consulter et à appliquer tous les
niveaux qu'ils peuvent mettre en œuvre, y compris les techniques
recommandées, afin de répondre au mieux aux besoins du plus grand nombre
possible d'utilisateurs.</p>

<p>Notons que même du contenu conforme au niveau le plus élevé (AAA) ne sera
pas accessible aux personnes ayant tous les types, les degrés ou les
combinaisons de limitations fonctionnelles, en particulier dans les domaines
cognitifs, du langage et de l'apprentissage. Les auteurs sont encouragés à
prendre en compte l'ensemble des techniques, y compris les techniques
recommandées, et à rechercher des conseils appropriés à propos des bonnes
pratiques en vigueur afin de faire en sorte que les contenus Web soient
accessibles, autant que possible, à cette communauté. Des <a rel="nofollow" class="external" target="_blank" hreflang="en" href="https://www.w3.org/TR/UNDERSTANDING-WCAG20/appendixC.html#understanding-metadata">métadonnées
(en anglais)</a> pourront aider l'utilisateur à trouver les contenus qui
correspondent le mieux à ses besoins.</p>

