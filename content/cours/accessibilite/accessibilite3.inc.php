mis à jour pour décrire comment les WCAG devront être appliquées pour les technologies futures. Les documents d'accompagnement comprennent&nbsp;:</p>
<ol class="enumar">
  <li><p><strong><a rel="nofollow" class="external" target="_blank" href="https://www.w3.org/WAI/WCAG21/quickref/" hreflang="en">Comment satisfaire aux WCAG (en anglais)</a></strong> -
    Une liste personnalisable de références rapides pour les WCAG qui contient toutes les règles, les critères de succès et les techniques
