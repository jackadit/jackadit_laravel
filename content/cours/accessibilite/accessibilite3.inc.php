  </li>
  <li><p><strong><a rel="nofollow" class="external" target="_blank" href="https://www.w3.org/WAI/standards-guidelines/wcag/docs/" hreflang="en">Les
    documents des WCAG (en anglais)</a></strong> - Un diagramme et une description de la façon dont les documents techniques sont reliés entre
    eux.</p>
  </li>
</ol>

<p>Voir la <a rel="nofollow" class="external" target="_blank" href="https://www.w3.org/WAI/standards-guidelines/wcag/fr" hreflang="fr">Vue
d'ensemble des règles pour l'accessibilité des contenus Web</a> pour une description des éléments d'accompagnement, y compris les ressources éducatives en lien avec les WCAG. <span>Des ressources supplémentaires qui couvrent des points tels que des analyses de rentabilité sur l'accessibilité, la planification des mises en œuvre d'améliorations de l'accessibilité des sites Web ou les politiques d'accessibilité sont décrits dans les <a rel="nofollow" class="external" target="_blank" href="https://www.w3.org/WAI/resources/" hreflang="en">ressources de la WAI (en anglais)</a>.</span></p>

<p>Les règles pour l'accessibilité des contenus du Web, qui sont proposées à travers ce référentiel, reposent sur le WCAG 2.0 (Web Content Accessibility Guidelines 2.0), rédigé par la WAI.</p>
<p><a rel="nofollow" class="external" target="_blank" href="https://www.w3.org/Translations/WCAG20-fr/#text-equiv">Les WCAG 2.0</a> adoptent une approche thématique proposant 12 directives structurantes selon 4 principes fondamentaux.</p>
<p>Chaque « Principe » regroupe une série de règles. Chaque règle est détaillée sous la forme d'un ou plusieurs « <a rel="nofollow" class="external" target="_blank" href="https://www.w3.org/Translations/NOTE-UNDERSTANDING-WCAG20-fr/intro.html#introduction-layers-head">critères de succès</a> » reliés aux tests unitaires. Ce document est à inclure en annexe du cahier des charges de toute opération de mise en oeuvre ou de refonte de contenu Web.</p>

<h2>Conformité</h2>
<p>Cette section est normative. </p>

<p>Cette section reprend les exigences de conformité aux WCAG 2.0. Elle	explique aussi comment faire une déclaration de conformité, ce qui est optionnel.
Finalement, elle décrit la signification du terme compatible avec l'accessibilité, puisque la conformité ne peut <a title="définition : dépendre (technologies dont on dépend)" href="#reliedupondef" class="termref">dépendre</a> que des technologies qui
sont utilisées de manière compatible avec l'accessibilité.
Le document <a  rel="nofollow" class="external" target="_blank" href="https://www.w3.org/TR/UNDERSTANDING-WCAG20/conformance.html" hreflang="en">Comprendre la conformité (en anglais)</a> comprend des
explications plus précises sur la notion de compatibilité avec l'accessibilité.</p>

<h3>Les exigences de conformité</h3>

<p>Afin qu'une page Web soit conforme aux WCAG 2.0, toutes les exigences suivantes doivent être satisfaites&nbsp;:</p>

<p class="sctxt"><strong class="sc-handle">1. Niveau de conformité&nbsp;:</strong> l'un des niveaux de conformité suivants est atteint par le respect de la totalité de ses critères de succès.</p>
<ul>
	<li><strong>Niveau A&nbsp;: </strong>pour une conformité de niveau A (le niveau minimal de conformité), la page Web satisfait à tous les critères de succès de niveau A ou une version de remplacement conforme est fournie.</li>
	<li><strong>Niveau AA&nbsp;: </strong>pour une conformité de niveau AA, la page Web remplit tous les critères de succès de niveau A et AA ou une version de remplacement conforme au niveau AA est fournie.</li>
	<li><strong>Niveau AAA&nbsp;: </strong>pour une conformité de niveau AAA, la page Web remplit tous les critères de succès de niveau A, AA et AAA ou une version de remplacement conforme au niveau AAA est fournie.</li>
</ul>

<div class="notice remarque">
<p class="prefix"><em>Note 1&nbsp;: </em>bien que la conformité se définisse par	paliers, les auteurs sont invités à mentionner (dans leur déclaration) tous	les critères de succès satisfaits au-delà du niveau de conformité atteint.</p>
<p class="prefix"><em>Note 2&nbsp;: </em>il n'est pas recommandé de se fixer le niveau AAA comme objectif à l'échelle de sites entiers car il n'est pas possible de satisfaire à tous les critères de succès du niveau AAA pour certains contenus.</p>
</div>

<p class="sctxt"><strong class="sc-handle">2. Pages complètes&nbsp;:</strong> la
<a title="définition : conformité" href="#conformancedef" class="termref">conformité</a> (et le niveau de conformité) s'entend
uniquement pour des <a title="définition : page Web" href="#webpagedef" class="termref">pages Web</a> complètes et ne peut être atteinte si une
partie de la page Web est exclue. </p>

<div class="notice remarque">
<p class="prefix"><em>Note 1&nbsp;: </em>dans le but de déterminer la conformité,
les versions de remplacement à une partie du contenu de la page sont
considérées comme une partie de la page quand les versions de remplacement
peuvent être obtenues directement depuis la page, comme par exemple, une
description longue ou la présentation de remplacement d'une vidéo.</p>

<p class="prefix"><em>Note 2&nbsp;:</em><span>les auteurs de</span> pages Web qui
ne peuvent être considérées comme conformes en raison d'un contenu qui n'est
pas sous le contrôle de l'auteur peuvent envisager d'utiliser une <a href="#conformance-partial">déclaration de conformité partielle</a>.</p>
</div>

<p class="sctxt"><strong class="sc-handle">3. Processus complets&nbsp;:</strong>
quand une <a title="définition : page Web" href="#webpagedef" class="termref">page Web</a> fait partie d'un ensemble représentant un <a title="définition : processus" href="#processdef" class="termref">processus</a> (comme une succession d'étapes devant être
complétées afin d'accomplir une activité), toutes les pages Web du processus
sont conformes au moins au niveau spécifié. (La conformité à un certain
niveau est impossible s'il existe une page de ce processus qui n'atteint pas au
moins ce niveau.)</p>

<div class="notice exemple">
<p class="prefix"><em>Exemple&nbsp;: </em>une boutique en ligne présente une
série de pages permettant de sélectionner et d'acheter des produits. Toutes
les pages de la séquence depuis le début jusqu'à la fin (le paiement) sont
conformes afin que toute page faisant partie du processus soit conforme.</p>
</div>

<p class="sctxt"><strong class="sc-handle">4. L'usage des technologies selon des méthodes exclusivement compatibles avec l'accessibilité&nbsp;:</strong> la satisfaction à un critère de succès ne <a title="définition : dépendre " href="#reliedupondef" class="termref">dépend</a> que des méthodes
d'utilisation des <a title="définition : technologie Web" href="#technologydef" class="termref">technologies</a> qui sont <a title="définition : compatible avec l'accessibilité" href="#accessibility-supporteddef" class="termref">compatibles avec
l'accessibilité</a>. Toute information ou fonctionnalité proposée d'une manière non compatible avec l'accessibilité est également disponible sous une forme compatible avec l'accessibilité. (Voir <a rel="nofollow" class="external" target="_blank" href="https://www.w3.org/TR/UNDERSTANDING-WCAG20/conformance.html#uc-accessibility-support-head" hreflang="en">Comprendre la compatibilité avec l'accessibilité (en
anglais)</a>.) </p>


<p class="sctxt"><strong class="sc-handle">5. Non interférence&nbsp;:</strong> si des <a title="définition : technologie Web" href="#technologydef" class="termref">technologies</a> sont employées de manière non <a title="définition : compatible avec l'accessibilité" href="#accessibility-supporteddef" class="termref">compatible avec
l'accessibilité</a> ou non conforme, alors elles n'empêchent pas les utilisateurs d'accéder au reste de la page. En outre, la <a title="définition : page Web" href="#webpagedef" class="termref">page Web</a>
dans sa globalité continue de répondre aux exigences de conformité dans chacun des cas suivants&nbsp;: </p>
<ol class="enumar">
	<li>quand toute technologie non dépendante est activée dans l'agent utilisateur,</li>
	<li>quand toute technologie non dépendante est désactivée dans l'agent utilisateur et</li>
	<li>quand toute technologie non dépendante n'est pas reconnue par l'agent utilisateur</li>
</ol>

<p class="sctxt">De plus, les critères de succès suivants s'appliquent à tout le contenu de la page, y compris au contenu dont on ne dépend pas
autrement pour atteindre la conformité, car un échec à les satisfaire pourrait perturber toute utilisation de la page&nbsp;:</p>
<ul>
	<li><strong>1.4.2 - Contrôle du son</strong>,</li>
	<li><strong>2.1.2 - Pas de piège au clavier</strong>,</li>
	<li><strong>2.3.1 - Pas plus de trois flashs ou sous le seuil critique</strong> et</li>
	<li><strong>2.2.2 - Mettre en pause, arrêter, masquer</strong>.</li>
</ul>

<div class="notice remarque">
<p class="prefix"><em>Note&nbsp;: </em>si une page ne peut pas être rendue conforme (par exemple une page de test de la conformité ou une page
d'exemple), elle ne peut pas être incluse dans le périmètre de conformité ou dans une déclaration de conformité.</p>
</div>

<p class="sctxt">Pour plus d'information et des exemples, voir <a rel="nofollow" class="external" target="_blank" href="https://www.w3.org/TR/UNDERSTANDING-WCAG20/conformance.html#uc-conformance-requirements-head" hreflang="en">Comprendre les exigences de conformité (en anglais)</a>. </p>

<h3>Déclarations de conformité (optionnelle) </h3>

<p>La conformité est définie seulement pour les pages Web. Toutefois, une déclaration de conformité peut être faite pour une seule page, une série de pages ou un ensemble de pages Web reliées entre elles.</p>

<h4>Éléments requis pour une déclaration de conformité</h4>

    <p>Une déclaration de conformité <strong>n'est pas obligatoire</strong>. Les auteurs peuvent se conformer aux WCAG 2.1 sans faire aucune déclaration. Toutefois, si une déclaration de conformité est faite, cette déclaration de conformité <strong>doit</strong> inclure les informations suivantes&nbsp;:</p>
    <ol class="enumar">
      <li><strong>Date</strong> de la déclaration;</li>
      <li><strong>Titre des Règles, version et URI&nbsp;: </strong> <a rel="nofollow" class="external" target="_blank" href="https://www.w3.org/Translations/WCAG20-fr/">«&nbsp;Règles pour l'accessibilité des contenus Web (WCAG) 2.0</a>&nbsp;»</li>
      <li><strong>Niveau de conformité</strong> satisfait&nbsp;: (Niveau A, AA ou AAA)</li>
      <li><p><strong>Une brève description des pages Web</strong>, comme une liste d'URI pour lesquelles la déclaration est faite en précisant si les sous-domaines sont inclus dans la déclaration.</p>

        <div class="notice remarque">
        <p class="prefix"><em>Note 1&nbsp;: </em>les pages Web peuvent être décrites sous forme de liste ou par une expression qui décrit tous les URI inclus dans la déclaration.</p>
        <p class="prefix"><em>Note 2&nbsp;: </em>les produits Web qui ne comportent pas d'URI avant leur installation sur le site Web du client peuvent comporter un engagement à l'effet que le produit sera conforme une fois installé.</p>
        </div>
      </li>
      <li><p>Une liste des <strong><a title="définition : technologie Web" href="#technologydef" class="termref">technologies Web</a> dont <a title="définition : dépendre (technologies dont on dépend)" href="#reliedupondef" class="termref">dépend</a></strong> l'utilisation du
        contenu.</p>
      </li>
    </ol>

    <div class="notice remarque">
    <p class="prefix"><em>Note&nbsp;: </em>si un logo de conformité est utilisé, il	devrait constituer une déclaration et doit donc être accompagné des	éléments requis pour une déclaration de conformité tel qu'énumérés ci-dessus.</p>
    </div>
	</div>

	<h4><a name="conformance-optional" id="conformance-optional"></a>Éléments optionnels d'une déclaration de conformité.</h4>

	<p class="sctxt">En plus des éléments ci-dessus qui sont requis pour une déclaration de conformité, considérer la possibilité d'ajouter des
	informations pour aider les utilisateurs. Les informations supplémentaires 	recommandées comprennent&nbsp;:</p>
	<ul>
	  <li><p>Une liste des critères de succès qui vont au-delà du niveau de conformité atteint. Cette information devrait être donnée d'une façon que les utilisateurs pourront utiliser, de préférence sous la forme de métadonnées lisibles par machine. </p>
	  </li>
	  <li><p>Une liste des technologies qui «&nbsp;<em>sont utilisées mais dont l'utilisation du contenu ne <a title="définition : dépendre (technologies dont on dépend)" href="#reliedupondef" class="termref">dépend</a></em> pas&nbsp;».</p>
	  </li>
	  <li><p>Une liste des agents utilisateurs, incluant les technologies d'assistance qui ont été utilisées pour vérifier le contenu. </p>
	  </li>
	  <li><p>De l'information à propos des dispositions prises pour aller au-delà des critères de succès dans le but d'améliorer l'accessibilité.</p>
	  </li>
	  <li><p>Une version lisible par machine d'une métadonnée sur les technologies spécifiques dont <a title="définition : dépendre (technologies dont on dépend)" href="#reliedupondef" class="termref">dépend</a> l'utilisation du contenu.</p>
	  </li>
	  <li><p>Une version lisible par machine de la déclaration de conformité.</p>
	  </li>
	</ul>

	<div class="notice remarque">
	<p class="prefix"><em>Note 1&nbsp;: </em>lire <a rel="nofollow" class="external" target="_blank" href="https://www.w3.org/TR/UNDERSTANDING-WCAG20/conformance.html#uc-conformance-claims-head" hreflang="en">Comprendre la déclaration de conformité (en anglais)</a> pour
	plus d'information et pour des exemples de déclarations de conformité.</p>

	<p class="prefix"><em>Note 2&nbsp;: </em>lire <a rel="nofollow" class="external" target="_blank" href="https://www.w3.org/TR/UNDERSTANDING-WCAG20/appendixC.html#understanding-metadata" hreflang="en">Comprendre les métadonnées (en anglais)</a> pour plus
	d'informations à propos de l'utilisation des métadonnées dans une	déclaration de conformité.</p>
	</div>

	<h3><a name="conformance-partial" id="conformance-partial"></a>Déclaration de conformité partielle - Contenu fourni par un tiers.</h3>

	<p>Parfois, des pages Web sont créées afin que l'on puisse y rajouter un
	contenu. Par exemple, une application Web de courrier électronique, un blogue,
	un article permettant l'ajout de commentaires par les utilisateurs ou des
	applications acceptant du contenu généré par l'utilisateur. Un autre exemple
	serait une page, telle que celles d'un portail ou d'un site d'informations,
	composées d'une somme de contenus rédigés par de multiples contributeurs ou
	des sites insérant automatiquement, au fur et à mesure, du contenu provenant
	d'autres sites, tels que des publicités insérées automatiquement.</p>

	<p class="sctxt">Dans ces cas-là, il est impossible lors de l'affichage
	original de savoir quel sera le contenu non contrôlé de ces pages. Il est
	important de remarquer que le contenu non contrôlé peut également affecter
	l'accessibilité du contenu contrôlé. Deux options sont envisageables&nbsp;:</p>
	<ol class="enumar">
	  <li><p>La détermination de conformité peut être basée sur la connaissance
	    de l'existant. Si une page de ce type est vérifiée et réparée (le
	    contenu non conforme est enlevé ou mis en conformité) en moins de deux
	    jours ouvrables, alors une détermination ou affirmation de la conformité
	    peut être faite puisque, excepté les erreurs dans le contenu généré en
	    externe qui sont corrigées ou enlevées lorsqu'elles sont découvertes, la
	    page est conforme. Aucune affirmation de conformité ne peut être faite
	    s'il est impossible de vérifier ou de réparer le contenu non conforme</p>
	    <p><strong>ou</strong></p>
	  </li>
	  <li><p>Une «&nbsp;déclaration de conformité partielle&nbsp;» peut être faite
	    comme quoi la page n'est pas conforme, mais qu'elle pourrait l'être si
	    certaines parties étaient enlevées. La forme de cette déclaration
	    pourrait être&nbsp;: «&nbsp;Cette page n'est pas conforme, mais pourrait être
	    conforme aux WCAG 2.0 niveau X si les parties suivantes, issues de sources
	    non contrôlées, lui étaient retirées&nbsp;». De plus, ce qui suit pourrait
	    également être reconnu comme un contenu non contrôlé comme décrit dans
	    la déclaration de conformité partielle&nbsp;:</p>
	    <ol class="enumla">
	      <li><p>C'est du contenu qui n'est pas contrôlé par l'auteur.</p>
	      </li>
	      <li><p>C'est du contenu décrit de façon à être identifié par les
	        utilisateurs (par exemple, on ne peut pas les décrire comme «&nbsp;toutes
	        les parties que nous ne contrôlons pas&nbsp;» à moins qu'elles ne soient
	        clairement marquées comme telles).</p>
	      </li>
	    </ol>
	  </li>
	</ol>
	</div>

	<h3>Déclaration de conformité partielle - Langue.</h3>

	<p>Une «&nbsp;déclaration de conformité partielle selon la langue&nbsp;» peut être
	faite lorsqu'une page n'est pas conforme et pourrait l'être si la <a title="définition : compatibilité avec l'accessibilité" href="#accessibility-supporteddef" class="termref">compatibilité avec
	l'accessibilité</a> était assurée pour toutes les langues utilisées dans la
	page. La déclaration s'énonce alors comme suit&nbsp;: «&nbsp;Cette page <span>n'est
	pas conforme, mais</span> serait conforme aux WCAG 2.1 au niveau X si la
	compatibilité avec l'accessibilité était assurée pour la (les) langue(s)
	suivante(s)&nbsp;:&nbsp;»</p>	
