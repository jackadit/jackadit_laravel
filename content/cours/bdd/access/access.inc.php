<h1>Formation à ACCESS</h1>
	<p>Access est un logiciel de gestion de base de données relationnelles. C'est un outil très puissant. Toutefois sa prise en main peut se révéler plus délicate que celle d'un tableur ou d'un traitement de texte.</p>
	<p>Ce cours est élaboré pour vous permettre de découvrir Access par l'exemple.</p>
	<p>Nous allons donc rencontrer les principaux cas de figure dans des situations types.</p>
	<p>Ce cours ne se veut pas exhaustif, mais il parcourt néanmoins les notions fondamentales nécessaires à la production d'une application type sous Access.</p>

