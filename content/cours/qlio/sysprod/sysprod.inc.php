<h1>Système de production</h1>

	<h2>Objectifs du module</h2>
	<p>À l'issue du module, l'étudiant doit être capable de :</p>
	<ul>
	<li>Décrire un système de production, son processus, son organisation,</li>
	<li>Identifier des éléments de pilotage de ce système</li>
	<li>Identifier les documents techniques d'industrialisation du processus</li>
	<li>Faire une modélisation simple des postes</li>
	</ul>
	<h2>Compétences visées</h2>
	<ul>
		<li>Modéliser un processus</li>
		<li>Identifier/caractériser les processus (entrées, sorties, acteurs, points de mesure/contrôle, indicateurs…), établir une cartographie des processus et des flux</li>
		<li>Renseigner les supports de contrôle des processus et supports de constat (listes, grilles,…), renseigner les fiches d'enregistrement</li>
		<li>Mesurer la performance d'un processus de délivrance produit ou de service</li>
	</ul>
	
	<p class="h2-like">Prérequis</p>
	<p>Il conviendra de construire conjointement la mise en place des modules M1205, M1302 et M1304</p>

	<h2>Contenus</h2>
	<ul>
	  <li>Identifier les activités d'un processus de fabrication d'un produit simple</li>
	  <li>Identifier la structure d'un système de production et ses relations internes (aspects fonctionnels et technologiques)</li>
	  <li>Identifier le cycle d'une machine de production en fonctionnement en s'appuyant sur des documents techniques, première approche de modélisation</li>
	  <li>Identifier les degrés d'automatisation d'un système de production et les différentes tâches de l'opérateur</li>
	  <li>Identifier les flux de produits et les flux d'information nécessaires au pilotage et à la production</li>
	  <li>Expérimenter une situation de production ; utiliser des supports de pilotage et de contrôle du processus</li>
	  <li>Expérimenter l'élaboration d'un processus de production, conception du poste, des documents, etc</li>
	</ul>

	<h2>Prolongements possibles</h2>
	<p>Étude d'implantation</p>
	<p>Systèmes automatisés</p>
	<p>Sûreté de fonctionnement</p>




