<a name="A">A</a>
<dl compact>

  <dt><b>Analyse des écarts</b></dt>
  <dd>Méthode de contrôle des coûts de revient fondée sur les écarts entre standards et
    réalisations; permet, par l‘analyse des causes, d‘entreprendre des actions correctives.</dd>
</dl>

<a name="B">B</a>
<dl compact>

  <dt><b>Besoin brut</b></dt>
  <dd>Besoin résultant de la demande ou du programme de production, pour les produits finis,
    sous-ensembles, composants ou matières premières, compte non tenu des stocks existants
    et des règles de gestion de stock.</dd>
  <dt><b>Besoin dépendant</b></dt>
  <dd>Dans un état prévisionnel de stock, les besoins dépendants sont les besoins
    nécessaires à la fabrication de composés d‘un niveau supérieur dans la nomenclature ;
    ils proviennent de la procédure de calcul des besoins nets.</dd>
  <dt><b>Besoin indépendant</b></dt>
  <dd>Dans un état prévisionnel de stock, les besoins indépendants représentent des
    consommations externes au système productif, telles que les pièces de rechange.</dd>
  <dt><b>Besoin net</b></dt>
  <dd>Il est égal au besoin brut déduction faite des stocks et en-cours et après
    application des règles de gestion de stock (groupage, quantités économiques, etc.).</dd>
  <dt><b>Bon de sortie matières</b></dt>
  <dd>Document servant à retirer du magasin la quantité de matières prévue sur un ordre de
    fabrication. Sert à tenir la comptabilité matières.</dd>
  <dt><b>Bon de travail</b></dt>
  <dd>Document remis à l‘opérateur et qui indique le travail à effectuer, la machine et
    l‘outillage nécessaires, le temps alloué et éventuellement la date prévue par
    l‘ordonnancement pour l‘achèvement du travail. Sert aussi à enregistrer les temps
    passés pour la comptabilité analytique.</dd>
  <dt><b>Budget</b></dt>
  <dd>Plan généralement annuel et détaillé mois par mois, décrivant les prévisions
    d‘activité et les charges (ou dépenses) et produits (ou recettes) correspondants.</dd>
  <dt><b>Budget flexible</b></dt>
  <dd>Budget qui exprime les relations entre charges et produits en fonction d‘un niveau
    d‘activité.</dd></dl>
    
<a name="C">C</a>

<dl compact>

  <dt><b>Cadence</b></dt>
  <dd>Rythme de travail exprimé par le nombre de produits, ou d‘unités d‘oeuvre, fabriqués
    par unité de temps.</dd>
  <dt><b>Calcul des besoins</b></dt>
  <dd>Il consiste à partir de la demande connue ou prévisionnelle en produits finis pour
    déterminer les besoins correspondants en sous-ensembles et composants à tous les niveaux
    de la nomenclature.</dd>
  <dt><b>Calendrier d&#146;activité</b></dt>
  <dd>Ensemble de plages horaires d‘ouverture de moyens de production. Détermine la capacité
    des moyens de production qui sont attachés à un calendrier. On peut définir plusieurs
    calendriers de fonctionnement.</dd>
  <dt><b>Capacité</b></dt>
  <dd>Quantité de production correspondant aux moyens humains et matériels. Elle s‘exprime
    en nombre de produits ou unités d‘oeuvre (poids, volumes, heures standards) par unité de
    temps.</dd>
  <dt><b>Capacité finie</b></dt>
  <dd>Le concept de capacité finie signifie qu‘un lissage automatique de la charge est
    effectué suivant certaines procédures et règles de priorité.</dd>
  <dt><b>Capacité infinie</b></dt>
  <dd>Le concept de capacité infinie suppose que c‘est le gestionnaire qui prendra les
    mesures appropriées, soit en décalant certains ordres, soit en cherchant à modifier la
    capacité.</dd>
  <dt><b>Cas d‘emploi</b></dt>
  <dd>Recherche des composés dans lesquels un composant donné est utilisé.</dd>
  <dt><b>Chargement</b></dt>
  <dd>Action d‘affecter les opérations des ordres de fabrication aux différents postes de
    travail.</dd>
  <dt><b>Chevauchement</b></dt>
  <dd>Méthode d‘ordonnancement dans laquelle on engage la première partie d‘un lot sur une
    opération sans attendre que l‘opération précédente soit terminée.</dd>
  <dt><b>Code de plus bas niveau</b></dt>
  <dd>Pour une pièce donnée, niveau le plus bas auquel on la trouve dans toutes les
    nomenclatures. La mise à jour de ce code est fondamentale pour que le calcul des besoins
    fonctionne correctement.</dd>
  <dt><b>Coefficient de montage</b></dt>
  <dd>Nombre de composants qui entre dans chaque unité du composé. Figure dans les liens de
    nomenclature.</dd>
  <dt><b>Coefficient de rendement</b></dt>
  <dd>Coefficient multiplicateur qui doit être pris en compte pour transformer la capacité
    théorique d‘un poste de charge ou la consommation théorique d‘une matière, afin de
    connaître les valeurs effectives à prévoir dans la mise en place des moyens, dans les
    programmes et les plans.</dd>
  <dt><b>Commande ouverte</b></dt>
  <dd>Commande client ou fournisseur qui correspond à un contrat portant généralement sur
    un an pour la fourniture de pièces à un prix déterminé. Cette commande ne spécifie
    pas les dates de livraison. Les appels de livraison provoquent l‘approvisionnement ou
    l‘expédition des pièces.</dd>
  <dt><b>Composant</b></dt>
  <dd>Terme désignant un élément entrant dans la composition d‘un sous-ensemble ou d‘un
    produit fini, dit composé.</dd>
  <dt><b>Composé</b></dt>
  <dd>Produit fini ou sous-ensemble qui se compose de plusieurs éléments appelés
    composants. La description de la composition figure dans sa nomenclature directe.</dd>
  <dt><b>Coût complet</b></dt>
  <dd>Méthode de calcul des coûts de revient des produits d‘une entreprise qui prend en
    compte l‘ensemble des charges de production fixes et variables.</dd>
  <dt><b>Coût direct</b></dt>
  <dd>Coût qui peut être affecté sans calcul intermédiaire ou imputation à un produit,
    une famille de produits, une machine, un atelier.</dd>
  <dt><b>Coût fixe</b></dt>
  <dd>Coût qui reste constant tant que le volume de production ne sort pas de certaines
    limites (loyer, chauffage, amortissements, etc.).</dd>
  <dt><b>Coût indirect</b></dt>
  <dd>Coût qui ne peut être affecté directement et doit donc être affecté par une clef de
    répartition selon le nombre d‘unités d‘oeuvre consommées.</dd>
  <dt><b>Coût standard</b></dt>
  <dd>Coût prévisionnel servant de base à l‘établissement des budgets et fondé sur
    l‘observation des coûts passés et de normes techniques.</dd>
  <dt><b>Couverture</b></dt>
  <dd>Rapport entre la quantité des ventes et celui des stocks.</dd>
  <dt><b>Cycle de fabrication</b></dt>
  <dd>C‘est l‘intervalle de temps nécessaire entre la date de lancement et la date d‘entrée
    en stock de produits finis ou la date de livraison. C‘est la somme des temps de
    préparation et réglage, des temps opératoires, des temps d‘attente et de transfert
    interopération, et des délais administratifs de préparation du lancement. Il figure
    dans la zone Délai d‘obtention de la fiche article.</dd>
  <dt><b>Cycle total</b></dt>
  <dd>C‘est l‘intervalle le plus long pour fabriquer un article à partir de ses composants si
    aucun composant n‘est disponible. Est calculé par la fonction &quot;Calcul des codes de
    plus bas niveau&quot;. Est indiqué dans la fiche Article.</dd>
</dl>

<a name="D">D</a>

  <dl compact>

  <dt><b>Dates de validité</b></dt>
  <dd>Suite aux évolutions techniques d‘un produit, dates de début et de fin de prise en
    compte d‘un lien de nomenclature dans le calcul des besoins.</dd>
  <dt><b>Décalage de lien</b></dt>
  <dd>Mesure le temps séparant le début de la fabrication d‘un produit de la date de besoin
    effective d‘un composant dans le processus de fabrication.</dd>
  <dt><b>Délai d‘obtention</b></dt>
  <dd>Nombre de jours qu‘il faut normalement pour approvisionner ou fabriquer un lot
    d‘articles. Dans un calcul de besoins, c‘est le temps qui sépare les dates des mises à
    disposition des composants et du composé (plus généralement, des différents niveaux de
    la nomenclature). Est saisi dans la fiche Article.</dd>
  <dt><b>Délai de réaction</b></dt>
  <dd>Nombre de jours qu‘il faut normalement pour pouvoir fabriquer un lot d‘articles à
    partir de ses composants. A la différence du cycle total, on considère que les
    composants qui sont gérés sur stock ont un délai d‘obtention nul. Est calculé par la
    fonction &quot;Calcul des codes de plus bas niveau&quot;. Est indiqué dans la fiche
    Article.</dd>
  <dt><b>Diagramme de Gantt</b></dt>
  <dd>Tableau dans les colonnes duquel on indique une échelle de temps, dans les lignes, les
    différentes opérations d‘un ordre de fabrication y sont représentées par des bandes
    proportionnelles à leur durée et disposées selon les dates de jalonnement.</dd>
  <dt><b>Disponible à la vente</b></dt>
  <dd>Quantité d‘un article pour laquelle on peut accepter une commande en fonction du
    disponible prévisionnel (stock disponible et ordres de fabrication prévus)</dd>
    </dl>
    <a name="E">E</a>

    <dl compact>

  <dt><b>Éclatement du plan industriel et commercial</b></dt>
  <dd>L‘éclatement est l‘opération qui consiste à reporter au niveau des articles
    fabriqués les plans industriels et commerciaux en multipliant les quantités programmées
    par les coefficients figurant dans les liens des nomenclatures de planification.</dd>
  <dt><b>En-Cours</b></dt>
  <dd>Tout composant, sous-ensemble ou produit en cours de fabrication, se situant donc entre
    le magasin de matières premières et le stock de produits finis.</dd>
    </dl>
    <a name="F">F</a>

    <dl compact>

  <dt><b>Fantôme</b></dt>
  <dd>Sous-ensemble créé dans le seul but de structurer une nomenclature, mais qui n‘a pas
    obligatoirement d‘existence comme groupe physique de pièces.</dd>
  <dt><b>Fiche de stock</b></dt>
  <dd>Fiche où l‘on note le stock physique de départ, puis les mouvements de stock (entrées
    et sorties) souvent en calculant à chaque fois le solde.</dd>
  <dt><b>Fiche suiveuse</b></dt>
  <dd>Fiche créée lors du lancement. Elle énumère la succession des opérations qui
    doivent être effectuées sur un ordre de fabrication (OF); elle guide le cheminement du
    lot de pièces dans les ateliers. Elle peut servir également à l‘enregistrement des
    consommations et des temps passés réels.</dd>
  <dt><b>Fournitures</b></dt>
  <dd>Articles achetés par l‘entreprise pour permettre la fabrication de ses productions,
    mais qui ne sont généralement pas spécifiés dans la nomenclature des produits (cf.
    consommables).</dd>
    </dl>
    <a name="G">G</a>

    <dl compact>

  <dt><b>Gamme de fabrication</b></dt>
  <dd>Document émis par le Bureau des Méthodes qui indique, pour la fabrication d‘une pièce
    ou l‘assemblage d‘un sous-ensemble ou d‘un produit fini : la liste logique des
    opérations, les natures et les quantités des matières premières nécessaires, les
    machines, outillages et outils, les temps standards de préparation, de réglage et
    d‘opération.</dd>
  <dt><b>Gamme budget</b></dt>
  <dd>Gamme figée qui sert de base à la constitution des budgets.</dd>
  <dt><b>Gamme de lancement</b></dt>
  <dd>Parmi toutes les gammes qui peuvent être utilisées pour la fabrication d‘un article,
    la gamme de lancement est celle qui est prise en compte automatiquement pour les
    lancements à venir.</dd>
  <dt><b>Gamme mère</b></dt>
  <dd>Gamme de base utilisée pour définir par duplication et modification les gammes
    spécifiques à des produits similaires.</dd>
  <dt><b>Gamme de remplacement</b></dt>
  <dd>Gamme créée pour être éventuellement choisie lors du lancement, en remplacement de
    la gamme principale, notamment en cas de surcharge ou de panne de certaines machines ou
    postes de travail.</dd>
  <dt><b>Goulet d‘étranglement</b></dt>
  <dd>Poste de charge dont la capacité est inférieure en moyenne à celle des autres postes
    intervenant dans le processus. C‘est ce poste qui limite la capacité globale de
    production.</dd>
  <dt><b>Groupage</b></dt>
  <dd>Action consistant à définir les quantités des ordres de fabrication ou d‘achat en
    regroupant les besoins suivant des critères économiques ou techniques. Les règles de
    groupage des articles sont précisées dans l‘onglet &quot;Paramètres&quot; de la fiche
    Article.</dd>
    </dl>
  
    <a name="I">I</a>

    <dl compact>

  <dt><b>Implosion des coûts</b></dt>
  <dd>Action de calculer le coût d‘un composé à partir des coûts de ses composants. La
    procédure d‘implosion des coûts part des articles de plus bas niveau (les articles
    achetés) et remonte jusqu‘aux produits finis.</dd>
  <dt><b>Inventaire</b></dt>
  <dd>État détaillé article par article décrivant le nombre, l‘état (matières
    premières, en-cours, produits finis) et la valeur des différents stocks de l‘entreprise.
    On peut faire des inventaires périodiques (par exemple une fois par an) ou des
    inventaires tournants qui consistent à contrôler le niveau du stock sur des parties du
    magasin ou des articles pour éviter la pointe de charge de l‘inventaire complet</dd>
  </dl>

  <a name="J">J</a>

  <dl compact>
  <dt><b>Jalonnement</b></dt>

  <dd>Aussi appelé ordonnancement à capacité infinie.</dd>
  <dd><br>
    Dans le jalonnement au plus tôt, on positionne les opérations en partant de la première
    opération à la date de lancement de l‘OF sur les plages horaires définies dans les
    calendriers associés aux postes de charge concernés par chaque opération. <br>
    Dans le jalonnement au plus tard, on remonte depuis la fin de la dernière opération
    positionnée à sa date de besoin de l‘OF. </dd>
  <dt><b>Jour ouvrable</b></dt>
  <dd>Un jour ouvrable est un jour pour lequel on a défini une durée dans l‘une des trois
    équipes sur le calendrier standard (CS).</dd>
</dl>

  <a name="L">L</a>

    <dl compact>
  <dt><b>Lancement</b></dt>
  <dd>Émission d‘un ordre de fabrication. Il doit se faire en cohérence avec
    l‘ordonnancement et après s‘être assuré que les matières, composants et outillages
    sont disponibles.</dd>
  <dt><b>Lien de nomenclature</b></dt>
  <dd>Relation entre un composé et un composant. C‘est le fichier informatique des liens qui
    sert à représenter les nomenclatures arborescentes.</dd>
  <dt><b>Lissage de charge</b></dt>
  <dd>Action qui consiste à rejalonner les ordres de fabrication de façon que la charge de
    travail n‘excède pas la capacité. Certains logiciels réalisent un lissage automatique
    suivant des règles de priorité entre les ordres.</dd>
  <dt><b>Lot de fabrication</b></dt>
  <dd>Quantité d‘un même article qui restera groupée pour toutes les opérations de
    production. Représente physiquement un ordre de fabrication.</dd>
  <dt><b>Lot de transfert</b></dt>
  <dd>Lorsqu‘il est possible d‘organiser le chevauchement des opérations de production, le
    lot de transfert est la fraction minimum du lot de fabrication qui peut être transmise
    d‘un poste à l‘autre.</dd>
  <dt><b>Lot standard</b></dt>
  <dd>Quantité standard de fabrication qui sert au calcul des coûts des gammes en
    répartissant les coûts de lancement sur les articles du lot standard.</dd>
    </dl>
    <a name="M">M</a>

    <dl compact>
  <dt><b>Main-d‘oeuvre directe</b></dt>
  <dd>Main-d‘oeuvre dont le coût peut être affecté à un produit. Ce sont les opérateurs
    de production.</dd>
  <dt><b>Main-d‘oeuvre indirecte</b></dt>
  <dd>Main-d‘oeuvre dont le coût doit être réparti entre plusieurs produits de l‘entreprise
    (ex. : personnel de maintenance, de la chaufferie).</dd>
  <dt><b>Manquant</b></dt>
  <dd>Article qui n‘est pas disponible alors que l‘ordre de fabrication ou de vente sur lequel
    il figure doit être lancé.</dd>
  <dt><b>Marge</b></dt>
  <dd>1- Planification : intervalle de temps dont on peut disposer sans perturber les autres
    opérations de production.</dd>
  <dd><br>
    2- Comptabilité : différence entre le prix de vente et le coût de revient. </dd>
    </dl>
    <a name="N">N</a>

    <dl compact>
  <dt><b>Nomenclature</b></dt>
  <dd>Décomposition d‘un produit en ses sous-ensembles, pièces élémentaires et matières
    premières, avec le nombre d‘unités de chaque constituant nécessaire à la fabrication
    d‘une unité de produit fini.</dd>
  <dt><b>Nomenclature arborescente</b></dt>
  <dd>Présentation de la nomenclature d‘un produit par niveaux successifs, par exemple :
    niveau 0, produit fini; niveau 1, sous-ensembles; niveau 2, pièces élémentaires; niveau
    3, matières premières. Elle fait ainsi apparaître les liens et les structures
    hiérarchiques entre les constituants et permet la mise en place du processus de
    production et son ordonnancement.</dd>
  <dt><b>Nomenclature cumulée</b></dt>
  <dd>Liste des composants élémentaires et de leurs coefficients d‘utilisation dans le
    produit fini. Contrairement à la nomenclature arborescente, elle ne fait pas apparaître
    la structure du produit (liens et niveaux).</dd>
  <dt><b>Nomenclature Ressource</b></dt>
  <dd>Gamme de fabrication simplifiée qui considère des opérations groupées suivant les
    principales phases d‘élaboration du produit. Ces &quot;macrogammes&quot; sont utilisées
    dans l‘élaboration du plan directeur pour calculer la charge prévisionnelle de travail.
    Elle s‘applique à des articles fictifs (article Plan directeur), créés pour
    représenter un ensemble de références voisines.</dd>
    </dl>
    <a name="O">O</a>

    <dl compact>
  <dt><b>Opération de fabrication</b></dt>
  <dd>Subdivision de la gamme opératoire. Une opération ne fait appel qu‘à une machine dans
    un réglage déterminé.</dd>
  <dt><b>Ordonnancement</b></dt>
  <dd>Opération qui consiste à définir l‘ordre de passage des lots ou des commandes sur les
    différents postes de travail d‘un atelier.</dd>
  <dt><b>Ordonnancement centralisé</b></dt>
  <dd>Méthode d‘ordonnancement dans laquelle les informations concernant les programmes
    d‘abord prévisionnels puis réels, et leur avancement, sont regroupées pour permettre
    des actions correctives et l‘information des fournisseurs, des services concernés ou des
    clients.</dd>
  <dt><b>Ordonnancement décentralisé</b></dt>
  <dd>Cette méthode d‘ordonnancement se limite à la gestion des files d‘attente que
    constituent les lots devant chaque poste de travail. Des règles de priorité définissent
    l‘ordre de passage des lots pour atteindre des objectifs tels que le respect des dates de
    livraisons, la réduction des en-cours, l‘optimisation du temps d‘utilisation des
    machines.</dd>
  <dt><b>Ordre de fabrication</b></dt>
  <dd>Document donnant instruction à la fabrication de produire dans un délai fixé, une
    quantité donnée d‘un article.</dd>
  <dt><b>Origine des charges</b></dt>
  <dd>Information qui identifie les ordres de fabrication ayant engendré une charge
    prévisionnelle sur un poste de charge.</dd>
    </dl>
    <a name="P">P</a>

    <dl compact>
  <dt><b>Plan industriel et commercial</b></dt>
  <dd>Programme de production à moyen terme qui fixe les quantités qui devraient être
    produites. Le plan cherche à tirer le meilleur parti possible des moyens de production
    actuels ou à acquérir, et à gérer la sous-traitance pour faire face à la demande et
    atteindre les niveaux de stock désirés période par période et référence par
    référence.</dd>
  <dt><b>Plan de production</b></dt>
  <dd>Engagement de production à moyen terme qui établit dans les grandes lignes les
    quantités à produire.</dd>
  <dt><b>Planification hiérarchisée</b></dt>
  <dd>Méthode qui consiste à planifier une succession de programmes cohérents, depuis le
    plan de production à moyen terme des types de produits ou des familles jusqu‘au programme
    de fabrication à court terme des références élémentaires.</dd>
  <dt><b>Planning de charge</b></dt>
  <dd>Calcul de la charge de travail correspondant à un programme de fabrication,
    positionnement de cette charge dans le temps et comparaison à la capacité disponible.</dd>
  <dt><b>Planning des entrées-sorties</b></dt>
  <dd>Méthode de régulation du flux de production, qui consiste à mesurer la baisse de
    charge résultant des ordres de fabrication terminés pour prendre les décisions de
    lancement (entrées).</dd>
  <dt><b>Point de commande</b></dt>
  <dd>Système de gestion de stock consistant à passer une commande ou à lancer un ordre de
    fabrication lorsque la consommation du stock le ramène à un niveau dit &quot;point de
    commande&quot;. La quantité lancée ou commandée est fixe. Elle est calculée selon des
    critères économiques.</dd>
  <dt><b>Post-consommation</b></dt>
  <dd>Méthode consistant à effectuer comptablement la sortie des composants nécessaires à
    la réalisation d‘un OF à partir des déclarations d‘entrée en stock, donc après leur
    sortie physique. Cela allège les procédures administratives.</dd>
  <dt><b>Poste de charge</b></dt>
  <dd>Ensemble de machines ou d‘opérateurs qui peuvent être considérés comme équivalents
    du point de vue des travaux à effectuer.</dd>
  <dt><b>Prévisions commerciales</b></dt>
  <dd>Évaluation de la demande future à long ou moyen terme, en vue notamment d‘établir un
    plan directeur de production. On fait les prévisions commerciales sur des articles
    fictifs qui représentent des familles d‘articles vendus.</dd>
  <dt><b>Profil de charge</b></dt>
  <dd>Répartition dans le temps des heures de travail affectées à un poste de charge.</dd>
  <dt><b>Profil de ressources</b></dt>
  <dd>Répartition dans le temps des consommations des différentes ressources nécessaires à
    la réalisation d‘un produit. Les ressources peuvent être heures de travail, heures de
    réglage, approvisionnements, trésorerie, etc.</dd>
  <dt><b>Progamme directeur</b></dt>
  <dd>Programme qui présente les entrées et les sorties prévisionnelles, fermes ou
    suggérées, pour chaque article. Son horizon est de quelques semaines à quelques mois.</dd>
    </dl>
    <a name="Q">Q</a>

    <dl compact>
  <dt><b>Quantité économique</b></dt>
  <dd>Quantité à lancer en fabrication ou à approvisionner pour minimiser la somme des
    coûts de possession du stock et des coûts de lancement ou de passation de commande.</dd>
    </dl>
    <a name="R">R</a>

    <dl compact>
 
  <dt><b>Recomplètement périodique</b></dt>
  <dd>Système de gestion de stock consistant à ramener, par commande ou lancement, le stock
    à un niveau dit de &quot;recomplètement&quot;. Cette opération s‘effectue selon une
    périodicité fixe; en revanche, la taille du lancement ou de la commande est variable.
    Elle est égale à la consommation depuis la dernière commande ou le dernier lancement.</dd>
  <dt><b>Ressource critique</b></dt>
  <dd>Toute ressource susceptible de limiter la capacité de production (ex. : main-d‘oeuvre,
    machines, approvisionnements, trésorerie).</dd>
    </dl>
    <a name="S">S</a>

    <dl compact>
  <dt><b>Section auxiliaire</b></dt>
  <dd>Dans un plan comptable analytique, centre d‘accumulation et d‘analyse des coûts dont
    les charges correspondent à des prestations qui doivent être réparties entre les
    centres qui utilisent leurs services (ex. : chauffage, maintenance). Cette répartition se
    fait par le biais d‘unités d‘oeuvre.</dd>
  <dt><b>Section principale</b></dt>
  <dd>Dans un plan comptable analytique, centre d‘accumulation et d‘analyse des coûts, dont
    les charges peuvent être directement imputées par des unités d‘oeuvre aux comptes de
    coûts de revient des produits (ex. : sections de production, d‘approvisionnement).</dd>
  <dt><b>Sous-traitance</b></dt>
  <dd>Action qui consiste à confier à une entreprise extérieure la réalisation de tout ou
    partie d‘un produit dont on garde la maîtrise d‘oeuvre.</dd>
  <dt><b>Stock d‘anticipation</b></dt>
  <dd>Stock constitué avant la date normale d‘utilisation en prévision d‘un phénomène à
    venir, notamment une charge dépassant la capacité dans une industrie saisonnière.</dd>
  <dt><b>Stock de sécurité</b></dt>
  <dd>Stock constitué pour éviter les ruptures qui peuvent être la conséquence des aléas
    sur la demande, le délai d‘approvisionnement ou la quantité livrée. Son volume et donc
    son coût varient en fonction du niveau de protection désiré (ou taux de service).</dd>
    </dl>
    <a name="T">T</a>

    <dl compact>
  <dt><b>Taux d‘utilisation</b></dt>
  <dd>Rapport entre le temps effectif de travail d‘un équipement et le temps ouvré.</dd>
  <dt><b>Temps alloué</b></dt>
  <dd>Temps imparti à un opérateur pour réaliser une tâche; il constitue une référence
    pour la mesure des performances.</dd>
  <dt><b>Temps d‘attente</b></dt>
  <dd>Durée pendant laquelle le lot reste en attente devant le poste de travail, soit que
    celui-ci n‘est pas disponible, soit que se trouvent dans la file d‘attente des lots de
    priorités plus élevées.</dd>
  <dt><b>Temps copeaux</b></dt>
  <dd>C‘est le temps pendant lequel une machine réalise effectivement une transformation
    physique sur la pièce qui lui est confiée. Le temps copeaux est le seul pendant lequel
    on produit de la valeur ajoutée au produit. L‘analyse montre habituellement qu‘il est faible
    comparé au temps de présence des pièces dans le système productif (cycle de
    production).</dd>
  <dt><b>Temps de préparation</b></dt>
  <dd>Durée pendant laquelle la machine est en cours de préparation (nettoyage, changement
    d‘outillage et réglage) avant que le lot puisse être traité.</dd>
  <dt><b>Temps standard</b></dt>
  <dd>Temps déterminé par les spécialistes de l‘organisation scientifique du travail pour
    accomplir une tâche donnée dans des conditions d‘exécution normales.</dd>
  <dt><b>Temps de transfert</b></dt>
  <dd>Durée pendant laquelle le lot est considéré comme ayant quitté le poste précédent
    mais n‘est pas encore disponible pour le poste suivant (recouvre le temps de déplacement
    du lot d‘un poste au suivant).</dd>
    </dl>
    <a name="U">U</a>

    <dl compact>
  <dt><b>Unité d‘achat</b></dt>
  <dd>Unité de vente du fournisseur d‘un article. L‘unité d‘achat peut être différente de
    l‘unité d‘emploi pour l‘entreprise cliente. Par exemple, un fournisseur peut vendre de la
    tôle en rouleaux et le client la mesurer en mètres carrés ou en kilos. Un coefficient
    de conversion permet de passer de l‘une à l‘autre des unités.</dd>
  <dt><b>Unité de gestion</b></dt>
  <dd>Quantité de référence (tonne, mètre, mètre cube) pour mesurer les flux et stocks de
    pièces dans le processus de production.</dd>
  <dt><b>Unité d‘oeuvre</b></dt>
  <dd>Unité exprimant, en langage commun à la production et à la comptabilité analytique,
    la mesure des quantités produites ou susceptibles d‘être produites (litres, kilogrammes,
    mètres, heures standards).</dd>
    </dl>
    <a name="V">V</a>

    <dl compact>
  <dt><b>Valorisation des stocks</b></dt>
  <dd>Évaluation des stocks à leur coût d‘achat ou de production; la valeur vénale au jour
    de la valorisation, si elle est inférieure, peut aussi être retenue.</dd>
</dl>



