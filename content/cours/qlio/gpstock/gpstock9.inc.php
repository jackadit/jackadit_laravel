<h2>Les choix stratégiques</h2>

<h3>Place de l‘étude d‘entrepôt dans la stratégie logistique d‘entreprise</h3>

<img src="/assets/img/qlio/gpstock/strategie_logistique.png" alt="Strategie logistique" />


<p>Le schéma ci-dessus (adapté par A.K. Samii de William C. Copacino) montre les dix composantes d‘une stratégie logistique. Il va de soi que la conception, l‘étude de positionnement et l‘étude d‘un ou plusieurs entrepôts ou plates-formes doivent s‘intégrer dans la stratégie logistique de l‘entreprise et ne sauraient être réalisés indépendamment d‘un plan logistique.</p>

<ul>
  <li>Le <span class="tag--info">niveau de service clientèle</span> est la décision stratégique de base qui recouvre l‘établissement d‘un éventail de services logistiques pour chaque segment de clientèle.</li>
  <li>La <span class="tag--info">conception du réseau de distribution</span> permet de définir le rôle que l‘on veut donner à des distributeurs ou d‘autres intermédiaires par rapport à ses moyens propres.</li>
  <li>La stratégie en matière de réseaux physiques permet de déterminer les cycles logistiques, le rôle, l‘emplacement et la taille des entrepôts et plates formes, d‘en évaluer les coûts et d‘en mesurer l‘impact sur le service client. Bien entendu, la détermination de cette stratégie a pour rôle d‘arbitrer entre transports et stockage selon plusieurs scénarios étudiés avec les données disponibles et des méthodes d‘optimisation.</li>
  <li>Dans le choix du mix logistique ( Marketing Mix : terme de marketing désignant l'ensemble des éléments qui constituent ce que l'on propose au client : produit, prix, promotion, niveaux de service. Ce dernier élément se décompose lui-même en d'autres qui constitue le mix logistique), la conception des entrepôts et de leur gestion doit répondre à des questions importantes sur l‘externalisation ou non des entrepôts et plates formes et de leur gestion. C‘est à cette occasion que seront tranchées les problèmes de positionnement fin, d‘automatisation ou non et d‘organisation des entrepôts.</li>
  <li>L‘application des décisions précédentes va se traduire en ce qui concerne les entrepôts et plates-formes par des procédures techniques de réalisation sensiblement différentes selon les options prises au niveau précédentes. </li>
</ul>

