    <p>Lors de la mise en place d‘une nouvelle plate-forme ou d‘un nouvel entrepôt, l‘entreprise peut penser qu‘un tel investissement n‘entre pas dans son cœur de métier et risque d‘immobiliser des capitaux pour lesquels elle saurait trouver un meilleur retour sur investissement dans ses activités propres. Une autre formule consiste à réaliser un leasing pour l‘acquisition de tels locaux. Par ailleurs, il arrive que des entreprises, soucieuses par exemple de se désendetter, revendent leurs locaux -et particulièrement leurs entrepôts - à des sociétés immobilières spécialisées qui les leurs louent ensuite. Ceci leur permet de transformer des immobilisations en loyers et de récupérer le produit de la vente soit pour se désendetter, soit pour procéder à des investissements dans leur cœur de métier. L‘avantage est souvent d‘avoir des locaux adaptés à leur activité propre. </p>
  <li><strong>L‘exploitation dans les locaux de l‘entreprise mais par une entreprise logistique extérieure</strong></li>
    <p>C‘est une formule qui tend à se répandre parmi ceux qui cherchent à externaliser leur activité logistique sans perdre l‘avantage des locaux dont ils disposent par exemple au sein d‘une usine. </p>
  <li><strong>L‘exploitation par une entreprise logistique extérieure dans des locaux dédiés</strong></li>
    <p>C‘est la formule classique de l‘externalisation. Elle présente cependant l‘inconvénient d‘exiger la création de nouveaux locaux pour lesquels l‘entreprise logistique ne s‘engagera que sur plusieurs années, ce qui enlève une part importante de la flexibilité de l‘ externalisation. Cet engagement de plusieurs années permettra cependant à l‘entreprise logistique d‘acquérir progressivement la maîtrise des flux et donc de réaliser progressivement des gains de productivité. Il importe de réaliser un partage équitable de ces gains soit par une formule de réduction progressive des coûts selon un programme déterminé à l‘avance, soit par un système d‘ "open book", mise à disposition par la société logistique de ses livres de compte et partage des bénéfices réalisés selon une règle déterminée à l‘avance. </p>
  <li><strong>L‘exploitation par une entreprise logistique extérieure dans des locaux banalisés ("entrepôts publics")</strong></li>
  <p>C‘est une formule qui présente l‘avantage de permettre une régularisation des charges entre différentes entreprises ayant des variations saisonnières (ventes, sorties de stock, transports, etc…qui dépendent des jours de la semaine ou des périodes de l'année (saisons, vacances, météo, fêtes, etc…))  compatibles.</p>
</ul>

<h3>Entrepôts privés et entrepôts publics</h3>

<p>Le choix entre entrepôt public ou entreposage privé est en principe simple et souvent présenté ainsi :</p>

<ul>
<li>l‘<span class="tag--info">entreposage privé</span> comporte des frais fixes (immeubles, moyens de stockage et manutentions, etc.) et des frais variables (personnel, chauffage, électricité, etc.),</li>
<li>l‘<span class="tag--info">entreposage public</span> se paye en fonction de tarifs qui tiennent compte de l‘activité (stocks, flux d‘entrée et de sortie, etc.).</li>
</ul>

<p>En réalité le problème est beaucoup plus complexe :
<ul>
  <li>Avec des prestataires, il y a souvent un minimum de facturation ou des accords de réservation de surface et de moyens qui ressemblent à des charges fixes (tout au moins pour obtenir des prix intéressants) et d‘autre part les prix sont le plus souvent dégressifs et non pas proportionnels aux volumes.</li>
  <li>Pour l‘entreposage privé, la part des charges variables est réduite car le personnel est normalement stable et l‘appel à du personnel intérimaire est limité. Néanmoins, en fonction du degré d‘automatisation de l‘entrepôt, il existe des différences très sensibles de coûts .</li>
  <li>L‘exploitation par une entreprise logistique dans des locaux dédiés ou dans ses propres locaux suppose des accords d‘assez longue durée avec des engagements réciproques.</li>
</ul>

<p>Les choix entre les différentes formules doivent donc être étudiés au cas par cas. Cependant, en ce qui concerne l‘investissement et les charges, il existe des différences importantes entre les divers modes.</p>

<h3>Tendance à la réduction du nombre des entrepôts</h3>

<p>Depuis quelques années, on constate une tendance forte à la réduction du nombre d‘ entrepôts. Ceci résulte de deux facteurs :</p>
<ul>
  <li>l‘accélération des vitesses des flux au sein de la supply chain et la préférence apportée au "Juste à temps" : cette tendance pourrait être freinée dans l‘avenir par l‘ augmentation des coûts des transports routiers;</li>
  <li>La variabilité d‘une demande exprime l‘importance des variations de la demande autour d‘une moyenne. Une demande qui varie chaque mois entre 80 et 120 unités n‘est pas identique à une demande qui varie entre 20 et 180 chaque mois, même si ces deux demandes ont la même moyenne. Cette variabilité s‘exprime le plus souvent en écarts-types ou écarts absolus moyens. Cette variabilité de la demande oblige à avoir des " stocks de sécurité " d‘autant plus importants que la variabilité est grande. 
  </li>
</ul>
<p> On démontre que lorsqu‘on regroupe les demandes d‘un même produit, par exemple les demandes de plusieurs entrepôts équivalents sur un seul entrepôt, cette variabilité du regroupement n‘est pas proportionnelle au nombre d‘entrepôts regroupées mais à la racine de ce nombre.</p>
<p>Par exemple si l‘on regroupe 4 entrepôts ayant chacun un stock de sécurité de 100 unités d‘un article , soit 400 unités au total , le stock de sécurité de l‘entrepôt unique sera avec le même taux de service de 100 x racine de 4 = 200.</p>
<p>Cette réduction du nombre des entrepôts accompagnée d‘une augmentation de leur importance permet de mettre en œuvre économiquement des techniques automatisées et des stockages de grande hauteur avec une réduction des coûts. </p>



