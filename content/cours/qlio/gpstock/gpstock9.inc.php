<h3>Entrepôts multiples :</h3>

<ul>
  <li>Déterminer le nombre et le positionnement d‘un ensemble d‘entrepôts.</li>
</ul>


<p>On peut tout d‘abord vouloir déterminer l‘emplacement optimal d‘une plate-forme ou d‘un entrepôt dont on connaît tous les points à approvisionner à partir de cet entrepôt et tous les points d‘où proviennent les marchandises. C‘est le problème de l‘entrepôt unique.</p>

<p>Assez souvent c‘est un problème de plate-forme ou d‘entrepôt régional pour un groupe industriel : les marchandises proviennent d‘un petit nombre de points relativement éloignés, une ou plusieurs usines, mais les points à livrer sont nombreux et tous dans une même région ; il s‘agit alors de trouver l‘emplacement optimal dans cette région et les limites à donner à cette région.</p>

<p>Un distributeur rencontrera un problème très semblable dans la mesure où il considère les surfaces de vente d‘une région, surfaces qu‘il doit approvisionner à partir de sa plate-forme. Les marchandises proviennent de multiples fournisseurs assez souvent franco et ces fournisseurs peuvent changer très souvent de telle sorte que ce qu‘il doit optimiser, ce sont les transports depuis la plate-forme vers les points de distribution.</p>

<p>Le problème peut être un peu plus complexe si l‘on ne se limite pas à envisager la position d‘un seul entrepôt mais d‘un nombre d‘entrepôts à déterminer sur un territoire assez vaste pour optimiser les transports vers tous les clients de ce territoire. Il va falloir déterminer à la fois :</p>
<ul>
  <li>Le nombre optimal d‘entrepôts ou plate-forme.</li>
  <li>Les emplacements optimaux de ces sites.</li>
</ul>


<h3>Le principe du barycentre</h3>

<p>La méthode du barycentre permet de déterminer un point théorique optimal d'implantation d'un entrepôt ou à l'intérieur d'un entrepôt (ex : poste de filmage).</p>

<p>Les coordonnées des points à desservir peuvent être pondérées par un indicateur de trafic exprimé en poids, volume, distance, nombre de lignes de commandes, ou chiffre d'affaires...<br />
Ceci en fonction de :</p>
<ul>
  <li>des modes d'acheminement,</li>
  <li>des axes de communication et de circulation,</li>
  <li>des limites et des opportunités du site,</li>
  <li>du coût de revient de l'implantation.</li>
</ul>

<p>Le but étant de réduire les coûts de déplacements ou les distances, afin de se placer au plus près de ses clients (internes ou externes). Les clients n'ont pas toujours la même importance, il faut donc en tenir compte également dans la pondération.</p>

<p>Pour comprendre le principe du barycentre on peut s‘imaginer le dispositif suivant :</p>
<p><img src="/assets/img/qlio/gpstock/principe_barycentre.png" alt="Principe du barycentre" />Principe du barycentre</p>
<p>&nbsp</p>
<ul>
  <li>d‘une table avec un plan de la région à couvrir,</li>
  <li>de trous à l‘emplacement de chacun des points à livrer,</li>
  <li>d‘un anneau relié par des fils à des poids proportionnels au tonnage à transporter à ce point,</li>
  <li>chaque fil passe par un des trous représentant l‘emplacement d‘un point de livraison.</li>
</ul>
<p>L‘anneau se positionne spontanément au "barycentre" du plan, là où devrait être l‘entrepôt pour minimiser les tonnes - kilomètres.</p>

<p>Un tel dispositif n‘est jamais réalisé. Il est strictement pédagogique. On le reconstitue par le calcul :</p>
<ul>
  <li>on détermine les distances entre chaque site susceptible d‘être retenu comme site de l‘entrepôt et chaque point de livraison,</li>
  <li>on détermine les quantités à livrer en chaque point pour une période de temps représentative,</li>
  <li>on calcule ensuite pour chaque site la somme des produits "Tonnes x Kilomètres".</li>
</ul>
<p>Le site pour lequel cette somme est minimale est optimal pour ce critère.</p>

<h4>Méthodologie</h4>
<ul>
<li><strong>Étape 1 : Corriger le trafic « <strong>t</strong> » si l'implantation est différée dans le temps.</strong>
<p>Dans ce cas, Multiplier le trafic « <strong>t</strong> » par la valeur relative de son évolution, élevée à la puissance « <strong>n</strong> » correspondant au délai de réalisation, pour obtenir « n<sub>i</sub> » :
<ul>
  <li>Si l'évolution est positive, n<sub>i</sub> = t * [(1 + évolution)<sup>n</sup>]</li>
  <li>Si l'évolution est négative, n<sub>i</sub> = t * [(1 - évolution)<sup>n</sup>]</li>
</ul></li>
<li><strong>Étape 2 : Calculer la somme « n<sub>i</sub> »</strong>. C‘est-à-dire, effectuer la somme de toutes les valeurs de trafic « <strong>t</strong> » eventuellement corrigé si un délai existe.</li>
<li><strong>Étape 3 : Placer les points dans un repère orthonormé.</strong>
  <p>Sur un plan, prendre l'origine « O » du système à l'extrême gauche des points à desservir, pour éliminer les valeurs négatives.</p>
  <p>Tracer et identifier les axes :</p>
  <ul>
    <li>l'axe horizontal est l'axe des abscisses « x »</li>
    <li>l'axe vertical est l'axe des ordonnées « y ».</li>
  </ul>
</li>
<li><strong>Étape 4 : Relever les coordonnées des points connus.</strong>
<p>Mesurer les distances séparant chaque point des axes :</p>
<ul>
  <li>Projeter le point sur l'axe des abscisses, on obtient l'ordonnée « y<sub>i</sub> » ;</li>
  <li>Projeter le point sur l'axe des ordonnées, on obtient l'abscisse « x<sub>i</sub> ».</li>
</ul>

</li>
<li><strong>Étape 5 : Pondérer les coordonnées par la valeur du trafic « <strong>t</strong> » et totaliser.</strong>
<p>Multiplier chaque coordonnée par le trafic correspondant (n<sub>i</sub>) pour obtenir les valeurs « x<sub>i</sub>n<sub>i</sub> » et «y<sub>i</sub>n<sub>i</sub> ».</p>
<p>Totaliser l'ensemble des « x<sub>i</sub>n<sub>i</sub> » puis l'ensemble des « y<sub>i</sub>n<sub>i</sub> ».</p>
</li>
<li><strong>Étape 6 : Calculer les coordonnées « X » et « Y » du point d'implantation optimale.</strong>
<div class="eq-c">
    Abscisse « X » du point =
    <div class="fraction">
        <span class="numerateur">&Sigma;(x<sub>i</sub>n<sub>i</sub>)</span>
        <span class="bar">/</span>
        <span class="denominateur">&Sigma;n<sub>i</sub></span>
    </div>
</div>
<div class="eq-c">
    Ordonnée « Y » du point =
    <div class="fraction">
        <span class="numerateur">&Sigma;(y<sub>i</sub>n<sub>i</sub>)</span>
        <span class="bar">/</span>
        <span class="denominateur">&Sigma;n<sub>i</sub></span>
    </div>
</div>

<li><strong>Étape 7 : Localiser le point d'implantation.</strong>
<p>Reporter, sur le plan, les coordonnées du point d'implantation optimale :</p>
<ul>
    <li>mesurer « x » sur l'axe des abscisses,</li>
    <li>mesurer « y » sur l'axe des ordonnées.</li>
</ul>
<p>Tracer, à partir de ces points, 2 perpendiculaires aux axes : l'intersection est le point d'implantation.</p>
<ul>
<li><strong>Étape 8 : Corriger éventuellement la localisation.</strong> Déplacer le point d'implantation en fonction des contraintes.</li>
</ul>

