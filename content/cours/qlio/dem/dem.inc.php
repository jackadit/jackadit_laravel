<h1>Gestion de la demande</h1>

	<h2>Objectifs du module</h2>
	<p>À l'issue du module, l'étudiant doit être capable de :</p>
	<ul>
	<li>Identifier les données techniques nécessaires à la dématérialisation d'un processus, de comprendre leurs interactions</li>
	<li>Mesurer ces données sur un poste</li>
	<li>Rédiger des documents techniques sur un processus simple</li>
	</ul>
	<h2>Compétences visées</h2>
	<ul>
		<li>Étudier les postes de travail, les implantations ou les modalités de manutention et d'entreposage pour les activités demtrielles et de service, et les caractériser en termes économiques (temps standard, capacité, débit…) et en termes ergonomiques</li>
		<li>Évaluer et chiffrer les coûts et temps de fabrication, déterminer les standards de prix et les devis</li>
		<li>Mesurer la performance</li>
		<li>Mesurer les résultats obtenus, résoudre les problèmes</li>
		<li>Diagnostiquer l'efficience des postes de travail et définir des plans d'actions pour maintenir les ressources en état de bon fonctionnement</li>
	</ul>
	
	<p class="h2-like">Prérequis</p>
	<p>Aucun</p>

	<h2>Contenus</h2>
	<ul>
	  <li>Processus de dématérialisation</li>
	  <li>Moyens de production, poste de charge, poste de travail, moyens de manutention de stockage et de contrôle</li>
	  <li>Modes de description d'un processus de production (gammes, contrat de phase, schémas opératoires, …)</li>
	  <li>Cycle de fabrication, temps de cycle, simmogramme ; identifier le cycle d'une machine de production</li>
	  <li>Délais, temps opératoires, temps improductifs, causes d'attente</li>
	  <li>Apprentissage de la mesure et de l'estimation de temps</li>
	  <li>Étude et organisation d'un poste de travail (ergonomie)</li>
	  <li>Débit ou cadence d'un processus et d'un poste de travail (évaluation)</li>
	  <li>Sûreté de fonctionnement</li>
	</ul>

	<h2>Prolongements possibles</h2>
	<p>Données techniques</p>
	<p>Organisation d'un poste de travail</p>


