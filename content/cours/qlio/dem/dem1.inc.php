<h3>CRM (Customer Relationship Management).</h3>
<p>La qualité de l'interaction d'une entreprise avec ses clients peut devenir un atout stratégique et différentiateur.</p>

<p>Il offre un avantage à attirer de nouveaux clients et à créer de la fidélité parmi les clients actuels.</p>

<p>Des interactions importantes avec les clients se produisent dans un certain nombre de domaines, comme indiqué ci-dessous.</p>

<ul>
<li>Aide à la conception : aider à la conception de nouveaux produits ou à l'amélioration de ceux existants</li>
<li>Besoins des clients : évaluer les affaires du client et créer ou développer des offres de produits</li>
<li>Informations et communications : collecter et analyser des données clients pour soutenir le marketing, les ventes et le service client</li>
</ul>

<h4>Gestion des commandes</h4>
<p>La gestion de commande du client est une activité majeure de CRM et joue également un rôle majeur dans l'efficacité des opérations de la chaîne d'approvisionnement et du point de vue du service à la clientèle.</p>
<p>Ceci grâce à :</p>
<ul>
<li>Entrée et suivi rapide et précis des commandes</li>
<li>Respect des dates de livraison et des quantités promises</li>
<li>Traiter les demandes et les plaintes des clients , les retours et la réparation</li>
<li>Documentation d'expédition précise et en temps opportun, facturation et enregistrement de l'historique des ventes</li>
</ul>


<h3>Avantages qualifiants/gagnants.</h3>
<p>La détermination des caractéristiques de qualification et de gain de commande se rapporte largement à la conception de produits pour répondre aux attentes des clients : 
	the VOC (Voice of Costumer) la voix du client.</p>

<h4>Avantages qualifiants</h4>
<blockquote>Caractéristiques compétitives qu'une entreprise doit posséder pour être un concurrent viable sur le marché.</blockquote>
<p>Ainsi une entreprise peut se battre sur des caractéristiques autres que le prix, 
mais pour se qualifier sur un marché concurrentiel ses coûts et prix associés doivent se situer dans une zone donnée 
pour être acceptables pour ses clients.</p>

<h4>Avantages gagnants</h4>
<blockquote>Caractéristiques concurrentielles d'une entreprise qui incitent les clients à choisir ses produits ou services plutôt que ceux de ses concurrents.</blockquote>
<p>Les avantages gagnants se focalisent sur une (rarement plus de deux) des stratégies suivantes : le prix (coût), la qualité, le délai de livraison, la fiabilités des délais, la conception du produit, la flexibilité, le service après-vente, l'image.</p>

<h3>Planification de la demande.</h3>
<p>Les prévisions et les commandes des clients conduisent les opérations de planification, et les fabricants doivent les obtenir correctement afin de comprendre quoi, combien, et quand produire.</p>
<p>La reconnaissance des besoins du client se fait à travers :</p>
<ul>
	<li>Prévisions.</li>
	<li>Gestion des commandes de :
		<ul>
			<li>liens internes.</li>
			<li>liens externes.</li>
		</ul>
	</li>
</ul>

<h2>Place de la gestion de la demande dans la <cite lang="en">supply chain</cite>.</h2>

<p>La <cite lang="en">supply chain</cite> (chaîne d'approvisionnement) est conduite par la demande des clients (produits ou services).</p>
<p>Les producteurs de biens ou services réagissent à cette demande.</p>
<p>Les producteurs influencent et façonnent la demande par les activités de marketing et de vente.</p>
<p>Ils déterminent la demande grâce à la prévision et aux commandes des clients.</p>

