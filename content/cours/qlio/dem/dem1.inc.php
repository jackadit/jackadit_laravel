<h1>Gestion de la demande.</h1>

<h2>Processus de la gestion de la demande.</h2>

<p>L'objectif principal des solutions de gestion de la demande est d'aider les entreprises à prévoir, 
planifier et gérer la demande de produits, afin de pouvoir optimiser les approvisionnements, la production et la distribution.</p>
<p>En ce sens la gestion de la demande est le processus de base d'une chaîne d'approvisionnement optimisée.</p>
<p>La performance globale de la chaîne d'approvisionnement de l'entreprise dépend directement de la qualité du processus de gestion de la demande.</p>
<p>Les principaux bénéfices à attendre d'une bonne gestion de la demande sont :</p>
<ul>
	<li>La réduction des niveaux de stock.</li>
	<li>Un ajustement optimal et permanent des niveaux de stock à la politique commerciale voulue (taux de service).</li>
	<li>La réduction de l'obsolescence des stocks.</li>
	<li>Une meilleure productivité et réactivité des unités de production et des moyens de distribution.</li>
</ul>

<img src="../../public/img/optimized/dem/processus_gestion_demande.png" alt="Image explicant le processus de gestion de la demande" />

<p>Le processus de gestion de la demande est une interaction entre trois processus métier :</p>
<ul>
<li>Gestion commerciale (Ensemble des activités de ventes)</li>
<li>Gestion de la relation client (CRM)</li>
<li>Planification de la demande (Les prévisions et les commandes clients induisent l'activités de production )</li>
</ul>

<p>Une gestion adéquate de la demande est stratégique car elle facilite la planification et l'utilisation des ressources à travers la <cite lang="en">supply chain</cite>.</p>


<h3>Facteurs influençant la demande.</h3>

<p>Le processus de gestion commerciale s'appuie sur un ensemble d'outils appelés <span class="tag--info">marketing mix</span> ou les <span class="tag--info">4 P</span> :
<cite lang="en">Product</cite>, <cite lang="en">Price</cite>, <cite lang="en">Promotion</cite>, <cite lang="en">Place</cite> (canaux de distribution).</p>

<p>L'objectif est de tirer parti de chaque <span class="tag--info">P</span> afin de créer des produits et des services conformes à la stratégie commerciale, au prix, à la promotion et au placement dans les bons canaux de distribution

<p>Ce <span class="tag--info">marketing mix</span> permet de prendre conscience des facettes de la commercialisation, des décisions qui influencent et qui façonnent la demande.</p>

