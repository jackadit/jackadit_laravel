<h1>Écoconception</h1>

	<h2>Objectifs du module</h2>
	<p>À l'issue du module, l'étudiant doit être capable de :</p>
	<ul>
	<li>Réaliser un dessin technique</li>
	<li>Déterminer les matières nécessaires à la conception d'un produit</li>
	<li>Analyser la conception du produit jusqu'au processus de recyclage et revalorisation des matières</li>
	<li>Analyser l'impact environnemental et sociétal de la conception d'un produit</li>
	</ul>
	<h2>Compétences visées</h2>
	<p>Intégrer la gestion de production dans la vie d'un produit (de la conception au recyclage)</p>

	<p class="h2-like">Prérequis</p>
	<p>Aucun</p>

	<h2>Contenus</h2>
	<ul>
	  <li>Réalisation et analyse de dessins techniques</li>
	  <li>Détermination des besoins matières</li>
	  <li>Analyse des chutes et des articles de remplacement</li>
	  <li>Réalisation de la nomenclature du bureau d'études</li>
	  <li>Analyse Fonctionnelle du Besoin (AFB) et Analyse Fonctionnelle Technique (AFT)</li>
	  <li>Analyse du cycle de vie du produit</li>
	  <li>Impact de la conception sur le processus de recyclage</li>
	  <li>Principe du recyclage à l'infini (C2C : Cradle to Cradle)</li>
	  <li>Positionnement du produit dans la société et son environnement</li>
	</ul>



