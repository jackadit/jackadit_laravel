    <h2>Écoconception</h2>

	<p>Il n'existe pas d'outil universel de l'éco-conception, mais il existe de nombreuses méthodes et autant d'outils.</p>
	
        <h3>Objectif de l'écoconception</h3>
	<p>L'<strong>écoconception</strong> a pour objectif de réduire les impacts environnementaux des produits tout au long de leur cycle de vie : extraction des matières premières, production, distribution, utilisation et fin de vie.</p>

    <div class="notice remarque">
        <p>Environ 80% des coûts engendrés par un produit sur l'ensemble de son cycle de vie sont inhérents aux choix faits lors de la phase de conception et idem pour les impacts environnementaux.</p>
        <p>La phase de conception représentant moins de 10% des coûts totaux de développement des produits, il est pertinent d'intégrer l'environnement dès la phase de conception d'un produit. </p>
        <p>Ainsi, une bonne maîtrise des impacts dès la conception peut réduire sensiblement les coûts sans augmenter le "coût global du produit".</p>
    </div>


	<p>L'<strong>écoconception</strong> se caractérise par une vision globale de ces impacts environnementaux basée sur une approche préventive des problèmes d'environnement.</p>
	<p>Elle est centrée sur le produit et se caractérise par une démarche :</p>
	<ol>
		<li>Multi-étapes (prenant en compte les diverses étapes du cycle de vie).</li>
		<li>Multi-critères (prenant en compte les consommations de matière et d'énergie, les rejets dans les milieux naturels, les effets sur le climat et la biodiversité).</li>
		<li>Multi-acteurs (État, organismes non gouvernementaux, région, collectivité locale, entreprise, salarié, consommateurs,...).</li>
	</ol>

	<p>Le produit doit être considéré comme un système « multi-composants ». Pour un objet, on considère l'objet lui-même, les différents emballages utilisés, les consommables nécessaires à son fonctionnement, les pièces de rechange, et les éléments de promotion et de communication (courriers, publicité, etc.).</p>

	<p>L'<strong>écoconception</strong> est une démarche qui peut être appliquée dans tous les secteurs de l'économie. Elle est aujourd'hui mise en oeuvre à des degrés divers et avec une grande variété d'outils dans de très nombreux secteurs comme l'électronique, l'automobile, l'aéronautique, les produits d'équipement (électro-ménager, ameublement, éclairage, décoration…) et la plupart des produits de grande consommation (alimentaire, produits d'entretien, hygiène, beauté, etc).</p>

    <div class="notice remarque">
        <p>Dans le cas d'une boucle ouverte, le cycle de vie est appelé "du berceau au tombeau" (craddle to grave).</p>
        <p>Dans le cas d'une boucle fermée formant un cercle vertueux, on parle de démarche craddle to craddle.</p>
    </div>


    <h3>Qu'est-ce qu'un produit écoconçu ?</h3>

    <p>Quelles sont les conditions d'utilisation du terme « produit écoconçu » ?</p>
	<p>L'écoconception étant une démarche d'amélioration continue, le terme "écoconçu" ne permet pas de savoir quel est le niveau d'excellence environnementale atteint par un produit, contrairement à un produit portant l'écolabel européen.</p>
	<p>Les conditions d'utilisation du terme « écoconçu » ont été définies dans le « <a class="external" href="http://www.economie.gouv.fr/files/guide_allegations_31janv.pdf" target="_blank" title="Le guide pratique des allégations environnementales à l'usage des professionnels et des consommateurs (nouvelle fenêtre)">Guide pratique des allégations environnementales à l'usage des professionnels et des consommateurs</a> » publié par le Conseil national de la consommation (CNC).</p>
	<p>Quand elle utilise le terme « produit écoconçu », l'entreprise doit être en mesure de « fournir des éléments pertinents, significatifs, vérifiables et concrets démontrant qu'elle a mis en place une démarche d'écoconception ».</p>
	<p>Les informations que l'on doit trouver sur le produit où est apposée cette mention sont :</p>
	
	<ul>
		<li>Une définition de l'écoconception (issue de la directive n° 2009/125 ou de la norme ISO 14 062).</li>
		<li>Des précisions sur ce qui est écoconçu : le produit, l'emballage ou un composant. À défaut, ce terme vise le produit tel qu'il est commercialisé, c'est-à-dire emballage compris.</li>
		<li>Les principales caractéristiques environnementales du produit et/ou de son emballage.</li>
		<li>La nature et si possible l'ampleur des réductions d'impacts environnementaux résultant de la démarche d'écoconception (ces informations peuvent figurer sur un autre support approprié, comme un site Internet).</li>
	</ul>

    <h3>Développement durable</h3>
	<p>L'<strong>écoconception</strong> s'inscrit ainsi dans une démarche globale de <strong>développement durable</strong>.</p>
	<p>Le <strong>développement durable</strong> est une traduction française, en partie erronée, de "sustainable development" (développement soutenable). La définition la plus couramment admise est celle apparaissant dans le <a class="external" href="https://fr.wikipedia.org/wiki/Rapport_Brundtland" target="_blank" title="rapport Brundtland de 1987 : Notre avenir à tous">rapport Brundtland en 1987</a> :</p>
    <div class="notice info">
        <p>Le développement durable est un développement qui répond aux besoins du présent sans compromettre la capacité des générations futures à répondre à leurs propres besoins.</p>
    </div>
	<p>Cette publication de  la Commission mondiale sur l'environnement et le développement de l'Organisation des Nations unies servira de base au sommet de Rio en 1992.</p>
	<p>Le schéma ci-dessous illustre le concept de développement durable.</p>
	<img src="/assets/img/qlio/ecoconception/developpement_durable.png" alt="schéma de développement durable" />
	
	<p>Le développement durable est moins une définition qu'une déclaration d'intention. Derrière ce concept abstrait se trouvent trois notions fondamentales :</p>
	<ul>
		<li>La notion de développement au sens économique du terme.</li>
		<li>La notion de besoin, ce qui sous-entend la satisfaction des besoins des plus démunis, à qui il faudra donc porter une attention particulière.</li>
		<li>La notion de finitude des ressources, qui impose la prise en compte de l'environnement pour que la planète puisse répondre aux besoins du présent et du futur.</li>
	</ul>
	<p>Le développement durable est donc une conception de l'intérêt public appliquée à trois critères fondamentaux :</p>
	<ol>
		<li><strong>La croissance économique</strong>.</li>
	</ol>
	<p>Il s'agit de développer la croissance et l'efficacité économiques pour favoriser la création de richesses pour tous à travers des modes de production et de consommation durables. Le pilier économique repose notamment sur :</p>
	<ul>
		<li>L'utilisation raisonnée des ressources et des milieux naturels.</li>
		<li>Une évolution des relations économiques internationales (ex : place du commerce équitable et du commerce éthique…).</li>
		<li>L'intégration des coûts environnementaux et sociaux dans les prix des biens et des services.</li>
	</ul>
 	<ol start="2">
		<li><strong>l'environnement global</strong> (c'est-à-dire considéré à l'échelle planétaire).</li>
	</ol>
	<p>Il s'agit de préserver, améliorer et valoriser l'environnement et les ressources naturelles sur le long terme. Le pilier environnemental repose notamment sur : </p>
	<ul>
		<li>La gestion durable des ressources naturelles.</li>
		<li>Le maintien des grands équilibres écologiques (climat, diversité biologique, océans, forêts…).</li>
		<li>La réduction des risques et la prévention des impacts environnementaux.</li>
	</ul>
	<ol start="3">
	<li><strong>les enjeux sociaux (sociétaux)</strong> d'une planète globalisée.</li>
	</ol>
	<p>Il s'agit de satisfaire les besoins humains (en matière de santé, logement, consommation, éducation…) et répondre à un objectif d'équité sociale. Le pilier social repose notamment sur : </p>
	<ul>
		<li>La satisfaction des besoins essentiels des populations.</li>
		<li>La lutte contre l'exclusion et la pauvreté.</li>
		<li>La réduction des inégalités.</li>
		<li>Le respect des cultures.</li>
	</ul>

	<p class="notice remarque">En anglais les trois piliers du développement durable sont couramment appelés les trois P : Profit, People, Planet.</p>


	<p>Pour aller plus loin : <a class="external" href="http://www.e-sige.ensmp.fr//cours/qlio/presentation&id=7&url=2&demo=1&type=db&parentid=0" target="_blank" title="Introduction à la formation et au concept de développement durable">Formation au concept de développement durable</a></p>
	
	<h4>Le développement durable comme outil de management</h4>
	<p>Les enjeux du développement durable sont déclinés en objectifs stratégiques et opérationnels, tant au niveau des états par exemple, qu'au niveau des entreprises. Ainsi, de projet politique le développement durable est peu à peu devenu un projet managérial, souvent mis en œuvre avec par le déploiement de normes volontaires telles que :
	<ul>
		<li>ISO 9001 pour introduire le management de la qualité et l'amélioration continue.</li>
		<li>ISO 14001 traitant du management environnemental.</li>
		<li>ISO 26000 en ce qui concerne la responsabilité sociétale des entreprises (non certifiable).</li>
	</ul>

	<h4>Quelques dates à retenir ...</h4>
	<p>L'« aventure » du développement durable commence réellement en 1972 avec la création du club de Rome, la naissance du concept d'éco-développement et la tenue du premier sommet de la terre. Un autre point fort est évidemment la publication du rapport Brundtland sur le développement durable. Le sommet de Rio est un événement majeur qui est la source de nombreux documents de base en matière de développement durable. Est venue ensuite la signature du protocole de Kyoto sur les changements climatiques, et beaucoup plus tard sa ratification. L'après Kyoto est en train de s'écrire, avec un point fort au sommet « Rio+20 »</p>
	<ul>
		<li><span class="tag--primary">Le Club de Rome</span> (1972) : alarmiste il prône la croissance zéro.</li>
		<li><span class="tag--primary">Conférence de Stockholm</span> (1972) : Conférence des Nations Unies pour l'Environnement Humain - mise en place des <abbr title="Programme des Nations Unies pour l'Environnement">PNUE</abbr> et <abbr title="Programme des Nations Unies pour le Développement">PNUD</abbr>.</li>
		<li><span class="tag--primary">Sommet de Nairobi</span> (1982) : Échec en pleine guerre froide.</li>
		<li><span class="tag--primary">Rapport Brundtland</span> (1987) : Le rapport Brundtland pose la définition « officielle » du développement durable. Il a servi de base de discussion dans les négociations internationales qui ont précédé et prolongé le <span class="tag--primary">sommet de Rio</span>.</li>
		<li><span class="tag--primary">Sommet de Rio</span> (1992) : L'Agenda 21 est la référence des actions à mener pour un développement durable.</li>
		<li><span class="tag--primary">Le protocole de Kyoto</span> (1997) : objectifs de réduction ou de limitation des émissions des gaz à effet de serre, ratifié en 2005.</li>
		<li><span class="tag--primary">Sommet de Johannesburg</span> (2002) : Sommet mondial sur le développement durable, bilans et complément à Rio.</li>
		<li><span class="tag--primary">Sommet de Durban</span> (2011) : Le Canada a profité du sommet de Durban pour sortir du protocole de Kyoto !</li>
		<li><span class="tag--primary">Sommet Rio + 20</span> : Elle a abouti à plus de 700 engagements volontaires.</li>
	</ul>

