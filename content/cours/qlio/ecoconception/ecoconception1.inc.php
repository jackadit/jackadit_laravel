    <h3>Enjeux pour l'entreprise</h3>

	<p>L'éco-conception a pour objectif principal de réduire les impacts qu'un produit génère à toutes les étapes de son cycle de vie.

	<p>Mais cette réduction d'impacts ne doit pas se faire aux dépends de la qualité d'usage (besoins et fonctions) du produit considéré, d'où la nécessité de la connaître, de la préciser, et de la redéfinir le cas échéant.</p>
	<p>Cette démarche est innovante et efficace dès lors qu'il s'agit de concevoir ou de reconcevoir des produits dans une démarche d'éco-conception.</p>
	
	<p>Pour une entreprise il s'agit donc de :</p>

	<ul>
		<li>Connaître les flux de matières et d'énergie (ce qui permet au demeurant de les optimiser).</li>
		<li>Innover (exemple : économie de la fonctionnalité).</li>
		<li>Diminuer les risques, anticiper les conflits internes et externes, identifier les situations de crise (par exemple à cause du prix des matières premières ou de l'énergie).</li>
		<li>Se démarquer de la concurrence.</li>
		<li>Respecter et anticiper la réglementation (ce qui permet d'ailleurs d'en limiter les impacts financiers).</li>
		<li>Réduire les coûts de production (par exemple en limitant la quantité de matières premières, ou en diminuant l'énergie nécessaire à un procédé).</li>
		<li>Anticiper et répondre aux attentes du marché (en particulier en anticipant les demandes des clients et/ou en développant son activité dans des "niches").</li>
		<li>Donner davantage confiance aux consommateurs et/ou aux citoyens.</li>
		<li>Assoir sa réputation, sa légitimité.</li>
		<li>Etc.</li>
   	</ul>

    <div class="notice remarque">
        <p>L'économie de la fonctionnalité est un concept du développement durable et de l'économie consistant à vendre un service ou l'usage d'un bien plutôt que le bien lui même.</p>
    </div>

