    <h3>Environnement</h3>

	<p>La prise en compte de l'environnement s'est d'abord limitée à des mesures curatives. Typiquement, chaque fois qu'un impact environnemental était constaté (et pour peu que la réglementation y poussait), on mettait en place un système destiné à le traiter.</p>
	
	<p class="notice exemple">Exemple : Les eaux polluées par les rejets d'une entreprise ou d'une collectivité sont traitées dans une station d'épuration.</p>

	<p>Progressivement, l'accent a été mis sur des mesures préventives, de sorte qu'indépendamment du cadre législatif ou réglementaire, des solutions technologiques et des mesures organisationnelles sont mises en place dès qu'un impact environnemental est constaté.</p>
	
	<p class="notice exemple">Exemple : Pour réduire les pluies acides, on doit réduire les émissions de dioxyde soufre et donc installer des filtres adéquats, et on mesure ses émissions (mise en place d'indicateurs).</p>

	<p>Fortes de cette expérience, les "entreprises" se sont ensuite aperçues que l'environnement était une composante à part entière de leur développement stratégique, dès lors qu'elles s'engageaient dans des démarches volontaires de management environnemental.<p>

	<p>Évidemment, les entreprises qui ont mis en place un <abbr title="système de management environnemental">SME</abbr> se sont intéressées prioritairement aux impacts générés par leurs activités. On parle d'approche organisme ou d'<strong>approche site</strong>, un site fabriquant généralement plusieurs produits.</p>

	<p>L'<strong>approche produit</strong>, qui consiste à intégrer l'ensemble du <strong>cycle de vie</strong> d'un produit dans la réflexion est en fait une approche complémentaire, de sorte que les deux approches sont en fait deux visions d'une politique de développement durable.</p>
	<img src="/assets/img/qlio/ecoconception/cycle_de_vie.png" alt="schéma du cycle de vie" />
	
