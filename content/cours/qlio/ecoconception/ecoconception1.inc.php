<h1>Écoconception</h1>

	<h2>Processus de conception</h2>
	<p>L'<strong>écoconception</strong> est une démarche de développement d'un produit (bien ou service) qui consiste à intégrer la protection de l'environnement dès la phase de conception, tout en conservant ses fonctionnalités et ses performances, afin d'en diminuer les impacts tout au long de son cycle de vie (de l'extraction des matières premières jusqu'à la fin de vie)</p>
	<p>Elle ajoute ainsi la dimension environnementale aux côtés des critères fondamentaux que sont :</p>
	<ul>
		<li>Les attentes du marché.</li>
		<li>La faisabilité technique.</li>
		<li>La maîtrise des coûts.</li>
	</ul>
	<img src="/assets/img/qlio/ecoconception/ecoconception.png" alt="schéma de l'écoconception" />
	<div class="notice remarque">
        <p>Les concepts de l'écoconception ont été posés au niveau international, en 2002, avec la parution de la norme ISO/TR 14062.</p>
        <p>Celle-ci précise que l'écoconception vise à « intégrer des aspects environnementaux dans la conception et le développement de produits ».</p>
    </div>

	<p>Dès 1998, la France, notamment par l'intermédiaire de l'ADEME (Agence de l'environnement et de la maîtrise de l'énergie), a contribuer à la rédaction de cette norme.</p>
	


	<p>Plus de détails dans le guide de l'écoconception des produits et services : <a class="external" href="http://ecoconception.oree.org" target="_blank" title="Guide de l'écoconception des produits et services">http://ecoconception.oree.org</a></p>
	
    <h3>Attente du client</h3>

	<p>Le schéma suivant explique l'évolution de l'acte d'achat d'un produit depuis l'avênement de la production de masse.</p>
	<p>Le client achète un produit pour :</p>
	<ul>
		<li>Répondre à un besoin.</li>
		<li>Satisfaire une envie.</li>
	</ul>

	<img src="/assets/img/qlio/ecoconception/attente_client_historique.png" alt="historique de l'attente des clients" />
	
	<p>Toute conception démarre à partir de l'expression d'un BESOIN.</p>
	<p>Pour ce faire, il faut imaginer le produit dans son domaine d'utilisation en interaction avec le client. On compare les fonctions attendues par le client avec ceux réalisées par le produit fini.</p>
	<p>L'écart entre ces deux aspects est source de non conformité (ou défaillance)</p>
	
	<img src="/assets/img/qlio/ecoconception/attente_client.png" alt="schéma de défaillance non conformité" />
	


    <h3>Faisabilité technique</h3>
	<p>Dans son approche d'analyse systémique, l'écoconception doit tenir compte des moyens techniques mis en oeuvre pour aboutir au produit final. Ceci permettra notamment d'avoir une vision sur la capacité propre à produire le produit souhaité. Si cela n'est pas le cas, il faudra soit investir, soit sous-traiter.</p>
	<img src="/assets/img/qlio/ecoconception/faisabilite_technique.png" alt="schéma de faisabilité technique" />

