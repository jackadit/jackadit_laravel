    <h3>Principe</h3>
	<p>Il semble cependant que tous les outils doivent aborder quatre principes fondamentaux :</p>

    <ul>
    <li><strong>L'objectif de la démarche doit être défini.</strong>

    	<p>En particulier, deux approches sont possibles :</p>
		<ul>
        <li>L'éco-conception d'un nouveau produit (démarche de rupture).</li>
        <li>L'amélioration d'un produit existant.</li>
        </ul>
	</li>
	<li><strong>L'ensemble du cycle de vie doit être considéré.</strong>

    	<p>En particulier, deux impératifs apparaissent :</p>
		<ul>
        <li>Il faut déterminer et évaluer le service rendu par le produit (mise en place d'une analyse fonctionnelle et définition d'une unité fonctionnelle).</li>
		<li>Il convient d'identifier les étapes du cycle de vie du produit et de ses composants.</li>
		</ul>
	</li>
	<li><strong>Les sources d'impacts environnementaux doivent être identifiées.</strong>

    	<p>Cela revient à s'intéresser aux points suivants :</p>
    	<ul>
		<li>Les flux entrants et sortants doivent être connus et estimés/évalués.</li>
		<li>Les impacts de ces flux doivent être calculés.</li>
		</ul>
	</li>
	<li><strong>Des pistes d'amélioration doivent être proposées.</strong>

    	<p>Dès lors qu'on a calculé les principaux impacts, il convient de s'approprier les questions ci-après :</p>
    	<ul>
        <li>Les paramètres influant le plus sur les impacts doivent être repérés.</li>
        <li>Une action sur ces paramètres doit permettre de minimiser les impacts.</li>
		</ul>
	</li>
	</ul>
	

    <h2>Approche produit et norme ISO 14062</h2>
	<p>Basée sur outil dérivé de la roue de stratégie, plus ou moins complexe et détaillée, elle permet de mettre en place certains principes élémentaires en matière d'éco-conception :</p>
	<ul>
		<li>Choix de matériaux à moindre impact.</li>
		<li>Réduction des quantités de matières.</li>
		<li>Optimisation des techniques de production.</li>
		<li>Réduction et optimisation des emballages.</li>
		<li>Choix de logistique.</li>
		<li>Réduction d'impacts en phase d'utilisation du produit.</li>
		<li>Augmentation de la durée de vie du produit.</li>
		<li>Optimisation de la fin de vie.</p></li>
	</ul>

    <h3>Méthodologie</h3>
	<p>L'écoconception n'est pas une nouvelle méthode de conception, mais l'intégration de l'environnement dans les méthodes classiques de conception.</p>

	<p>L'ISO 14062 est le standard normatif de l'éco-conception : cette norme décrit les concepts et les pratiques ayant trait à l'intégration des aspects environnementaux dans la conception et le développement de produits ou services. Cette norme est principalement destinée aux concepteurs et développeurs de produits. Elle énonce les principes de base pour prendre en compte l'environnement lors de la phase de conception de produits.</p>

	<p>L'idée générale, reprise dans cette norme est de mettre en place un processus basé sur :</p>
	<ul>
		<li>L'amélioration continue.</li>
		<li>Le retour d'expérience.</li>
	</ul>

	<p>La méthode repose sur plusieurs étapes.</p>
	<p class="center"><img src="/assets/img/qlio/ecoconception/methode_ecoconception.png" alt="Méthode de l'écoconception" /></p>

	<h4>Planification</h4>
	<ul>
		<li>On relève et on hiérarchise les faits, selon leurs avantages, leur faisabilité, la stratégie de l'organisme.</li>
		<li>On envisage les aspects environnementaux.</li>
		<li>On réfléchit "cycle de vie".</li>
		<li>On formule des exigences environnementales.</li>
		<li>On analyse les facteurs externes.</li>
		<li>On choisit les approches de conception environnementale appropriées.</li>
		<li>On vérifie l'approche choisie en fonction des enjeux fondamentaux.</li>
		<li>On effectue l'analyse environnementale d'un produit de référence.</li>
	</ul>
	<h4>Conception préliminaire</h4>
	<ul>
		<li>On effectue des séances de brainstorming et de créativité destinées à trouver de nouveaux concepts.</li>
		<li>On effectue des analyses orientées sur l'ensemble du cycle de vie.</li>
		<li>On met en place des objectifs précis en termes d'environnement.</li>
		<li>On respecte les exigences environnementales.</li>
		<li>On intègre tous ces éléments dans le cahier des charges préliminaire (spécifications).</li>
	</ul>
	<h4>Conception détaillée</h4>
	<ul>
		<li>On applique les approches de conception définies préalablement.</li>
		<li>On finalise les spécifications en incluant les considérations sur le cycle de vie.</li>
	</ul>
	<h4>Essais, prototype</h4>
	<ul>
		<li>On valide les considérations prises.</li>
		<li>On examine les conséquences des décisions prises sur le cycle de vie du produit.</li>
	</ul>
	<h4>Production, lancement sur le marché</h4>
	<ul>
    	<li>On communique sur les aspects environnementaux, l'utilisation recommandée et la fin de vie du produit. </li>
	</ul>
	<h4>Revue du produit</h4>
	<ul>
		<li>On effectue un retour d'expérience intégrant les aspects environnementaux et les impacts.</li>
		<li>On "reboucle" vers l'étape de planification pour une re-conception éventuelle du produit.</li>
	</ul>

