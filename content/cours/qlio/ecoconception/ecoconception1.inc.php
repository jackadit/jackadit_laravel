    <h3>Le cadre juridique de l'éco-conception</h3>
	<p>Le champ d'application de l'éco-conception est régi par:</p>

	<ul>
		<li><strong>3 directives européennes</strong>.
			<ul>
				<li>La directive 2005/32/CE du 6 juillet 2005, qui établit un cadre pour fixer des exigences en matière d'éco-conception.</li>
				<li>La directive 2002/95/CE du 27 janvier 2003 dite ROHS (Restriction of Hazardous Substances), qui interdit a partir du 1er Juillet 2006 l'utilisation de certaines substances dangereuses.</li>
				<li>La directive 2002/96/CE du 27 Janvier 2003 dite WEEE (Waste of Electric and Electronic Equipments), qui impose la collecte, le traitement et la valorisation de tous les déchets.</li>
			</ul>
		</li>
		<li><strong>Les normes ISO dans le cadre de l'approche « site »</strong>.
			<ul>
				<li>ISO 14004 et 14061 qui établit les grandes lignes de la stratégie de management environnemental.</li>
				<li>ISO 14001 qui approfondit la stratégie de management environnemental.</li>
				<li>Série ISO 14010, qui traite de l'audit environnemental.</li>
				<li>Série ISO 14030, qui permet d'évaluer les performances environnementales d'une entreprise.</li>
			</ul>
		</li>
		<li><strong>les normes ISO dans le cadre de l'approche « produit »</strong>.
			<ul>
				<li>La norme XP ISO/TR 14062  qui indique la prise en compte de l'environnement dans la conception du produit.</li>
				<li>La série ISO 14040 qui traite de l'analyse du cycle de vie du produit.</li>
				<li>La série ISO 14020 qui traite de l'étiquetage environnemental.</li>
			</ul>
		</li>
	</ul>

