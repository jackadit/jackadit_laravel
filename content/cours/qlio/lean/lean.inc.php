<h1>Lean Manufacturing</h1>

<h2>Avantages et inconvénients de l’amélioration continue</h2>
<p>La démarche Lean est composée de plusieurs méthodes applicable dans différents domaines et/ou services.</p>
<p>Appliquer une méthode Lean dans le seul but d’accroître les performances industrielles et les bénéfices n’est pas l’objectif.</p>
<p>Accélérer les cadences sans se préoccuper de ses collaborateurs et de leur bien-être n’est pas un principe du Lean Manufacturing ni du Lean Management.</p>
<p>On risque, au contraire, de démotiver les collaborateurs et d'engendrer des arrêts de travail à cause de rythme trop soutenus.</p>
<p class="notice remarque">L’état d’esprit du Lean est à l’inverse, mettre le bien-être des employés en avant pour améliorer ensuite la productivité.</p>
<p>On misera plutôt sur la réduction des déplacements inutiles, sur le gain de place, sur l'augmentation de la disponibilité des matières premières,  etc.</p>
<p>En ne respectant pas ces principes tenant du bon sens, certaines usines s’écartent des fondements de l’amélioration continue.</p>




