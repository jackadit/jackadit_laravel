<h2>Descriptif</h2>

<h3>Objectifs généraux</h3>
<ul>
    <li>Savoir animer une réunion (présentation des participants, modérateur, clôture).</li>
    <li>Savoir rédiger et présenter des supports de présentation lors de réunions professionnelles.</li>
</ul>
<h3>Objectifs professionnels :</h3>
<ul>
    <li>Analyser et/ou améliorer un processus / une procédure / un procédé (amélioration continue).</li>
    <li>Préparer et conduire un audit.</li>
    <li>Animer une réunion.</li>
    <li>Développer un point de vue sur un sujet d’actualité ou sur une stratégie d’entreprise.</li>
</ul>

<h3>Objectifs linguistiques :</h3>
<ul>
    <li>Être capable d’expliquer clairement à un novice un outil logistique ou qualité.</li>
    <li>Élargir et utiliser en situation le champ lexical de la logistique et de la qualité.</li>
    <li>Utiliser le vocabulaire lié à l’animation d’une réunion : argumenter, gérer les interruptions, défendre un projet, convaincre
les participants.</li>
</ul>

<h3>Exemples de contenu :</h3>
<ul>
<li>Expliquer l’évaluation des risques (systèmes de management).</li>
<li>Présenter des outils du management de la qualité, de l’amélioration continue en anglais : Plan Do Check Act (PDCA),
analyse Strengths Weaknesses Opportunities Threats (SWOT), DMAICS, Roue de Deming, etc.</li>
<li>Analyser le cycle de vie d’un produit (environnement).</li>
<li>Préparer un questionnaire d’audit, conduire un audit et faire un compte-rendu avec des recommandations.</li>
<li>Préparer en anglais certains livrables : la soutenance, partie de document, synthèse.</li>
<li>Concevoir une formation (à l’écrit ou à l’oral) pour expliquer un outil logistique ou qualité.</li>
<li>Établir des objectifs du semestre ; définir sa stratégie de progrès ; s’évaluer (support visuels ex. diagramme d’araignée, courte présentation au groupe de TP, etc.).</li>

</ul>

