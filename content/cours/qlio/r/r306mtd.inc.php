<h2>Descriptif</h2>

<p>1] Définir son profil, en partant de ses appétences, de ses envies et asseoir son choix professionnel notamment au travers de
        son parcours.</p>
<ul>
    <li>Connaissance de soi tout au long de la sa formation.</li>
    <li>Modalités d’admissions (école et entreprise).</li>
    <li>Initiation à la veille informationnelle sur un secteur d’activité, une entreprise, les innovations, les technologies...</li>
    <li>Quels sont les différents métiers possibles avec les parcours proposés.</li>
</ul>

<p>[2] Construire un/des projet(s) professionnel(s) en définissant une stratégie personnelle pour le/les réaliser.</p>

<ul>
    <li>Identifier les métiers associés au(x) projet(s) professionnel(s).</li>
    <li>Construire son parcours de formation en adéquation avec son/ses projet(s) professionnel(s) (spécialité et modalité en
alternance ou initiale, réorientation, internationale, poursuite d’études, insertion professionnelle).</li>
    <li>Découvrir la pluralité des parcours pour accéder à un métier : Poursuite d’études et passerelles en B.U.T.2 et B.U.T.3
(tant au national qu’à l’international), VAE, formation tout au long de la vie, entrepreneuriat.</li>
</ul>

<p>[3] Analyser les métiers envisagés : postes, types d’organisation, secteur, environnement professionnel.</p>
<ul>
    <li>Les secteurs professionnels.</li>
    <li>Les métiers représentatifs du secteur.</li>
    <li>Quels sont les métiers possibles avec le parcours choisi.</li>
</ul>

<p>[4] Mettre en place une démarche de recherche de stage et d’alternance et les outils associés.</p>

<ul>
    <li>Formaliser les acquis personnels et professionnels de l’expérience du stage (connaissance de soi, choix de domaine et
de métier/découverte du monde l’entreprise, réadaptation des stratégies de travail dans la perspective de la 3e année).</li>
    <li>Accompagnement à la recherche de stage, alternance et job étudiant (en lien avec formation).</li>
    <li>Développer une posture professionnelle adaptée</li>
    <li>Technique de recherche de stage ou d’alternance : rechercher une offre, l’analyser, élaborer un CV & LM adaptés. Se
préparer à l’entretien. Développer une méthodologie de suivi de ses démarches.</li>
    <li>Gérer son identité numérique et e-réputation.</li>
    <li>Construire et argumenter son projet de métiers.</li>
    <li>Affiner, maturer et (re)formaliser son projet professionnel.</li>
    <li>Comprendre les attentes du marché du travail en termes de compétences.</li>
    <li>Transformer son vécu professionnel en expérience capitalisable (stage, alternance, job, associatif...).</li>
    <li>Retour d’expérience en entreprise individuel et collectif pour découvrir le champ des possibles.</li>
    <li>Actualiser ses outils d’insertion professionnelle (CV, lettre de motivation, profil de réseau social professionnel, portfolio,
etc.).</li>
    <li>Définir sa stratégie personnelle permettant la réalisation du projet professionnel.</li>
    <li>Travailler sa posture professionnelle.</li>
    <li>Prendre conscience de son évolution et de sa montée en compétences (ROME 4.0, soft-skills, etc.) en s’appuyant sur
son portfolio.</li>
    <li>Préparer le diplômé à savoir évoluer pour les métiers de demain et à s’inscrire dans la formation tout au long de la vie
(VAE, 3ème voie, etc.).</li>
</ul>

<p>Différentes modalités peuvent être mises en oeuvre :</p>

<ul>
    <li>Travail de sa stratégie professionnelle qui relie points forts, appétences, attentes du monde professionnel... (exemples :
IKIGAI*, blason**, pitch***, etc.).</li>
    <li>Participation à des visites d’entreprises, speed-meeting pro, conférences métiers, table-ronde, et autres rencontres...</li>
    <li>Décryptage des fiches métiers (ROME...) ou des offres d’emploi et positionnement personnel.</li>
    <li>Interview de professionnels (dont alumni, etc.) avec retours sur leurs parcours</li>
    <li>Simulation d’entretien de candidature pour analyser la posture professionnelle, questionnement lors de retour de situation
d’immersion professionnelle.</li>
    <li>Construction de leur mapping réseau proche et étendu (qui, pourquoi, quand et comment les solliciter pour mon projet ?).</li>
    <li>Création et/ou audit de son identité numérique professionnelle.</li>
</ul>

<p>*IKIGAI = concept japonais utilisé en développement personnel et professionnel pour trouver sa raison d’être personnelle et
professionnelle à l’intersection de ce que j’aime, ce que sais faire, ce pourquoi je pourrai être employé et ce qui est attendu par
la société.</p>
<p>**blason = ma devise, ce que je suis, ce que je ne veux pas être, ce que j’attends des autres et ce que j’attends de la vie.</p>
<p>***pitch = savoir se valoriser en 120 ou 180 secondes.</p>


