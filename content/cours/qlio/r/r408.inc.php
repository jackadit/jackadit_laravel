<h2>Descriptif</h2>

<p>Cette ressource aborde la planification à moyen terme et long terme de la production en intégrant les partenaires de l’entreprise.</p>
<p>Elle inclut notamment :</p>
<ul>
    <li>Types de marché et de demande.</li>
    <li>Prévision de la demande, enjeux et liens avec la planification.</li>
    <li>Notion de plan stratégique, de choix tactique et de part des achats dans les produits.</li>
    <li>Plan Industriel et Commercial (PIC) : Notions de familles de produits, de macro-nomenclatures, de produits finis par famille, d’approvisionnements, de configuration.</li>
    <li>Calcul des charges sur les ressources critiques (ratio, macro-gammes, articles ressources, etc.) et adéquation avec leurs capacités.</li>
    <li>Programme Directeur de Production (PDP).</li>
    <li>Typologies de PDP (sur prévision, à la commande, mixte).</li>
    <li>Horizons, périodes ou mailles et zone de gestion (ferme, etc.), Disponible à vendre, consommation des prévisions.</li>
    <li>Cohérence PIC/PDP.</li>
    <li>Partenaires externes, enjeux et liens avec la planification.</li>
    <li>Mesures de performance du processus global de planification.</li>
</ul>

