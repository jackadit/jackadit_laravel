<h2>Descriptif</h2>

<p>Cette ressource permet de prendre en main les outils nécessaires au pilotage d’une équipe de production.</p>

<p>Organiser et accompagner les activités de production :</p>
<ul>
    <li>Définir les objectifs de performance QCDHSE.</li>
    <li>Construire un tableau de management visuel.</li>
    <li>Suivre et évaluer la performance QCDHSE à travers les indicateurs.</li>
    <li>Clarifier les règles de travail et responsabilités.</li>
    <li>Former, développer et suivre la polyvalence.</li>
    <li>Traiter l’erreur, adresser une critique.</li>
    <li>Prendre en compte les remontées terrain.</li>
</ul>

<p>Développer l’autonomie des équipes de production :</p>
<ul>
    <li>Comprendre l’émergence des équipes semi-autonomes.</li>
    <li>Participer à un projet de déploiement d’une équipe semi-autonome.</li>
</ul>

