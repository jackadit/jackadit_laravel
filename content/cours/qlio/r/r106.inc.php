<h2>Descriptif</h2>

<h3>[1] S’approprier la démarche PPP.</h3>
<ul>
    <li>Développer une démarche réflexive et introspective (de manière à découvrir ses valeurs, qualités, motivations, savoirs, savoir-être, savoir-faire) au travers, par exemple de son expérience et ses centres d’intérêt.</li>
    <li>Placer l’étudiant dans une démarche prospective en termes d’avenir, souhait, motivation vis-à-vis d’un projet d’études et/ou professionnel.</li>
    <li>S’initier à la démarche réflexive (savoir interroger et analyser son expérience).</li>
</ul>

<h3>[2] S’approprier la formation</h3>
<ul>
    <li>S’approprier les compétences de la formation – identifier les blocs de compétences.</li>
    <li>Référencer les compétences et les associer avec la réalité du terrain.</li>
    <li>Découvrir, analyser les parcours B.U.T. de la spécialité.</li>
    <li>Accompagner le choix des parcours (type 1 / type 2).</li>
    <li>Préparer son stage et/ou son alternance et/ou son parcours à l’international.</li>
</ul>
<h3>[3] Découvrir les métiers et connaître le territoire</h3>
<ul>
    <li>Faire le lien avec les métiers (fiches ROME – Association article 1).</li>
    <li>Débouchés en fonction du territoire,.</li>
    <li>Bassins d’entreprise, réseaux d’entreprise, implantations.</li>
    <li>Identifier les métiers en lien avec la formation, en analyser les principales caractéristiques.</li>
</ul>
<h3>[4] Se projeter dans son environnement professionnel</h3>
<ul>
    <li>Codes, usages et culture d’entreprise.</li>
    <li>Intégration des codes sociaux au niveau France, Europe pour s’ouvrir à la diversité culturelle, ouverture sur la mondialisation socio-économique.</li>
    <li>Construire son réseau professionnel : découvrir les réseaux et sensibiliser à l’identité numérique.</li>
</ul>


<h3>Le Projet Personnel et Professionnel des semestres 1 et 2 de la première année de B.U.T. permet à l’étudiant :</h3>
<ul>
    <li>D’avoir une compréhension exhaustive du référentiel de compétences de la formation et des éléments le structurant.</li>
    <li>De faire le lien entre les niveaux de compétences ciblés, les SAÉ et les ressources au programme de chaque semestre.</li>
    <li>De découvrir les métiers associés à la spécialité et les environnements professionnels correspondants.</li>
    <li>D’identifier des secteurs d’activité et des champs professionnels de la spécialité.</li>
    <li>De découvrir l’organisation et le fonctionnement d’une organisation.</li>
    <li>De se positionner sur un des parcours de la spécialité lorsque ces parcours sont proposés en seconde année.</li>
    <li>De mobiliser les techniques de recrutement dans le cadre d’une recherche de stage ou d’un contrat d’alternance.</li>
    <li>D’engager une réflexion sur la connaissance de soi..</li>
</ul>

