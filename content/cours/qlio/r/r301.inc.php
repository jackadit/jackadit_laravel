<h2>Descriptif</h2>

<h3>Objectifs généraux</h3>
<ul>
<li>Communiquer avec aisance à l’écrit et à l’oral avec différents interlocuteurs professionnels (fournisseurs, clients...), demande de devis (machine, service, logiciel...)</li>
<li>Échanger sur des sujets liés au contexte professionnel</li>
<li>Rédiger des documents professionnels.</li>
</ul>
<h3>Objectifs professionnels :</h3>
<ul>
    <li>Comprendre et analyser une offre d’emploi.</li>
    <li>Rédiger un CV et une lettre de motivation.</li>
    <li>Rédiger un courriel professionnel pour des situations courantes.</li>
    <li>Négocier une solution à un problème avec une partie intéressée de l’entreprise.</li>
    <li>Présenter un projet lors d’une réunion.</li>
</ul>

<h3>Objectifs linguistiques :</h3>
<ul>
    <li>Être capable d’interagir spontanément dans une conversation.</li>
    <li>Repérer les mots accentués dans un discours.</li>
    <li>Manier à bon escient le langage de la négociation.</li>
    <li>Utiliser le vocabulaire d’entreprise.</li>
    <li>Maîtriser les conventions écrites des courriels en anglais.</li>
</ul>

<h3>Objectifs culturels :</h3>
<ul>
    <li>Adapter son choix de langage en respectant la culture des interlocuteurs.</li>
</ul>

<h3>Objectifs méthodologiques :</h3>
<ul>
    <li>Approfondir la démarche méthodologique acquise en R1.01 et R2.01.</li>
</ul>

<h3>Exemples de contenu :</h3>
<ul>
<li>Gérer une plainte / des réclamations à l’écrit et à l’oral.</li>
<li>Adapter des documents professionnels en anglais : signalétique professionnelle, consignes générales de sécurité, livret d’accueil.</li>
<li>Travailler sur des offres d’emplois variées : logistique, qualité, achats, commercial...</li>
<li>Rédiger des courriels professionnels : expliquer des problèmes de supply chain, de retour de produits non-conformes...</li>
<li>Établir des objectifs du semestre : définir sa stratégie de progrès, s’évaluer (support visuels ex. diagramme d’araignée, courte présentation au groupe de TP, etc.).</li>
</ul>

