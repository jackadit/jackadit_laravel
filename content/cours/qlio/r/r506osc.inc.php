<h2>Descriptif</h2>

<p>[1] Connaissance de soi et posture professionnelle (en lien avec années 1&2).</p>
<ul>
    <li>Réalisation d’un portfolio de présentation de soi / professionnel.</li>
    <li>Exploiter le mémoire d’alternance.</li>
    <li>Analyse de plusieurs situations professionnelles rencontrées lors des stages et ou stage/alternance (à relier au portfolio).</li>
</ul>

<p>[2] Formaliser son plan de carrière.</p>

<ul>
    <li>Enquêtes, veille numérique.</li>
    <li>Forum des anciens, réseaux Alumni.</li>
    <li>Job dating, salons professionnels, afterwork.</li>
    <li>Simulations en position de recruteur auprès des S2 et S4.</li>
    <li>Tutorat à la recherche de stage des étudiants de S4.</li>
    <li>Alerte sur les job boards : mots clés de recherche.</li>
</ul>

<p>[3] S’approprier le processus et s’adapter aux différents types de recrutement.</p>
<ul>
    <li>Actualisation des CV/LM.</li>
    <li>Simulation d’entretien d’embauche, par exemple sous la forme d’un jeu de rôle à partir d’offres d’emploi correspondant
aux grandes orientations de la formation/ ou créer l’annonce d’une offre pour un emploi idéal avec grille d’évaluation ?</li>
    <li>Mettre les étudiants du côté du recruteur lors des simulations d’entretien.</li>
    <li>Faire et présenter son bilan de PPP.</li>
    <li>Affiner et finaliser sa stratégie personnelle permettant la réalisation du projet professionnel.</li>
    <li>Travailler sa posture professionnelle.</li>
    <li>Prendre conscience de son évolution et de sa montée en compétences (APC, ROME 4.0, soft skills) en s’appuyant sur
son portfolio.</li>
    <li>Préparer le diplômé à savoir évoluer pour les métiers de demain et à s’inscrire dans la formation tout au long de la vie.</li>
    <li>Préparer son insertion professionnelle.</li>
    <li>Travailler son employabilité et finaliser ses outils d’insertion professionnelle (CV, Lettre de motivation, Profil de réseau
social professionnel, portfolio, etc.).</li>
    <li>Valoriser sa candidature de futur diplômé en sortie de B.U.T.</li>
</ul>

<p>Différentes modalités peuvent être mises en oeuvre :</p>

<ul>
    <li>Synthèse personnelle ou collective du bilan du PPP (outils et livrables les plus pertinents, bilan introspectif du cheminement
et de l’évolution personnelle, réinvestissement dans la vie personnelle et professionnelle).</li>
    <li>Présentation de la stratégie professionnelle qui relie points forts, appétences, attentes du monde professionnel (exemples :
IKIGAI, blason, pitch, etc.).</li>
    <li>Interview de professionnels (alumni, etc.) ayant mobilisé des dispositifs de formation continue, de VAE, etc.</li>
    <li>Présentation de parcours professionnels visés à court terme et à long terme (évolution).</li>
    <li>Simulation d’entretiens d’embauche.</li>
    <li>Décryptage d’offres d’emploi et positionnement personnel.</li>
    <li>Analyse de l’identité numérique professionnelle de l’étudiant par des professionnels et/ou entre pairs.</li>
</ul>

