<h2>Descriptif</h2>

<p>Cette ressource traite des enjeux stratégiques de la transformation digitale et de l’adhésion des salariés à cette transformation.
Il s’agit d’étudier, si possible au travers d’exemples, comment se conduit une transformation digitale au niveau décisionnel.</p>

<p>Les enjeux stratégiques (par exemple, concurrentiels et économiques) de la transformation digitale :</p>
<p>Permettre la transition d’un système fermé vers un système ouvert (bénéfices et risques).</p>
<p>La donnée, un enjeu stratégique pour l’entreprise :</p>
<p>Sensibiliser à la transformation de la donnée en connaissance (par exemple, via des techniques d’IA), en mettant l’accent sur
le lien entre le système de production et les technologies numériques (IoT, etc.).</p>
<p>Le projet de transformation digitale :</p>
    <p>Objectifs (KPI), enjeux, risques, taille et typologies d’entreprise, interlocuteurs, durée, causes et conditions de réussite ou
d’échec.</p>
<p>Positionner l’Humain au centre de l’organisation numérique :</p>
<p>La transformation digitale passe par l’accompagnement des salariés pour communiquer, expliquer, valoriser.</p>
<p>Qu’est-ce qu’une compétence ? Comment gérer les compétences ?</p>
<p>Investir sur le capital humain pour développer une organisation apprenante.</p>

<h2>Préconisations pédagogiques</h2>
