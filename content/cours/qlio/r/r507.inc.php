<h2>Descriptif</h2>

<p>Cette ressource permet :</p>

<ul>
    <li>D’introduire le contexte.</li>
    <li>De comprendre l’organisation en activités de l’entreprise (données d’entrées et de sorties).</li>
    <li>De construire à partir des activités, une décomposition de l’entreprise en différents processus.</li>
    <li>D’élaborer les interactions entre les processus et la représentation qui en découle (cartographie).</li>
    <li>D’identifier et de rationaliser les informations documentées répondant aux besoins d’une entreprise, aux exigences d’un
référentiel.</li>
    <li>D’organiser les informations documentées essentielles à l’aide d’un système d’informations lié à la qualité (création,
exploitation, archivage, traçabilité).</li>
    <li>De gérer les ressources externes et internes nécessaires au fonctionnement d’un processus (infrastructure, compétences,
équipements de mesure et surveillance (GMM)).</li>
    <li>De sensibiliser les équipes à la finalité du système de management de la qualité.</li>
    <li>De mettre en place un système de management de la qualité.</li>
</ul>

<h2>Préconisations pédagogiques</h2>
<ul>
    <li>La ressource R5.11 doit intervenir en début de semestre et avant la ressource R5.12.</li>
    <li>Le système d’information est à comprendre au sens large (supports papiers, numérique).</li>
    <li>Cette ressource pourra être étendue aux domaines de la sécurité et de l’environnement.</li>
    <li>Le contexte pourra prendre en compte les relations Fournisseurs (Assurance Qualité Fournisseurs).</li>
</ul>

