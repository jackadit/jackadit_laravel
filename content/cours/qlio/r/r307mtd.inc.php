<h2>Descriptif</h2>

    <p>Cette ressource permet :</p>
    <ul>
        <li>De valider la capabilité d’un moyen de mesure : R&R (MSA).</li>
        <li>De définir un plan de contrôle réception : échantillonnage (attribut, mesure), courbe d’efficacité.</li>
        <li>De caractériser une population (normalité).</li>
        <li>De calculer et exploiter les capabilités des process.</li>
        <li>De piloter le process : Maîtrise Statistique des Procédés (MSP) mesures et/ou attributs grâce aux cartes de contrôle.</li>
        <li>D’identifier les paramètres influents d’un process/produit et de l’améliorer en utilisant les plans d’expériences (complets,
fractionnaires orthogonaux).</li>
</ul>

<h2>Préconisations pédagogiques.</h2>
<ul>
    <li>Simulateurs statistiques (planche de Galton).</li>
    <li>Utilisation d’un logiciel de statistiques ou tableur.</li>
    <li>Utilisation d’un cas pratique pour les plans d’expériences (simulateurs ou mécanismes physiques).</li>
</ul>

<h2>Apprentissages critiques ciblés :</h2>
<ul>
    <li>AC21.03 | Mesurer et améliorer la performance</li>
    <li>AC21.06 | Fiabiliser la mesure</li>
</ul>
<h2>Mots clés :</h2>
<p>MSP (Plan de contrôles de réception – capabilités – cartes de contrôle) – Plan d’expériences Métrologie (gestion des équipements
de surveillance et de mesures).</p>

