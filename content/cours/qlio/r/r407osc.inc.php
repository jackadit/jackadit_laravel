<h2>Descriptif</h2>

<p>Cette ressource permet en s’appuyant sur les méthodes décrites dans les mots clés :</p>

<ul>
    <li>D’analyser une perte de performance au niveau d’un processus global.</li>
    <li>De dérouler une démarche d’amélioration de la performance.</li>
    <li>De manager une équipe projet (résistance aux changements, gestion des conflits).</li>
</ul>

<h2>Préconisations pédagogiques</h2>
<p>Cette ressource pourra être étendue aux domaines de la sécurité et de l’environnement.</p>

<h2>Apprentissages critiques ciblés :</h2>
<ul>
    <li>AC21.01 | Effectuer une démarche d’amélioration continue.</li>
    <li>AC21.03 | Mesurer et améliorer la performance.</li>
    <li>AC21.04 | Animer des équipes sur un projet.</li>
</ul>

<h2>Mots clés :</h2>
    <p>Démarche d’amélioration par percée (DMAIC – six sigma – design for six sigma (QFD – etc.) – Lean management) – Mesure
de la performance : diagnostic de l’existant et indicateurs de mesure de l’existant – Tableaux de bord – Animer/Manager une
équipe (management visuel – gestion des conflits – manager des équipes autonomes) – Restituer/Communiquer sur un projet
vers les opérationnels et vers le management.</p>

