<h1>Les situations d’apprentissage et d’évaluation (SAÉ)</h1>

<p>Les SAÉ permettent l’évaluation en situation de la compétence. Cette évaluation est menée en correspondance avec l’ensemble
des éléments structurants le référentiel, et s’appuie sur la démarche portfolio, à savoir une démarche de réflexion et de démonstration
portée par l’étudiant lui-même. Parce qu’elle répond à une problématique que l’on retrouve en milieu professionnel,
une SAÉ est une tâche authentique.</p>
<p>En tant qu’ensemble d’actions, la SAÉ nécessite de la part de l’étudiant le choix, la mobilisation et la combinaison de ressources
pertinentes et cohérentes avec les objectifs ciblés. L’enjeu d’une SAÉ est ainsi multiple :</p>
<ul>
<li>Participer au développement de la compétence ;</li>
<li>Soutenir l’apprentissage et la maîtrise des ressources ;</li>
<li>Intégrer l’autoévaluation par l’étudiant ;</li>
<li>Permettre une individualisation des apprentissages.</li>
</ul>
<p>Au cours des différents semestres de formation, l’étudiant sera confronté à plusieurs SAÉ qui lui permettront de développer
et de mettre en oeuvre chaque niveau de compétence ciblé dans le respect des composantes essentielles du référentiel de
compétences et en cohérence avec les apprentissages critiques.</p>
<p>Les SAÉ peuvent mobiliser des heures issues des 2000 heures de formation et des 600 heures de projet. Les SAÉ prennent
la forme de dispositifs pédagogiques variés, individuels ou collectifs, organisés dans un cadre universitaire ou extérieur, tels
que des ateliers, des études, des challenges, des séminaires, des immersions au sein d’un environnement professionnel, des
stages, etc.</p>


