<h1>Optimisation d'un poste de travail</h1>

	<h2>Objectifs du module</h2>
	<p>À l'issue du module, l'étudiant doit être capable de :</p>
	<ul>
		<li>Comprendre les effets physiologiques des ambiances de travail.</li>
		<li>Mener une intervention ergonomique (effectuer l'analyse de la demande, un pré-diagnostic, un diagnostic et être acteur de la conception et / ou de la modification d'une situation de travail).</li>
		<li>Observer, décrire et analyser l'activité physique et cognitive, individuelle et collective des opérateurs à l'aide d'observables (gestes et postures, déplacements, directions de regard et communications fonctionnelles) pour caractériser les déterminants de leur activité et leurs risques pour la santé.</li>
		<li>Analyser et appliquer la réglementation, les recommandations et normes ergonomiques.</li>
		<li>Évaluer les risques au poste de travail et mettre en œuvre les moyens de prévention et de protection.</li>

	</ul>
	<h2>Compétences visées</h2>
	<ul>
		<li>Optimiser un poste de travail</li>
	</ul>
	
