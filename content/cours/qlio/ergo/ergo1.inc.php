<h1>Méthodes des temps prédéterminés</h1>

<h2> Généralité sur les temps dans l'industrie.</h2>
 
<p>Avec le développement industriel, les entreprises ont très vite compris la nécessité de définir des temps au plus juste au moins pour deux raisons essentielles :</p>
<ul>
	<li>Valorisation de la charge de travail afin de définir les besoins en effectif en fonction des quantités de pièces à produire et des délais à respecter.</li>
	<li>Valorisation du coût de production des pièces en fonction du temps passé sur chacune d’elle.</li>
</ul>
<p>Il existe à ce jour deux grandes familles pour définir ces temps :</p>
<ul>
	<li>La mesure directe.</li>
	<li>Les temps prédéterminés.</li>
</ul>

<h2>Qu'est-ce qu'une technique de temps prédéterminés ?</h2>
 
<p>Expliquons tout d’abord et de manière simple ce qu’est le chronométrage (la mesure directe) :</p>
<p>Il s’agit de mesurer une succession de tâches réalisées par un exécutant sur un poste de travail existant.</p>

<p>Les aptitudes de chaque être humain étant différentes d’un individu à l’autre, il faut savoir rectifier les valeurs chronométrées, afin qu’elles soient réalisables par le plus grand nombre.</p>

<p>Un chronométreur doit être formé afin de porter un <strong>jugement d’allure</strong> sur l’individu qu’il est en train de chronométrer. Il obtiendra ainsi un temps appelé le <strong>temps à l’allure 100</strong>.</p>
 
<p>Si le chronométreur n’a pas cette compétence, il ne lui restera alors que la possibilité de demander à un échantillon représentatif de réaliser le même travail dans les mêmes conditions afin d’en définir une moyenne.</p>

<p>Cette dernière solution est bien entendue inenvisageable, d’autant plus qu’au cours de l’histoire, d’autres personnes se sont déjà chargées de réaliser l’expérience.</p>


<h2>Historique</h2>

<p>Le chronométrage fait son apparition avec l’ère industrielle, mais très vite, on s’aperçoit de ses limites lorsqu’il s’agit de définir des temps pour l’activité humaine.</p>

<p>Le jugement d’allure n’était pas encore au goût du jour.</p>

<p>En effet, nous ne sommes pas tous égaux : Certains sont plus rapides, d’autres plus précis, d’autres ne réalisent pas les gestes de la même manière d’un cycle à l’autre…</p>

<p>Ainsi, le temps passé par chaque individu pour exécuter une tâche donnée varie.</p>

<p>Cette variation augmente avec la longueur du cycle.</p>
 
<p>On se demande alors comment faire pour assurer systématiquement les mêmes temps de cycle face une telle imperfection de l’être humain.</p>

<p>Une solution est trouvée dans les années 1900, grâce à Frédérich Winslow Taylor. Il décompose chaque mouvement des exécutants dans le but d’assurer la répétabilité des tâches. Nous voici entré dans « les temps modernes ».</p>

<p>Dès les débuts de l'organisation scientifique du travail (OST), F. W. Taylor cherche à systématiser la mesure des temps qu'il effectue au chronomètre.</p>
<p>En 1912, il détaille la méthode qu'il préconise pour l'étude des temps (comptes-rendus ASME vol. 34 pp. 1199-1200) :</p>
<ol>
	<li>Décomposer le travail en mouvements élémentaires simples.</li>
	<li>Les codifier et mesurer au chronomètre leur temps optimal.</li>
	<li>Les recombiner en groupes logiques.</li>
	<li>Déterminer la succession de ces différents groupes correspondant à une opération donnée afin d'en déterminer un temps qui soit indépendant des facteurs environnementaux..</li>
</ol>
<p>Les principes des temps prédéterminés sont ainsi posés.</p>

<p>Progressivement, certains individus se sont naturellement intéressés à la manière de modéliser l’activité humaine.</p>

<p>Les études les plus abouties sont apparues dans les années 1920, par les travaux réalisés par Frank et Lillian Gilbreth qui se sont consacrés à l'étude minutieuse des micro-mouvements.</p>
<p>Ces deux chercheurs se sont « amusés » à décomposer chaque geste élémentaire d’une population représentative d’individus afin d’en définir des temps standards.</p>
<p>Ils ont pu ainsi dresser des tables récapitulatives qu’ils ont décidé de nommer les « THERBLIG » (qui, à une lettre près, ressemble à leur nom inversé ;-) ! ).</p>
<p>Ils sont également les premiers à utiliser la caméra pour réaliser leurs analyses.</p>
<p>Deux écoles vont alors s'opposer pendant une vingtaine d'années : les disciples de Taylor privilégiant l'étude (et la réduction) des temps, ceux de Gilbreth travaillant sur l'analyse des mouvements (et leur économie).</p>
<p>D'autre part, dès ses débuts, l'étude des temps est critiquée pour son manque de rigueur et de répétabilité, dû à divers facteurs d'origine humaine (compétences du chronométreur, volontés de la personne observée, méthodes utilisées,…).</p>
<p>C'est l'association d'une étude minutieuse des mouvements, et de la détermination des temps à une allure standard, qui a permis la définition de tables de temps prédéterminés.</p>
<p>Les premiers systèmes voient le jour dans les années 30 : MTA (Motion-Time Analysis, le plus ancien), Work factor ou MTS (Motion Time Standards). Puis dans les années 40, MTM (Methods-Time Measurement) a véritablement marqué l'histoire des méthodes de temps prédéterminés, en devenant l'une des principales références, et ce encore aujourd'hui.</p>

<h2>Méthodes de temps prédéterminés</h2>

<p>Les méthodes de temps prédéterminés sont donc des systèmes de calcul de temps opératoires basés sur des tables de temps standardisés.</p>
<p>Chaque mouvement, ou série de mouvements élémentaires, est côté : la séquence complète du mode opératoire peut alors être calculée en additionnant l'ensemble des temps élémentaires.</p>

<p>Ces tables de temps sont établies pour une allure normale (100). On peut donc considérer que ces temps correspondent à l'activité d'un opérateur moyen, et évitent le recours au chronométrage et au jugement d'allure.</p>
<p>Mais cela n'est pas suffisant pour bien déterminer le temps nécessaire aux différentes opérations, il faut donc en plus appliquer des coefficients majorateurs, ceux du BTE (Bureau des temps élémentaires) par exemple, afin d'obtenir un temps alloué pour l'opération dans la gamme (notion de cadence nominale).</p>


<h3>MTA (Motion-Time Analysis)</h3>

<p>Il s'agit du plus ancien des systèmes de temps prédéterminés, établi par Asa Bertrand Segur à partir de 1919. Il est basé sur des bases de temps variables selon les possibilités de combinaison de mouvements. Développé dans les années 20, MTA était encore utilisé 50 ans plus tard, par la société de conseil de A.B. Segur, seule détentrice des droits. Les données et informations de ce système furent jalousement gardées secrètes, ce qui ne favorisa pas son développement.</p>

<h3>Work Factor</h3>

<p>Développé par Joseph H. Quick et ses associés dans les années 30 et 40, Work Factor est une marque déposée de la Work Factor Company. Encore utilisé de nos jours, ce système comporte plusieurs tables de données, selon le degré de précision souhaité : Detailed WF, Ready WF, Very easy WF, … La particularité du système est de s'appuyer sur des facteurs d'influence (poids, degré de contrôle, …), dont l'importance est mesurée en facteurs de travail (wf). La combinaison de ces facteurs permet de calculer le temps associé au mouvement.</p>

<h3>Systèmes de la General Electric</h3>

<p>Le groupe General Electric a développé plusieurs systèmes en interne, notamment Get and Place, MTS (Motion Time Standards) et DMT (Dimensional Motion Time). MTS est proche du Detailed Work Factor. DMT se base sur la dimension des pièces manipulées pour déterminer les temps. Ces différents systèmes ont été construits dans les années 40 pour les besoins spécifique de GE, et n'ont guère été utilisés en dehors.</p>

<h3>MTM (Methods-Time Measurement)</h3>

<p>Il s'agit du système de temps prédéterminés le plus utilisé, et il sert souvent de référence. Le MTM est un outil du bureau des méthodes d'aide au calcul des coûts dans une entreprise de production. Ses concepteurs majeurs furent, dans les années 40, l'ingénieur en organisation américain Harold Bright Maynard, Gustave James Stegemerten et John L. Schwab.</p>

<p>Les gestes des opérateurs de production sont décomposés en temps élémentaires dont les durées connues permettent un chiffrage à la fois du calcul des coûts de main d'œuvre et des choix des modes opératoires.</p>

<p>Toutes les données (tables et documents de recherche) ont été rendues publiques, ce qui a contribué à sa diffusion. Le système originel, aujourd'hui appelé MTM-1, est très détaillé, chaque micro-mouvement étant côté. Il a été suivi de versions plus rapides à utiliser (mais aussi potentiellement moins précises) : MTM-2 (1965), MTM-3 (1970) et MTM-UAS (1975). D'autres systèmes en sont dérivés, comme le BMT (Basic Motion Study),ou MODAPTS (Modular Arrangement of Predetermined Time Standards).</p>

<p>Partant de l'axiome que le temps est la conséquence de la méthode choisie, les seuls gains de productivités s'opèrent par des améliorations de méthodes. Chaque action dépend de plusieurs facteurs. Le temps global est la somme de toutes les actions élémentaires.</p>

<p class="notice exemple">Par exemple l'action saisir une grosse vis posée sur un établi est mesurée à 2 cmh. La même action de saisir une rondelle posée sur l'établi est mesurée à 3,5 cmh. Pour améliorer le temps, plusieurs corrections sont possibles : placer la rondelle sur un morceau de mousse molle, dans un distributeur gravitationnel…</p>


<h3>MOST (Maynard Operation Sequence Technique)</h3>

<p>Il s'agit d'un des derniers systèmes créés par le cabinet Maynard, qui l'a développé dans les années 60 et déployé dans les années 70. Plus simple que MTM, il est devenu un standard très utilisé dans l'industrie. Il se décline en trois sous-systèmes, selon le degré de précision et la durée du cycle analysé : Mini MOST, Basic MOST et Maxi MOST.</p>

<p>Les temps sont analysés sous la forme de séquences prédéfinies ; par exemple, Basic MOST utilise les séquences Mouvement libre, Mouvement guidé, Utilisation d'outil et Utilisation de palan.</p>


<h3>MTM2 et MTS</h3>
<p>Le MTM2 et le MTS (Méthode des temps standards) sont des dérivés du MTM dans lequel la découpe des mouvements est considérablement réduite. Par exemple, les actions :</p>


<p>Atteindre — Saisir — Mouvoir — Positionner — Lâcher</p>
<p>sont regroupées en :</p>

<p>Prendre — Placer</p>
<p>et les modes de calcul dépendent de plus de facteurs : le poids, la nécessité de positionner précisément la pièce avant de placer…</p>

<p>Poussé à l'extrême par le taylorisme et le fordisme, malgré l'image négative que peut avoir le chronométrage, cet outil reste une méthode simple, rapide et objective d'amélioration de la productivité.</p>

<h2>Avantages et inconvénients</h2>

<div class="notice ok">
<ul>
	<li>Ils peuvent être utilisés a priori : le temps d'un mode opératoire peut être déterminé sans devoir l'observer, ce qui est utile lors d'une phase de conception.</li>
	<li>Par leur décomposition fine, ils permettent la critique et l'optimisation du mode opératoire utilisé, en mettant en lumière les mouvements, ou les séquences fortement consommateurs de temps.</li>
	<li>L'analyse peut être menée à plusieurs (notamment en intégrant les opérationnels), favorisant l'appropriation de la démarche.</li>
	<li>Ils permettent de s'affranchir du jugement d'allure, qui est nécessaire pour des temps déterminés par chronométrage.</li>
	<li>Ils peuvent être perçus comme socialement plus facile à utiliser que le chronométrage, et faire l'objet de moins de controverses.</li>
</ul>
</div>

<div class="notice alert">
	<p>Parmi les inconvénients, on peut citer :</p>
	<ul>
	<li>La possible déconnexion du terrain, et du mode opératoire réellement utilisé, s'ils ne sont pas accompagnés d'observations directes.</li>
	<li>La nécessité d'être formé et rompu à ces techniques. La manipulation de tables de temps prédéterminés demande beaucoup d'habitude et d'entrainement. Le risque est d'obtenir des temps inexacts.</li>
	<li>Leur limitation à des temps manuels. Les temps technologiques doivent être mesurés autrement.</li>
	</ul>
</div>


<h2>CONCLUSION</h2>
 
<p>Les outils évoluent au rythme du besoin.</p>
<p>Sachons nous remettre en cause pour utiliser le bon outil au bon moment.</p>

<p>N’ayons pas d’à priori lorsqu’il s’agit de maintenir la santé de nos industries.</p>
 
<p>Mais avant de jouer avec les TMU, posons-nous toujours au moins 2 questions :</p>
<ul>
	<li>Est-ce bien l’optimisation des temps de fabrication qui est déterminante dans la réduction du coût des produits ?</li>
	<li>Est-ce bien l’optimisation des temps qui est déterminante dans la réduction des délais d’obtention ?</li>
</ul> 

<p>En effet, les temps de fabrication ne sont certainement pas les seules clés qui permettent la réduction des coûts et des délais. Penser le contraire serait n’avoir qu’une vision réduite de l’organisation industrielle…</p>



