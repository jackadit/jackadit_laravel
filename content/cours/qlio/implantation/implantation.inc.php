<h1>Étude d'implantation</h1>

	<h2>Objectifs du module</h2>
	<p>À l'issue du module, l'étudiant doit être capable de :</p>
	<ul>
	<li>Identifier et dimensionner les caractéristiques des espaces de travail. en termes de temps et d'espace</li>
	<li>Aménager des espaces de travail</li>
	</ul>
	<h2>Compétences visées</h2>
	<ul>
		<li>Identifier/caractériser les processus (entrées, sorties, acteurs, points de mesure/contrôle, indicateurs…), établir une cartographie des processus et des flux</li>
		<li>Caractériser un système de production</li>
		<li>Sélectionner les postes de travail, les implantations ou les modalités de manutention et d'entreposage. Implanter un poste de travail, un atelier. Choisir la taille et la localisation des emplacements de stockage des articles</li>
		<li>Modéliser un processus</li>
		<li>Mesurer les résultats obtenus, résoudre les problèmes</li>
	</ul>
	
	<p class="h2-like">Prérequis</p>
	<p>Caractériser un système de production.<br />
	Connaître la typologie des produits et des procédés.<br />
	Savoir décrire un système de production avec ses caractéristiques et ses contraintes.</p>

	<h2>Contenus</h2>
	<ul>
	  <li>Influence de la modification des données techniques sur le processus.</li>
	  <li>Cohérence entre les données techniques et les processus (trio gamme nomenclature processus)</li>
	  <li>Analyse d'une implantation
	  <ul>
	  <li>besoins d'espaces de circulation en fonction des moyens technologiques de manutention et de production</li>
	  <li>besoins d'espaces liés à l'activité humaine</li>
	  <li>analyse fonctionnelle des surfaces</li>
	  </ul></li>
	  <li>Analyse des flux
	  <ul>
	  <li>quantification des trafics</li>
	  <li>débits et cadences</li>
	  </ul></li>
	  <li>Techniques simples d'implantation (Chaînons, en ligne, sections homogènes, mise en îlots …)</li>
	  <li>Implantation physique et mise en plan
	  <ul>
	  <li>localisation des stocks et en-cours</li>
	  <li>calcul des surfaces</li>
	  </ul></li>
	</ul>


	<h2>Prolongements possibles</h2>
	<p>Organisation et animation d'une unité de production de biens et de services</p>
	<p>Optimisation d'une implantation</p>
	<p>Gestion physique des stocks</p>
	<p>Simulation des systèmes de production de biens et de services</p>



