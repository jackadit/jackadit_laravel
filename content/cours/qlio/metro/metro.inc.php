<h1>Introduction à la métrologie</h1>

	<h3>Objectifs du module</h3>
	<p>À l'issue du module, l'étudiant doit être capable de :</p>
	<ul>
	<li>Appliquer une fonction métrologie existante</li>
	<li>Vérifier l'aptitude et assurer le suivi des équipements de surveillance, de mesure et d'essai</li>
	</ul>
	<h3>Compétences visées</h3>
	<ul>
		<li>Choisir les moyens de mesures/contrôles adaptés aux spécifications produits/process</li>
		<li>Contrôler l'état et la conformité des moyens de production par rapport aux caractéristiques prévues (productivité, qualité…) et évaluer les taux de disponibilité, qualité et performance des ressources et identifier les dysfonctionnements</li>
		<li>Définir et mettre en place des points de contrôle</li>
		<li>Renseigner les supports de contrôle des processus et supports de constat (listes, grilles…), renseigner les fiches d'enregistrement</li>
	</ul>
	
	<p class="h2-like">Prérequis</p>
	<ul>
	<li>Introduction à la qualité</li>
	<li>Eco-conception</li>
	</ul>

	<h3>Contenus</h3>
	<ul>
	  <li>Typologie des équipements de surveillance et de mesure</li>
	  <li>Les unités de mesure</li>
	  <li>Mesure (dimensionnelle ; électrique, masse, …)</li>
	  <li>Fonction métrologie - Maîtrise de l'environnement</li>
	  <li>Aptitudes des équipements de mesure et de surveillance</li>
	  <li>Déclaration de conformité des produits</li>
	</ul>

	<h3>Prolongements possibles</h3>
	<p>Maîtrise des procédés</p>
	<p>Gestion de la métrologie</p>
	<p>Contrôle et gestion des équipements</p>



