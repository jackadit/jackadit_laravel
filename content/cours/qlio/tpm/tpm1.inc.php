<h1>Conseils pour réduire les temps d'arrêt</h1>

<h2>Généralités</h2>
<p>Les temps d'arrêt sont la principale source de perte de productivité pour la plupart des fabricants et représentent souvent l'endroit où les gains les plus rapides peuvent être réalisés.</p>

<p>Bien que la Maintenance Productive Totale (TPM) soit une pratique exemplaire pour réduire durablement les temps d'arrêt, toutes les entreprises ne sont pas prêtes à investir le temps et les ressources nécessaires pour créer un programme TPM efficace.</p>

<h3>Capturer les raisons</h3>
<p>Il est essentiel de capturer une raison et la durée de chaque incident de temps d'arrêt pour permettre à l'équipe de les hiérarchiser et se concentrer efficacement.</p>
<p>Commencez simplement, avec pas plus de 25 raisons, dont l'une devrait être "Toutes les autres pertes".</p>
<p>Assurez-vous que chaque raison est claire (par rapport aux autres raisons) et décrit les symptômes (plutôt que d'essayer de diagnostiquer les causes fondamentales).</p>
<p>Supprimez les raisons qui ne sont pas régulièrement utilisées et ajoutez-en au besoin pour vous assurer que "Toutes les autres pertes" ne fait pas partie des dix principales pertes.</p>

<h3>Se concenter sur la contrainte</h3>
<p>Chaque processus de fabrication a une contrainte, qui est le point de levier pour l'ensemble du processus.</p>
<p>Mesurez les temps d'arrêt au niveau de la contrainte et améliorez cette contrainte pour garantir que les ressources sont concentrées là où elles auront le plus d'impact sur la production et la rentabilité.</p>

<h3>Sélectionner des indicateurs pour encourager les comportements souhaités</h3>
<p>Les indicateurs mis en avant et partagés sont de puissants moteurs de comportement.</p>
<p>Traitez les temps d'arrêt comme un KPI (indicateur clé de performance) et renforcez continuellement son importance.</p>
<p>Fixez des objectifs SMART (Spécifiques, Mesurables, Atteignables, Pertinents et Temporellement définis) et récompensez le succès, même par une simple reconnaissance.</p>

<h3>Rendre les temps d'arrêt visibles</h3>
<p>Fournissez des visuels clairs pour indiquer quand la ligne est arrêtée et faites escalader ces visuels si la ligne reste arrêtée longtemps.</p>
<p>Formez les membres de l'équipe à réagir rapidement et prévoyez plusieurs niveaux de réponse d'escalade (par exemple, opérateur, superviseur, responsable).</p>
<p>L'objectif est d'empêcher que les petits problèmes ne deviennent de gros incidents.</p>

<h3>Réparer rapidement ou escalader pour une solution durable</h3>
<p>Traitez chaque perte de temps d'arrêt de deux manières : soit avec une réparation rapide, soit avec une solution durable nécessitant des ressources externes.</p>

<h3>Réaliser un blitz 3S</h3>
<p>Un environnement de travail propre et organisé crée de meilleures conditions pour un équipement en bon état de fonctionnement.</p>
<p>Un blitz 3S consiste à trier (éliminer ce qui n'est pas nécessaire), ranger (organiser ce qui reste) et nettoyer (et inspecter l'équipement).

<h3>Réaliser un blitz de maintenance</h3>
<p>Vérifiez que toutes les pièces d'usure (par exemple, joints, roulements, courroies et rouleaux) sont en bon état et remplacez celles qui sont sous-optimales.</p>

<h3>Marquer les réglages optimaux</h3>
<p>Décidez et marquez les réglages optimaux de l'équipement pour éviter que des ajustements constants ne créent des conditions propices aux temps d'arrêt.</p>

<h3>Effectuer des révisions horaires</h3>
<p>Organisez des réunions debout de trois minutes toutes les heures à côté de la zone de production pour mettre à jour les performances, identifier le plus grand incident de temps d'arrêt de la dernière heure et convenir d'une action d'amélioration pour l'heure suivante.</p>

<h3>Ne changer qu'une chose à la fois</h3>
<p>Lorsque vous résolvez un problème de temps d'arrêt, apportez un changement à la fois pour faciliter le diagnostic des problèmes et l'évaluation de l'efficacité des solutions.</p>

<h2>Bénéfices</h2>
<p>À court terme, ces conseils pratiques et éprouvés permettent de réduire les temps d'arrêt en utilisant les ressources existantes.</p>
<p>À long terme, ils devraient être remplacés par une approche structurée, telle que la TPM, pour une réduction durable des temps d'arrêt.</p>

<h2>Rôles des personnes impliqués</h2>
<ul>
	<li>Manager : Initier le programme, identifier la contrainte, sélectionner les indicateurs et décider du moment de passer à une approche plus structurée.</li>
	<li>Superviseur : Maintenir les raisons, créer des visuels, escalader les problèmes nécessitant une solution durable, marquer les réglages optimaux, diriger les blitz 3S et de maintenance, diriger les révisions horaires, contrôler les changements.</li>
	<li>Opérateur : Capturer les raisons, réagir aux visuels, mettre en œuvre des réparations rapides et des actions correctives, vérifier les réglages optimaux.</li>
</ul>

<h2>Contexte général</h2>
<p>Les temps d'arrêt sont la plus grande source de perte de productivité pour la plupart des fabricants. Ces conseils pratiques visent à réduire les temps d'arrêt sans nécessiter de soutien externe important.</p>

<h2>Gardez un oeil sur l'ensemble</h2>
<p>Il est important d'avoir une vue d'ensemble précise et complète du temps de production perdu. Commencez par mesurer l'OEE (Overall Equipment Effectiveness) et enregistrez les raisons des pertes de disponibilité de l'OEE. Ensuite, suivez toutes les Six Grandes Pertes et calculez le TEEP (Total Effective Equipment Performance) pour des insights sur la capacité.</p>

<h2>Capturez les améliorations</h2>
<p>Chaque fois qu'une amélioration est apportée, demandez-vous si une nouvelle meilleure pratique a été créée et si d'autres équipes peuvent bénéficier de cette amélioration. En cas de réponse positive, considérez la capture de l'amélioration comme un TRAVAIL STANDARDISÉ.</p>

<h2>Passer de réactif à proactif</h2>
<p>Réservez du temps pour des améliorations proactives et essayez de faire passer l'équilibre du réactif au proactif.</p>

<p>Considérer la TPM</p>
<p>Pour une approche complète et proactive de la réduction des temps d'arrêt, considérez la TPM, qui inclut un ensemble riche d'outils et de processus pour éliminer les pertes.</p>

