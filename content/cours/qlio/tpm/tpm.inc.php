<h1>TPM, Total Productive Maintenance</h1>

<h2>Historique</h2>
<p>La TPM a succédé à la Preventive Maintenance (PM) américaine. Développée au Japon après la Seconde Guerre mondiale, la TPM serait née dans les usines du groupe Nippondenso avant d’être formalisée par le JIPM, Japan Institute of Plant Management.</p>
<p>En 1989 le JIPM a défini 8 piliers sur lesquels une stratégie de démarche TPM s'appuie pour atteindre un niveau élevé de performance industrielle.</p>
<p>La TPM est une marque déposée du JIPM.</p>
<p>L'approche participative est une nouveauté par rapport à la préventive maintenance.</p>
<p>En effet, les équipes et les opérateurs connaissent leur(s) machine(s), et de ce fait contribuent efficacement à leur maintenance.</p>

<h2>But de la démarche TPM</h2>
<p>Pour une entreprise, la démarche TPM, Total Productive Maintenance, est une étape de l’amélioration des performances de son outil de production, qui doit doit être fiable et disponible.</p>

<p>Pour cela l’entreprise doit supprimer l’imprévu et le hasard dans ses activités de productions.</p>


<h2>Moyens pour y arriver.</h2>
<p>La TPM, Total Productive Maintenance permet de visualiser facilement et sous forme graphique (Pareto, courbes, etc…) les principaux indicateurs et les facteurs qui influent sur leurs résultats. Ces graphiques fournissent des rapports, établis à partir de critères TPM, délivrant ainsi des analyses personnalisées pour des projections sur des durées ou des ressources particulières.</p>

<p>Le but de l’analyse des données est de déterminer les causes principales de sous-performance et d’orienter les actions de progrès sur les causes générant les gains de TRS, Taux de Rendement Synthétique, les plus importants.</p>

<p>Les systèmes TPM ont trois objectifs majeurs :</p>
<ol>
	<li>Minimiser la sous-utilisation de l’équipement causant de nombreuses pertes, y compris financières.</li>
	<li>Participer à la conservation des équipements par un vrai suivi de la maintenance</li>
	<li>Améliorer l’organisation, la sécurité et agir sur les facteurs qui permettent d’obtenir les meilleurs rendements.</li>
</ol>

<h2>Causes de non performance.</h2>
<h3>Équipements de production.</h3>
<p>Les causes de sous-utilisation et les pertes de rendement des moyens de production peuvent être distinguées en deux catégories :</p>
<h4>La machine ne fonctionne pas</h4>
<ul>
	<li><strong>Arrêts programmés</strong> : Réunions, pause repas, nettoyage, etc.</li>
	<li><strong>Arrêts prévisibles</strong> Manques de matière, congés de personnel, essais de nouvelles productions ou techniques, rupture d’alimentation des énergies, blocage par l’aval, maintenance prévisionnelle, etc.</li>
	<li><strong>Arrêts non programmés</strong> : Pannes, règlages, changement de série, absence personnel, etc.</li>
</ul>
<h4>La machine fonctionne</h4>
<ul>
	<li><strong>Performance non optimale</strong> : Micro-arrêts, sous-vitesse, démarrage de production, etc..</li>
	<li><strong>Non-qualité</strong>.</li>
</ul>

<h3>Consommation des matières.</h3>
<p>La non-qualité intervenant à différents stade d'une production ainsi que les consommations inutiles de matières dues aux défauts du processus de fabrication ou aux dysfonctionnements organisationnels peuvent être catégorisés :</p>
<ul>
	<li><strong>Gestion de matière</strong> : Localisées entre les lieux de stockage et les lieux d’utilisation, elle se traduisent généralement par les écarts d’inventaire dont on connait rarement l’origine. Il s’agit de quantités reçues non conformes, de qualité dégradée mais acceptée de par l’impossibilité d’un réapprovisionnement rapide, de dégradations générées par les conditions de stockage et moyens de manutention, d’égarements ici ou là, enfin des obsolètes.</li>
	<li><strong>Technique</strong> : Les manques de matières sont liés à des choix dépendant des critères d’achats ou de la nature des processus de fabrication : Chutes de découpes, copeaux, évaporations... admis dans les standards de consommation mais insuffisamment remises en cause, etc.</li>
	<li><strong>Transformation</strong> : Déchets et rebuts de démarrage ou en cours de fabrication, gaspillages, délaissés au fond des contenants, dégradations commises lors des manipulations aux postes et manutentions interpostes, quantités excédentaires aux besoins non réintégrées au magasin, etc.</li>
</ul>

<h3>Main d’œuvre.</h3>
<p>Les pertes de temps peuvent être regroupées en quatre catégories :</p>
<ul>
	<li><strong>Social</strong> : Il s’agit d’abord de l’absentéisme, toutes causes confondues. Mais également des temps consacrés à des activités reconnues : Information, réunion ou formation.</li>
	<li><strong>Occupation</strong> : Le personnel est sur poste mais un dysfonctionnement l’empêche de produire : Attente d’approvisionnement, rupture intempestive du flux, absence d’instructions, attente d’un changement d’outil ou d’un dépannage, mauvais équilibrage d’une ligne, sureffectif par rapport à la charge…</li>
	<li><strong>Efficacité</strong> : Le personnel est occupé mais sa performance est éloignée de l’objectif : Aptitudes inadaptées, motivation douteuse, lenteur gestuelle, irrespect des modes opératoires, conditions de travail éprouvantes ou anormales, non-qualité, reprises…</li>
	<li><strong>Méthodologie</strong> : Ce sont les gains potentiels, donc des pertes induites, qui nécessitent une revue critique des modes opératoires et agencements des postes. En effet, la constitution d’un poste de travail est en principe la situation optimisée le jour où on l’a conçu. Or rien ne permet d’affirmer que l’on a réuni ce jour-là les meilleures conditions d’organisation.</li>
</ul>

<h3>Gestion des flux et délais.</h3>
<p>La philosophie du <span class="tag--info">Juste-à-temps</span> vise le zéro stock tout en livrant la bonne quantité, à la bonne qualité et dans les délais. La réalité se voit cependant confronté à des pertes de temps pouvant être classées par :

<ul>
	<li><strong>Attentes</strong> : Stocks et en-cours de fabrication, confort que s’accordent les maillons successifs du processus de fabrication pour faire face aux dysfonctionnements et à l’insuffisante flexibilité : Taux élevé d’indisponibilité des équipements, inadaptation de la capacité à la charge, polyvalence insuffisante du personnel, tardive anticipation des approvisionnements, changements d’outils inorganisés…</li>
	<li><strong>Taille des lots</strong> : Plus la taille des ordres de fabrication et des contenants est importante, plus le temps de séjour est long à chaque étape du processus de fabrication. L’idéal en termes de délai est le flux en pièce à pièce de poste en poste.</li>
	<li><strong>Non fiabilité</strong> : Elle concerne essentiellement la multitude de micro-arrêts dans le fonctionnement des équipements, la non-conformité des approvisionnements, la difficulté de maitriser la qualité, les retouches et reprises, entrainant autant de retards dans l’écoulement du flux.</li>
</ul>

<h2>Avantages de la méthode TPM, résultats directs</h2>
<ul>
	<li>Instauration de maintenance et de la propreté sur les postes de travail et leur environnement.</li>
	<li>Mise en place d’une organisation visuelle.</li>
	<li>Élimination des gaspillages de temps et de matière.</li>
	<li>Amélioration du suivi des équipements.</li>
	<li>Amélioration Générale de l’environnement.</li>
</ul>

<h2>Avantages de la méthode TPM en termes de comportements.</h2>
<ul>
	<li>Communiquer et associer tout le monde et pas seulement les responsables.</li>
	<li>Remettre en cause ses habitudes.</li>
	<li>Maintenir en bon état.</li>
	<li>Rendre pérennes les résultats acquis et progresser en permanence.</li>
	<li>Respecter des engagements et des règles collectives.</li>
</ul>

<h2>Réussir sa méthode TPM.</h2>

<p>La démarche TPM, Total Productive Maintenance permet d'améliorer la capacité de production des moyens techniques.</p>
<p>Pour ce faire, il faut éviter les pertes et gaspillages et viser le  “zéro accident, zéro défaut et zéro panne”, en allant sur le terrain (Gemba) et en regardant concrètement (Gembutsu) ce qui se passe tout au long du cycle de vie du système de production.</p>
<p>Le TPM implique la participation de tous les collaborateurs, pas uniquement ceux liés à la production mais aussi la conception de l’exploitation, le développement, les services marketing et administratifs, et ceci, à tous les niveaux hiérarchiques, des dirigeants aux opérateurs.</p>
<p>Le “zéro pertes” est atteint en engageant des démarches d’amélioration par petits groupes. Tous les collaborateurs doivent trouver un avantage dans la démarche TPM et ce n’est pas que l’aspect financier !</p>
<p>Un des avantages irréfutables pour les opérateurs est que “ça fait du bien d’enlever le caillou que l’on a dans sa chaussure”.</p>
<p>Ce caillou peut aussi bien être la routine que l’on perçoit, l’attitude de la hiérarchie, le sentiment de ne pas être entendu que les difficultés que l’on rencontre pour réaliser son travail dans des conditions normales (difficultés dues au matériel, aux matières premières, à l’organisation, problèmes d'ergonomie).</p>
<p>Ce caillou peut être aussi le besoin de se sentir utile, de pouvoir résoudre les problèmes rencontrés.</p>
<p>Grâce à la TPM, l'équipement est préservé et la production optimisée.</p>


